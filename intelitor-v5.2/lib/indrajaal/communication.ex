  @doc """
  Bulk creates multiple communication messages.
  """
  @spec bulk_create_communication(list()) :: {:ok, list()} | {:error, term()}
  def bulk_create_communication(communication_list) when is_list(communication_list) do
    # Process bulk communication creation
    results =
      Enum.map(communication_list, fn attrs ->
        case create_message(attrs) do
          {:ok, message} -> message
          {:error, _} = error -> error
        end
      end)

    {successes, errors} =
      Enum.split_with(results, fn
        {:error, _} -> false
        _ -> true
      end)

    if Enum.empty?(errors) do
      Logger.info("Bulk communication creation completed", count: length(successes))
      {:ok, successes}
    else
      Logger.error("Bulk communication creation failed", error_count: length(errors))
      {:error, "Bulk creation failed with #{length(errors)} errors"}
    end
  end

  @doc """
  Imports communication from external __data source.
