  @doc """
  Lists communication with pagination and filtering.

  Enforces tenant isolation and access control.
  """
  @spec list_communication(any()) :: any()
  def list_communication(opts \\ []) do
    # Agent: worker-2 processes query
    # Helper-3 enforces tenant isolation

    user = Keyword.get(opts, :user)

    # TDG stub mode: if no user context provided, return empty list for testing
    if is_nil(user) do
      {:ok, []}
    else
      tenant_id = Keyword.get(opts, :tenant_id)
      page = Keyword.get(opts, :page, 1)
      page_size = Keyword.get(opts, :page_size, 20)
      search = Keyword.get(opts, :search)
      filters = Keyword.get(opts, :filters, %{})

      # STAMP Safety: Validate query parameters
      with :ok <- validate_query_params(page, page_size),
           :ok <- validate_user_access(user, :list, Communication) do
        initial_query =
          from(item in Message,
            where: item.tenant_id == ^tenant_id
          )

        base_query =
          initial_query
          |> apply_search(search)
          |> apply_filters(filters)
          |> order_by([item], desc: item.inserted_at)

        _total = Repo.aggregate(base_query, :count)

        items =
          base_query
          |> limit(^page_size)
          |> offset(^((page - 1) * page_size))
          |> Repo.all()

        {:ok, items}
      end
    end
  end

