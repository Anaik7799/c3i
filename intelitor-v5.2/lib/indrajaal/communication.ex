  def import_communication(data) when is_map(data) do
    # Process communication import data
    communication_data = Map.get(data, "communication", [])

    case bulk_create_communication(communication_data) do
      {:ok, created_communication} ->
        Logger.info("Communication import completed", count: length(created_communication))
        {:ok, %{imported: length(created_communication), failed: 0}}

      {:error, reason} ->
        Logger.error("Communication import failed", reason: reason)
        {:error, reason}
    end
  end

