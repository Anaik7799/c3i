  defp extract_module_name(file_path) do
    case File.read(file_path) do
      {:ok, content} ->
        case Regex.run(~r/defmodule\s+([\w\.]+)/, content) do
          [_, module_name] -> module_name
          _ -> "UnknownModule"
        end
      {:error, _} -> "UnknownModule"
    end
  end

