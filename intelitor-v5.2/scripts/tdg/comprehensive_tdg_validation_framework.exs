    },
    "TDG-008" => %{
      name: "Mock and Stub Validation",
      description: "Mocks MUST accurately represent real dependencies",
      validation_method: :validate_mock_accuracy,
      critical_level: :medium,
      tolerance: 10.0
    }
  }

  @ai_code_patterns [
    # Claude AI patterns
    ~r/Generated by Claude/i,
    ~r/AI Assistant/i,
    ~r/@doc.*AI-generated/i,
    
    # Common AI code markers
    ~r/TODO.*AI/i,
    ~r/FIXME.*generated/i,
    ~r/AI.*code/i,
    
    # Specific patterns
    ~r/def.*_generated/,
    ~r/defmodule.*Generated/,
    ~r/@moduledoc.*Generated/
  ]

  @test_quality_patterns %{
    good_patterns: [
      ~r/should.*when/i,
      ~r/test.*behavior/i,
      ~r/assert.*result/i,
      ~r/property.*holds/i,
      ~r/check all/
    ],
    bad_patterns: [
      ~r/test.*private/i,
      ~r/assert.*implementation/i,
      ~r/test.*internal/i,
      ~r/mock.*everything/i
    ]
  }

  def main(args \\ []) do
    Logger.info("🧪 TDG Comprehensive Validation Framework")
    Logger.info("📅 Starting at: #{local_timestamp()}")
    
    {_opts, __} = OptionParser.parse!(args, switches: [
      validate_all: :boolean,
      validate_rule: :string,
      analyze_project: :boolean,
      generate_tests: :boolean,
      check_ai_compliance: :boolean,
      report: :boolean
    ])
    
    cond do
      __opts[:validate_all] -> validate_all_tdg_rules()
      __opts[:validate_rule] -> validate_specific_rule(__opts[:validate_rule])
      __opts[:analyze_project] -> analyze_project_tdg_compliance()
      __opts[:generate_tests] -> generate_missing_tests()
      __opts[:check_ai_compliance] -> check_ai_code_compliance()
      __opts[:report] -> generate_tdg_report()
      true -> display_tdg_dashboard()
    end
  end

  defp display_tdg_dashboard do
    IO.puts("""
    
    🧪 TDG COMPREHENSIVE VALIDATION DASHBOARD
    ═════════════════════════════════════════════════════════════════════
    
    📊 TDG Compliance Overview:
    """)
    
    Enum.each(@tdg_validation_rules, fn {id, rule} ->
      status = get_rule_status(id)
      compliance = get_rule_compliance(id)
      
      IO.puts("#{id}: #{rule.name}")
      IO.puts("  📋 Description: #{rule.description}")
      IO.puts("  🎯 Critical Level: #{rule.critical_level}")
      IO.puts("  📊 Current Status: #{format_status(status)}")
      IO.puts("  ✅ Compliance: #{format_compliance(compliance)}%")
      IO.puts("  🔧 Method: #{rule.validation_method}")
      IO.puts("")
    end)
    
    overall_compliance = calculate_overall_compliance()
    ai_code_analysis = analyze_ai_code_coverage()
    
    IO.puts("""
    🎯 OVERALL TDG COMPLIANCE: #{format_overall_compliance(overall_compliance)}
    
