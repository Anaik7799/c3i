  defp validate_specific_rule(rule_id) do
    case Map.get(@tdg_validation_rules, rule_id) do
      nil ->
        Logger.error("❌ Unknown TDG rule ID: #{rule_id}")
        Logger.error("Available rules: #{Map.keys(@tdg_validation_rules) |> Enum.join(", ")}")
      rule ->
        Logger.info("🔍 Validating #{rule_id}: #{rule.name}")
        
        case validate_rule(rule_id, rule) do
          {:ok, result} ->
            Logger.info("✅ #{rule_id} PASSED: #{result.message}")
            if result[:details] do
              Logger.info("📊 Details: #{inspect(result.details)}")
            end
          {:error, result} ->
            Logger.error("❌ #{rule_id} FAILED: #{result.message}")
            if result[:recommendations] do
              Logger.info("💡 Recommendations:")
              Enum.each(result.recommendations, fn rec ->
                Logger.info("  - #{rec}")
              end)
            end
        end
    end
  end

