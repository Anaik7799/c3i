      #{generate_next_actions(overall_compliance, ai_code_analysis)}
    """)
  end

  defp validate_all_tdg_rules do
    Logger.info("🔍 Validating All TDG Rules")
    Logger.info("═" <> String.duplicate("=", 70))
    
    _results = Enum.map(@tdg_validation_rules, fn {id, rule} ->
      Logger.info("Validating #{id}: #{rule.name}")
      
      case validate_rule(id, rule) do
        {:ok, result} ->
          Logger.info("  ✅ #{id} PASSED: #{result.message}")
          {id, :passed, result}
        {:error, result} ->
          Logger.error("  ❌ #{id} FAILED: #{result.message}")
          {id, :failed, result}
      end
    end)
    
    passed = Enum.count(results, fn {_, status, _} -> status == :passed end)
    failed = Enum.count(results, fn {_, status, _} -> status == :failed end)
    
    Logger.info("\n📊 TDG VALIDATION SUMMARY:")
    Logger.info("  ✅ Passed: #{passed}/#{length(results)}")
    Logger.info("  ❌ Failed: #{failed}/#{length(results)}")
    Logger.info("  📈 Success Rate: #{Float.round(passed / length(results) * 100, 1)}%")
    
    if failed > 0 do
      Logger.error("\n🚨 CRITICAL: #{failed} TDG rule violations detected!")
      Logger.error("System is NOT compliant with TDG methodology __requirements")
      
      failed_rules = Enum.filter(results, fn {_, status, _} -> status == :failed end)
      Enum.each(failed_rules, fn {id, _, result} ->
        Logger.error("  ❌ #{id}: #{result.message}")
        if result[:recommendations] do
          Enum.each(result.recommendations, fn rec ->
            Logger.info("    💡 Recommendation: #{rec}")
          end)
        end
      end)
    else
      Logger.info("\n✅ SUCCESS: All TDG rules validated!")
      Logger.info("System is FULLY compliant with TDG methodology __requirements")
    end
    
    # Save validation report
    save_validation_report(results)
    
    {if(failed == 0, do: :success, else: :failure), results}
  end

  defp validate_rule(_id, rule) do
    case rule.validation_method do
      :validate_test_first_compliance -> validate_test_first_compliance()
      :validate_ai_code_testing -> validate_ai_code_testing()
      :validate_dual_property_testing -> validate_dual_property_testing()
      :validate_coverage_compliance -> validate_coverage_compliance()
      :validate_test_quality -> validate_test_quality()
      :validate_refactoring_safety -> validate_refactoring_safety()
      :validate_integration_testing -> validate_integration_testing()
      :validate_mock_accuracy -> validate_mock_accuracy()
      _ -> {:error, %{message: "Unknown validation method", recommendations: []}}
    end
  end

  # TDG-001: Test-First Requirement
  defp validate_test_first_compliance do
    Logger.info("    🧪 Validating test-first compliance for AI-generated code")
    
    ai_files = find_ai_generated_files()
    test_files = find_corresponding_test_files(ai_files)
    
    test_first_compliance = calculate_test_first_compliance(ai_files, test_files)
    
    if test_first_compliance >= 95.0 do
      {:ok, %{
        message: "Test-first compliance validated - #{test_first_compliance}% compliance",
        details: %{ai_files: length(ai_files), test_files: length(test_files)},
        recommendations: []
      }}
    else
      {:error, %{
        message: "Test-first compliance FAILED - #{test_first_compliance}% compliance (need >= 95%)",
        details: %{ai_files: length(ai_files), test_files: length(test_files)},
        recommendations: [
          "Write tests before generating new AI code",
          "Add tests for existing AI-generated code without tests",
          "Implement pre-generation test validation hooks"
        ]
      }}
    end
  end

  # TDG-002: AI Code Generation Compliance
  defp validate_ai_code_testing do
    Logger.info("    🤖 Validating AI-generated code testing compliance")
    
    ai_files = find_ai_generated_files()
    untested_files = find_untested_ai_files(ai_files)
    
    if length(untested_files) == 0 do
      {:ok, %{
        message: "AI code testing compliance validated - all #{length(ai_files)} files have tests",
        details: %{total_files: length(ai_files), untested: 0},
        recommendations: []
      }}
    else
      {:error, %{
        message: "AI code testing compliance FAILED - #{length(untested_files)} untested files",
        details: %{total_files: length(ai_files), untested: length(untested_files), files: untested_files},
        recommendations: [
          "Create comprehensive tests for all untested AI-generated files",
          "Implement automated test generation for AI code",
          "Add pre-commit hooks to pr__event untested AI code"
        ]
      }}
    end
  end

  # TDG-003: Dual Property Testing Requirement
  defp validate_dual_property_testing do
    Logger.info("    🔄 Validating dual property testing implementation")
    
    critical_files = find_critical_ai_files()
    dual_property_files = find_dual_property_test_files(critical_files)
    
    compliance = calculate_dual_property_compliance(critical_files, dual_property_files)
    
    if compliance >= 80.0 do
      {:ok, %{
        message: "Dual property testing validated - #{compliance}% compliance",
        details: %{critical_files: length(critical_files), dual_property: length(dual_property_files)},
        recommendations: []
      }}
    else
      {:error, %{
        message: "Dual property testing FAILED - #{compliance}% compliance (need >= 80%)",
        details: %{critical_files: length(critical_files), dual_property: length(dual_property_files)},
        recommendations: [
          "Implement PropCheck property tests for critical functions",
          "Add ExUnitProperties tests alongside PropCheck",
          "Create property test examples and documentation"
        ]
      }}
    end
  end

  # TDG-004: Test Coverage Requirement
  defp validate_coverage_compliance do
    Logger.info("    📊 Validating test coverage compliance")
    
    coverage_data = analyze_test_coverage()
    
    if coverage_data.overall_coverage >= 95.0 do
      {:ok, %{
        message: "Test coverage compliance validated - #{coverage_data.overall_coverage}% coverage",
        details: coverage_data,
        recommendations: []
      }}
    else
      {:error, %{
        message: "Test coverage compliance FAILED - #{coverage_data.overall_coverage}% coverage (need >= 95%)",
        details: coverage_data,
        recommendations: [
          "Add tests for uncovered functions and modules",
          "Focus on edge cases and error conditions",
          "Implement integration tests for complex workflows",
          "Review and improve existing test quality"
        ]
      }}
    end
  end

  # TDG-005: Test Quality Validation
  defp validate_test_quality do
    Logger.info("    🎯 Validating test quality metrics")
    
    test_files = find_all_test_files()
    quality_analysis = analyze_test_quality(test_files)
    
    if quality_analysis.quality_score >= 85.0 do
      {:ok, %{
        message: "Test quality validated - #{quality_analysis.quality_score}% quality score",
