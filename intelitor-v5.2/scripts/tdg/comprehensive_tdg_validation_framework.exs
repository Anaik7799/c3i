      AI-Generated Files Detected: #{ai_code_analysis.files_detected}
      Files with Tests: #{ai_code_analysis.files_with_tests}
      Coverage Compliance: #{ai_code_analysis.coverage_compliance}%
      Dual Property Testing: #{ai_code_analysis.dual_property_compliance}%
    
    🔧 Available Commands:
      --validate-all        Validate all TDG rules comprehensively
      --validate-rule       Validate specific TDG rule (e.g., TDG-001)
      --analyze-project     Analyze project TDG compliance
      --generate-tests      Generate missing tests for AI code
      --check-ai-compliance Check AI-generated code compliance
      --report              Generate comprehensive TDG report
    
