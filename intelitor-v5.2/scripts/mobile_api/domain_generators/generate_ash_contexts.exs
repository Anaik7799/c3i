      @foreign_key_type :binary_id

      schema "#{domain.name}" do
        field :name, :string
        field :description, :string
        field :active, :boolean, default: true
        field :metadata, :map, default: %{}

        # Multi-tenant isolation
        field :__tenant_id, :binary_id

        # Audit fields
