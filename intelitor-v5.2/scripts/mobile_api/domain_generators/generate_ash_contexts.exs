  @spec generate_all() :: any()
  def generate_all do
    IO.puts("🏗️ Starting Ash __context generation for #{length(@domains)} domains...

    # Create directories
    ensure_directories()

    # Log start
    log_start()

    # Generate __contexts in parallel by worker
    @domains
    |> Enum.group_by(& &1.worker)
    |> Enum.map(fn {worker, domains} ->
      Task.async(fn -> generate_worker_contexts(worker, domains) end)
    end)
    |> Task.await_many(60_000)

    # Generate shared modules
    generate_shared_modules()

    IO.puts("✅ Ash __context generation complete!")
    log_completion()
  end

