        #{generate_domain_fields(domain.name)}

        timestamps()
      end

      @doc false
  @spec changeset(any(), any()) :: any()
      def changeset(item, _attrs) do
        item
        |> cast(_attrs, [:name, :description, :active, :metadata])
        |> validate_required([:name])
        |> validate_length(:name, min: 1, max: 255)
        |> validate_length(:description, max: 1000)
        |> #{generate_domain_validations(domain.name)}
      end
    end
    """

    resource_file = "lib/indrajaal/#{domain.name}/#{domain.singular}.ex"
