        changed_files = get_changed_files()

        if Enum.empty?(changed_files) do
          IO.puts("✅ No changes detected - compilation skipped")
        else
          IO.puts("📝 Changed files: #{Enum.count(changed_files)}")

          # Step 2: Compile only changed modules
          compile_incremental(changed_files)
        end
      end

  @spec get_changed_files() :: any()
      defp get_changed_files do
        # Simple timestamp-based change detection
        case System.cmd("find",
    ["lib", "-name", "*.ex", "-newer", "_build/.compile_timestamp"], stderr_to_stdout: true) do
          {output, 0} ->
            output
            |> String.split("\\n", trim: true)
            |> Enum.filter(&String.ends_with?(&1, ".ex"))
          _ ->
            # If no timestamp file, consider all files changed
            Path.wildcard("lib/**/*.ex")
        end
      end

  @spec compile_incremental(term()) :: term()
      defp compile_incremental(files) do
        # Touch timestamp
        File.mkdir_p!("_build")
        File.touch!("_build/.compile_timestamp")

        # Compile with optimizations
        System.cmd("mix", ["compile", "--force"], env: [
          {"MIX_ENV", "dev"},
          {"ERL_COMPILER_OPTIONS", "[compressed,{inline_size,24}]"}
        ])
      end
    end

    FastCompile.run()
    """

