  @spec perform_health_checks() :: any()
  defp perform_health_checks do
    IO.puts("\n🏥 Phase 5: Health Checks")
    IO.puts("=" <> String.duplicate("=", 40))

    # Check table count
    IO.puts("📋 Verifying __database schema...")

    case System.cmd(
           "psql",
           [
             "-h",
             "localhost",
             "-p",
             "5433",
             "-U",
             "postgres",
             "-d",
             "indrajaal_dev",
             "-c",
             "SELECT count(*) as table_count FROM information_schema.tables WHERE table_schema = 'public';",
             "-t"
           ],
           stderr_to_stdout: true
         ) do
      {output, 0} ->
        table_count = String.trim(output)
        IO.puts("✅ Database tables: #{table_count}")

        case Integer.parse(table_count) do
          {count, _} when count >= 130 ->
            IO.puts("✅ Sufficient tables for all domains")

          {count, _} ->
            IO.puts("⚠️  Table count (#{count}) may be incomplete")
        end

      {output, _} ->
        IO.puts("❌ Database health check failed: #{String.trim(output)}")
    end

    # Check multi-tenant setup
    IO.puts("📋 Verifying multi-tenant configuration...")

    case System.cmd(
           "psql",
           [
             "-h",
             "localhost",
             "-p",
             "5433",
             "-U",
             "postgres",
             "-d",
             "indrajaal_dev",
             "-c",
             "SELECT count(DISTINCT table_name) as tenant_tables FROM information_schema.columns WHERE column_name = '__tenant_id' AND table_schema = 'public';",
             "-t"
           ],
           stderr_to_stdout: true
         ) do
      {output, 0} ->
        tenant_count = String.trim(output)
        IO.puts("✅ Multi-tenant tables: #{tenant_count}")

      {output, _} ->
        IO.puts("⚠️  Multi-tenant check: #{String.trim(output)}")
    end

    # Check Oban tables
    IO.puts("📋 Verifying Oban background job system...")

    case System.cmd(
           "psql",
           [
             "-h",
             "localhost",
             "-p",
             "5433",
             "-U",
             "postgres",
             "-d",
             "indrajaal_dev",
             "-c",
             "SELECT EXISTS(SELECT FROM information_schema.tables WHERE table_name = 'oban_jobs');",
             "-t"
           ],
           stderr_to_stdout: true
         ) do
      {output, 0} ->
        if String.trim(output) == "t" do
          IO.puts("✅ Oban job system configured")
        else
          IO.puts("❌ Oban tables missing")
        end

      {output, _} ->
        IO.puts("⚠️  Oban check: #{String.trim(output)}")
    end

    # Test compilation
    IO.puts("📋 Testing project compilation...")

    case System.cmd("mix", ["compile", "--warnings-as-errors"], stderr_to_stdout: true) do
      {output, 0} ->
        IO.puts("✅ Compilation successful")

      {output, _} ->
        if String.contains?(output, "cannot be done atomically") do
          IO.puts("⚠️  Atomic operation warnings detected")
          IO.puts("   Run: elixir scripts/maintenance/fix_atomic_warnings.exs")
        else
          IO.puts("⚠️  Compilation issues: #{String.trim(output)}")
        end
    end

    IO.puts("✅ Health checks completed")
  end

