  @spec validate_environment() :: any()
  defp validate_environment do
    IO.puts("\n🔍 Phase 1: Environment Validation")
    IO.puts("=" <> String.duplicate("=", 40))

    # Check if in devenv shell
    case System.get_env("DEVENV_ROOT") do
      nil ->
        IO.puts("❌ ERROR: Must run in devenv shell")
        IO.puts("   Solution: Run 'devenv shell' first")
        System.halt(1)

      _ ->
        IO.puts("✅ devenv shell environment detected")
    end

    # Check PostgreSQL connection
    case System.cmd(
           "psql",
           [
             "-h",
             "localhost",
             "-p",
             "5433",
             "-U",
             "postgres",
             "-d",
             "postgres",
             "-c",
             "SELECT 1;"
           ],
           stderr_to_stdout: true
         ) do
      {_, 0} ->
        IO.puts("✅ PostgreSQL connection successful")

      {error, _} ->
        IO.puts("❌ ERROR: PostgreSQL connection failed")
        IO.puts("   Error: #{error}")
        IO.puts("   Solution: Check devenv.nix PostgreSQL configuration")
        System.halt(1)
    end

    # Check Mix project
    if File.exists?("mix.exs") do
      IO.puts("✅ Mix project detected")
    else
      IO.puts("❌ ERROR: mix.exs not found")
      IO.puts("   Solution: Run from project root directory")
      System.halt(1)
    end

    IO.puts("✅ Environment validation completed")
  end

