  @spec run() :: any()
  def run do
    IO.puts("🔧 Fixing all boolean WHERE clauses in migrations...")

    migrations_dir = "priv/repo/migrations"

    Path.wildcard("#{migrations_dir}/*.exs" |> Enum.each(&fix_migration_file/1))

    IO.puts("✅ All boolean WHERE clause fixes complete!")
  end

  @spec fix_migration_file(term()) :: term()
  defp fix_migration_file(filepath) do
    filename = Path.basename(filepath)
    IO.puts("Checking #{filename}...")

    content = File.read!(filepath)

    # Fix escaped quotes in WHERE clauses systematically
    fixed_content =
      content
      |> String.replace(
        ~r/where:\s*"\\\"([^"]+\?)\\\"(\s*=\s*(?:true|false))"/,
        "where: ~s/\"\\1\"\\2/"
      )

    if fixed_content != content do
      File.write!(filepath, fixed_content)
      IO.puts("  ✅ Fixed WHERE clause syntax")
    else
      IO.puts("  ℹ️  No changes needed")
    end
  end
