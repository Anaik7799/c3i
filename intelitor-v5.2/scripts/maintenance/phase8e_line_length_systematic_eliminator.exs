    |> Enum.reject(&String.contains?(&1, "_build"))
    |> Enum.reject(&String.contains?(&1, "deps"))
    |> Enum.reject(&String.contains?(&1, "backups"))
    |> Enum.reject(&String.contains?(&1, "__data"))
    |> Enum.uniq()
  end

  @spec apply_file_line_length_fixes(term()) :: term()
  defp apply_file_line_length_fixes(file_path) do
    try do
      content = File.read!(file_path)
      lines = String.split(content, "\n")

      # Conservative approach: only fix very safe patterns
      _updated_lines =
        Enum.map(lines, fn line ->
          cond do
            # Long comments-safest to fix
            String.contains?(line, "#") and String.length(line) > 90 ->
              fix_comment_line(line)

            # Very long lines with obvious safe breaks
            String.length(line) > 120 ->
              fix_very_long_line(line)

            # Normal case-leave unchanged
            true ->
              line
          end
        end)

      updated_content = Enum.join(updated_lines, "\n")

      if content != updated_content do
        File.write!(file_path, updated_content)
      end
    rescue
      # Skip problematic files safely
      _error -> :ok
    end
  end

  @spec fix_comment_line(term()) :: term()
  defp fix_comment_line(line) do
    # Very conservative comment line fixing
    if String.length(line) > 90 do
      # Simple truncation with continuation marker
      String.slice(line, 0, 85) <> " ..."
    else
      line
    end
  end

  @spec fix_very_long_line(term()) :: term()
  defp fix_very_long_line(line) do
    # Only fix obviously safe cases
    if String.contains?(line, " and ") and String.length(line) > 120 do
      # Replace " and " with "\n    and " for very long lines
      String.replace(line, " and ", "\n    and ", global: false)
    else
      line
    end
  end

