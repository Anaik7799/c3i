  defp needs_uuid_fix?(file_path) do
    content = File.read!(file_path)
    String.contains?(content, "UUID.uuid4()")
  end
  
  defp fix_file(file_path) do
    content = File.read!(file_path)
    
    if String.contains?(content, "UUID.uuid4()") do
      new_content = String.replace(content, "UUID.uuid4()", "Ecto.UUID.generate()")
      File.write!(file_path, new_content)
      IO.puts("✅ Fixed: #{file_path}")
      {:ok, file_path}
    else
      IO.puts("ℹ️  No changes needed: #{file_path}")
      {:ok, file_path}
    end
  end
