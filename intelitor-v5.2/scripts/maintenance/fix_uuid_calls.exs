  def fix_all do
    IO.puts("🔧 Fixing UUID function calls...")
    
    files_to_check = Path.wildcard("lib/**/*.ex")
    
    results = files_to_check
    |> Enum.filter(&needs_uuid_fix?/1)
    |> Enum.map(&fix_file/1)
    
