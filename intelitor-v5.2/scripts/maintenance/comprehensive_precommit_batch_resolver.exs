  defp create_timestamped_backup do
    timestamp = DateTime.utc_now() |> Calendar.strftime("%Y%m%d_%H%M%S")
    backup_dir = "backups/precommit_resolution_#{timestamp}"
    
    File.mkdir_p!(backup_dir)
    
    @critical_files
