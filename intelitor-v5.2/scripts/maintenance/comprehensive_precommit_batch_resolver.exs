        "❌ #{result.file} - Error: #{Map.get(result, :error, "Unknown error")}"
      end
    end)
    |> Enum.join("\n")
  end

  defp format_validation_summary(results) do
    results
    |> Enum.take(10)  # Show first 10 for brevity
    |> Enum.map(fn result ->
      if result.valid do
        "✅ #{Path.basename(result.file)} - Format validation PASSED"
      else
        "❌ #{Path.basename(result.file)} - Needs attention"
      end
    end)
    |> Enum.join("\n")
  end

  defp calculate_overall_success_rate(result_lists) do
    total_operations = Enum.map(result_lists, &length/1) |> Enum.sum()
    successful_operations = result_lists
    |> Enum.flat_map(&(&1))
    |> Enum.count(fn result ->
      Map.get(result, :success, false) or Map.get(result, :valid, false) or Map.get(result, :swept, false)
    end)
    
    if total_operations > 0 do
      Float.round((successful_operations / total_operations) * 100, 1)
    else
      0.0
    end
  end
end

