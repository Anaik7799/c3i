  end

  defp find_all_elixir_files do
    Path.wildcard("**/*.{ex,exs}")
    |> Enum.filter(&File.exists?/1)
  end

  defp sweep_single_file(file_path) do
    try do
      if File.exists?(file_path) and not String.contains?(file_path, "backup") do
        content = File.read!(file_path)
        
        # Check for similar patterns that might exist
        issues_found = detect_similar_issues(content)
        
        if length(issues_found) > 0 do
          fixed_content = apply_sweep_fixes(content, issues_found)
          File.write!(file_path, fixed_content)
          
          %{file: file_path, swept: true, issues_fixed: length(issues_found)}
        else
          %{file: file_path, swept: true, issues_fixed: 0}
        end
      else
        %{file: file_path, swept: false, reason: "skipped"}
      end
    rescue
      _error ->
        %{file: file_path, swept: false, reason: "error"}
    end
  end

  defp detect_similar_issues(content) do
    # Detect various syntax and formatting issues
    issues = []
    
    issues = if String.match?(content, ~r/SOPv5\.1Compliance:/), do: [:compliance_format | issues], else: issues
    issues = if String.match?(content, ~r/Multi\s*-\s*Agent/), do: [:agent_spacing | issues], else: issues
    issues = if String.match?(content, ~r/defmodule\s+[A-Z][a-zA-Z]*\s+[A-Z]/), do: [:module_spacing | issues], else: issues
    
    issues
  end

  defp apply_sweep_fixes(content, issues) do
    Enum.reduce(issues, content, fn issue, acc ->
      case issue do
        :compliance_format -> String.replace(acc, "SOPv5.1Compliance:", "SOPv5.1 Compliance:")
        :agent_spacing -> String.replace(acc, ~r/Multi\s*-\s*Agent/, "Multi-Agent")
        :module_spacing -> Regex.replace(~r/defmodule\s+([A-Z][a-zA-Z]*)\s+([A-Z][a-zA-Z\s]*)\s+do/, acc, "defmodule \\1\\g{2} do")
        _ -> acc
      end
    end)
  end

