  @spec scan_files() :: any()
  defp scan_files do
    banned_patterns = ["docker", "lxc", "lxd", "containerd", "systemd-nspawn"]

    Path.wildcard("**/*.{ex,exs,sh,yml,yaml}")

    |> Enum.reject(fn path -> String.contains?(path,
      ".git/") or String.contains?(path, "_build/") end)
    |> Enum.flat_map(fn file_path ->
      scan_file(file_path, banned_patterns)
    end)
  end

  @spec scan_file(term(), term()) :: term()
  defp scan_file(file_path, banned_patterns) do
    case File.read(file_path) do
      {:ok, content} ->
        content
        |> String.split("\n")
        |> Enum.with_index(1)
        |> Enum.flat_map(fn {line, line_num} ->
          if Enum.any?(banned_patterns, &String.contains?(line, &1)) do
            [{file_path, line_num, line}]
          else
            []
          end
        end)
      _ ->
        []
    end
  end
end

