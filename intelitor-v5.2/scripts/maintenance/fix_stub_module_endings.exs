  def fix_all do
    IO.puts("🔧 Fixing stub module endings...")
    
    stub_files = Path.wildcard("lib/indrajaal/integration/**/*.ex") ++
                 Path.wildcard("lib/stubs/*.ex")
    
    results = Enum.map(stub_files, &fix_stub_file/1)
    
    successful = Enum.count(results, fn {status, _} -> status == :ok end)
    IO.puts("\n✅ Fixed #{successful}/#{length(stub_files)} stub files")
  end
  
  defp fix_stub_file(file_path) do
    if File.exists?(file_path) do
      content = File.read!(file_path)
      
      # Check if file ends with 'end'
      lines = String.split(content, "\n")
      last_non_empty_line = lines
      |> Enum.reverse()
      |> Enum.find(&(String.trim(&1) != ""))
      
      if last_non_empty_line && String.trim(last_non_empty_line) != "end" do
        # Add missing end
        new_content = content <> "\nend\n"
        File.write!(file_path, new_content)
        IO.puts("✅ Fixed: #{file_path}")
        {:ok, file_path}
      else
        IO.puts("ℹ️  OK: #{file_path}")
        {:ok, file_path}
      end
    else
      IO.puts("❌ File not found: #{file_path}")
      {:error, :not_found}
    end
  end
end

