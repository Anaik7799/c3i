  @spec find_elixir_files() :: any()
  defp find_elixir_files do
    Path.wildcard("lib/**/*.ex")
    |> Enum.filter(&File.regular?/1)
  end

  @spec process_file(term()) :: term()
  defp process_file(file_path) do
    content = File.read!(file_path)

    if String.contains?(content, "__require_atomic?") do
      {_new_content, _fixes} = fix_require_atomic_issues(content)

      if fixes > 0 do
        File.write!(file_path, new_content)
        IO.puts("✅ Fixed #{fixes} issues in #{file_path}")
      end

      {fixes, file_path}
    else
      {0, file_path}
    end
  end

  @spec fix_require_atomic_issues(term()) :: term()
  defp fix_require_atomic_issues(content) do
    # Pattern 1: Remove __require_atomic? from CREATE actions
    # Matches: create :action_name do\n  __require_atomic? false
    create_pattern = ~r/(create\s+:\w+\s+do\s*\n\s*)__require_atomic\?\s+(?:true|false)\s*\n/

    {content_after_create, create_fixes} =
      content
      |> fix_pattern(create_pattern, fn full_match, prefix ->
        IO.puts("    Removing __require_atomic? from CREATE action: #{String.trim(f
        prefix
      end)

    # Pattern 2: Keep __require_atomic? false for UPDATE actions with function-base
    # This is already correct, so we just count them
    update_pattern = ~r/update\s+:\w+\s+do[^}]*?__require_atomic\?\s+false/

    update_matches = Regex.scan(update_pattern, content_after_create)

    if length(update_matches) > 0 do
      IO.puts(
        "    Verified #{length(update_matches)} UPDATE actions with __require_atomi
      )
    end

    {content_after_create, create_fixes}
  end

  defp fix_pattern(content, pattern, replacement_fn) do
    matches = Regex.scan(pattern, content, return: :index)

    if length(matches) > 0 do
      # Process matches in reverse order to maintain correct indices
      {new_content, fixes} =
        matches
        |> Enum.reverse()

    |> Enum.reduce({content, 0}, fn [{start, length} | captures], {acc_content, fix_count} ->
          full_match = String.slice(acc_content, start, length)

          # Extract capture groups
          capture_values =
            captures
            |> Enum.map(fn {cap_start, cap_length} ->
              String.slice(acc_content, cap_start, cap_length)
            end)

          replacement = apply(replacement_fn, [full_match | capture_values])

          new_acc =
            String.slice(acc_content, 0, start) <>
              replacement <>
              String.slice(acc_content, start + length, String.length(acc_content))

          {new_acc, fix_count + 1}
        end)

      {new_content, fixes}
    else
      {content, 0}
    end
  end
