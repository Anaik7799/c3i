        deps = String.split(output, "\n", trim: true)
        length(deps) > 10
      end)

    IO.puts("  Found #{length(hubs)} hub modules")
    hubs
  end

  @spec analyze_ash_resources() :: any()
  defp analyze_ash_resources do
    IO.puts("\n  Analyzing Ash resources...")

    # Find all Ash resources
    ash_files =
      Path.wildcard("lib / indrajaal/**/*.ex")
      |> Enum.filter(fn file ->
        content = File.read!(file)

        String.contains?(content, "use Ash.Resource") or
          String.contains?(content, "use Indrajaal.BaseResource")
      end)

    # Analyze each resource
    _resource_analysis =
      Enum.map(ash_files, fn file ->
        content = File.read!(file)

        # Count DSL complexity
        attributes = length(Regex.scan(~r / attribute\s+:/, content))
        relationships = length(Regex.scan(~r / belongs_to|has_many|has_one/, content))
        actions = length(Regex.scan(~r / create|read|update|destroy|action\s+:/, content))
        calculations = length(Regex.scan(~r / calculate\s+:/, content))

        complexity_score = attributes + relationships * 2 + actions * 3 + calculations * 2

        %{
          file: file,
          module: extract_module_name(content),
          attributes: attributes,
          relationships: relationships,
          actions: actions,
          calculations: calculations,
          complexity: complexity_score
        }
      end)

    # Sort by complexity
    complex_resources =
      resource_analysis
      |> Enum.sort_by(& &1.complexity, :desc)
      |> Enum.take(10)

    IO.puts("\n  📊 Most complex Ash resources:")

    Enum.each(complex_resources, fn r ->
      IO.puts("     #{Path.basename(r.file)}-Complexity: #{r.complexity}")
      IO.puts("       (#{r.attributes} attrs, #{r.relationships} rels, #{r.action
    end)

    resource_analysis
  end

