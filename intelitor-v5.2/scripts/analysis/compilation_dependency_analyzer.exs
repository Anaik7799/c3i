    defer_module = """
    defmodule Indrajaal.CompileOptimizer do
      @moduledoc \"\"\"
      Helper to defer compile-time dependencies to runtime.
      \"\"\"

      # Defer module loading until runtime
  @spec load_module(any()) :: any()
      def load_module(module_name) do
        Code.ensure_loaded(module_name)
      end

      # Call functions dynamically to avoid compile-time deps
  @spec call_dynamic(term(), term(), term()) :: term()
      def call_dynamic(module, function, args \\\\ []) do
        apply(module, function, args)
      end
    end
