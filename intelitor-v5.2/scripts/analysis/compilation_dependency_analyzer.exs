  @spec parse_xref_output(term()) :: term()
  defp parse_xref_output(output) do
    output
    |> String.split("\n", trim: true)
    |> Enum.filter(&String.contains?(&1, "→"))
    |> Enum.map(fn line ->
      [from, to] = String.split(line, " → ")
      {String.trim(from), String.trim(to)}
    end)
  end

  @spec extract_module_name(term()) :: term()
  defp extract_module_name(content) do
    case Regex.run(~r / defmodule\s+([\w.]+)/, content) do
      [_, module] -> module
      _ -> "Unknown"
    end
  end
