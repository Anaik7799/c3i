    ]

    hubs =
      Enum.filter(hub_candidates, fn module ->
        {output, _} =
          System.cmd(
            "mix",
            [
              "xref",
              "graph",
              "--sink",
              "lib/" <>
                String.downcase(String.replace(module, ".", "/")) <> ".ex",
              "--only-nodes"
