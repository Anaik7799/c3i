  @spec analyze_compile_time_dependencies() :: any()
  defp analyze_compile_time_dependencies do
    IO.puts("\n  Running mix xref graph analysis...")

    # Get compile-time dependencies
    {output, _} =
      System.cmd("mix", ["xref", "graph", "--label", "compile", "--format", "plain"],
        env: [{"MIX_ENV", "dev"}],
        stderr_to_stdout: true
      )

    # Parse dependencies
    deps = parse_xref_output(output)

    # Save graph for visualization
    File.write!("compilation_deps_graph.txt", output)

    # Find modules with most compile-time dependencies
    dep_counts =
      Enum.reduce(deps, %{}, fn {from, to}, acc ->
        Map.update(acc, from, [to], &[to | &1])
      end)

    # Report top offenders
    top_deps =
      dep_counts
      |> Enum.sort_by(fn {_, deps} -> -length(deps) end)
      |> Enum.take(10)

    if length(top_deps) > 0 do
      IO.puts("\n  📌 Top compile-time dependency sources:")

      Enum.each(top_deps, fn {module, deps} ->
        IO.puts("     #{module} → #{length(deps)} dependencies")
      end)
    end

    deps
  end

  @spec detect_dependency_cycles() :: any()
  defp detect_dependency_cycles do
    IO.puts("\n  Detecting dependency cycles...")

    {output, _} =
      System.cmd("mix", ["xref", "graph", "--label", "compile", "--cycles"],
        env: [{"MIX_ENV", "dev"}],
        stderr_to_stdout: true
      )

    cycles =
      if String.contains?(output, "No cycles found") do
        []
      else
        String.split(output, "\n", trim: true)
        |> Enum.filter(&String.contains?(&1, "→"))
      end

    if length(cycles) > 0 do
      IO.puts("  ⚠️  Found #{length(cycles)} dependency cycles!")

      Enum.each(Enum.take(cycles, 5), fn cycle ->
        IO.puts("     #{cycle}")
      end)
    else
      IO.puts("  ✅ No dependency cycles found")
    end

    cycles
  end

  @spec identify_hub_modules() :: any()
  defp identify_hub_modules do
    IO.puts("\n  Identifying hub modules...")

