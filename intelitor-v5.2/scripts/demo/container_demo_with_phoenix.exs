  @spec setup_container_environment() :: any()
  defp setup_container_environment do
    IO.puts """

    🔧 Phase 1: Container Environment Setup
    ======================================
    """

    # Enable PHICS integration
    System.put_env("PHICS_ENABLED", "true")
    System.put_env("CONTAINER_ENFORCEMENT", "true")

    # Create PHICS marker files
    File.touch(".phics")
    File.write(".phics", "PHICS_ENABLED=true\nCONTAINER_MODE=active\nPHOENIX_INTEGRATION=enabled")

    IO.puts "✅ PHICS Integration: ENABLED"
    IO.puts "✅ Container Enforcement: ACTIVE"
    IO.puts "✅ Phoenix Integration: READY"
  end

  @spec start_phoenix_server_in_container() :: any()
  defp start_phoenix_server_in_container do
    IO.puts """

    🚀 Phase 2: Phoenix Server Container Launch
    ===========================================
    """

    # Check if Phoenix server is already running
    phoenix_running = check_phoenix_status()

    if phoenix_running do
      IO.puts "✅ Phoenix Server: Already running in container"
    else
      IO.puts "🔄 Starting Phoenix Server in container..."

      # Start Phoenix server in background container process
      spawn(fn ->
        Logger.info("Starting Phoenix server in container mode")
        System.cmd("mix", ["phx.server"],
          env: [
            {"PHICS_ENABLED", "true"},
            {"MIX_ENV", "dev"},
            {"CONTAINER_MODE", "active"}
          ]
        )
      end)

      # Wait for Phoenix to start
      wait_for_phoenix_startup()
    end
  end

  @spec execute_demo_with_phoenix() :: any()
  defp execute_demo_with_phoenix do
    IO.puts """

    🎬 Phase 3: Demo Execution with Live Phoenix
    ===========================================
    """

    # Validate Phoenix is responsive
    validate_phoenix_endpoints()

    # Execute the continuous demo script
    IO.puts "🚀 Launching SOPv5.1 Continuous Enterprise Demo..."

    demo_args = [
      "scripts/demo/sop_v51_continuous_enterprise_demo.exs",
      "--comprehensive",
      "--enterprise",
      "--monitoring",
      "--phoenix-integration"
    ]

    # Execute demo in parallel with Phoenix
    demo_task = Task.async(fn ->
      System.cmd("elixir", demo_args,
        env: [
          {"PHICS_ENABLED", "true"},
          {"PHOENIX_INTEGRATION", "true"},
          {"CONTAINER_MODE", "active"}
        ]
      )
    end)

    IO.puts "✅ Demo Script: Executing in parallel with Phoenix"
    IO.puts "✅ Phoenix Server: Running and responsive"
    IO.puts "✅ PHICS Integration: Active hot-reloading"

    # Wait for demo completion or monitor indefinitely
    case Task.yield(demo_task, :infinity) do
      {:ok, result} ->
        IO.puts "✅ Demo Completed Successfully: #{inspect(result)}"
      nil ->
        IO.puts "⏳ Demo Running Continuously..."
    end
  end

  @spec monitor_demo_and_phoenix() :: any()
  defp monitor_demo_and_phoenix do
    IO.puts """

    📊 Phase 4: Continuous Monitoring
    =================================
    """

    # Start monitoring processes
    spawn(fn -> phoenix_health_monitor() end)
    spawn(fn -> demo_progress_monitor() end)
    spawn(fn -> container_resource_monitor() end)

    IO.puts "✅ Phoenix Health Monitor: ACTIVE"
    IO.puts "✅ Demo Progress Monitor: ACTIVE"
    IO.puts "✅ Container Resource Monitor: ACTIVE"

    # Keep monitoring running
    :timer.sleep(:infinity)
  end

  # Helper Functions

  @spec check_phoenix_status() :: any()
  defp check_phoenix_status do
    case System.cmd("curl",
    ["-s",
      "-o", "/dev/null", "-w", "%{http_code}", "http://localhost:4000/"], stderr_to_stdout: true) do
      {"200", 0} -> true
      _ -> false
    end
  rescue
    _ -> false
  end

  @spec wait_for_phoenix_startup() :: any()
  defp wait_for_phoenix_startup do
    IO.puts "⏳ Waiting for Phoenix server startup..."

    Enum.reduce_while(1..30, false, fn attempt, _acc ->
      :timer.sleep(2000)  # Wait 2 seconds between attempts

      if check_phoenix_status() do
        IO.puts "✅ Phoenix Server: Started successfully (attempt #{attempt})"
        {:halt, true}
      else
        IO.puts "⏳ Phoenix startup check #{attempt}/30..."
        {:cont, false}
      end
    end)
  end

  @spec validate_phoenix_endpoints() :: any()
  defp validate_phoenix_endpoints do
    IO.puts "🔍 Validating Phoenix endpoints..."

    endpoints = [
      {"Health Check", "http://localhost:4000/health"},
      {"Dashboard", "http://localhost:4000/dashboard"},
      {"API Status", "http://localhost:4000/api/health"}
    ]

    Enum.each(endpoints, fn {name, url} ->
      case System.cmd("curl", ["-s", "-o", "/dev/null", "-w", "%{http_code}", url]) do
        {"200", 0} -> IO.puts "✅ #{name}: Responsive"
        {code, _} -> IO.puts "⚠️  #{name}: HTTP #{code}"
      end
    end)
  end

  @spec phoenix_health_monitor() :: any()
  defp phoenix_health_monitor do
    Stream.interval(30_000)  # Check every 30 seconds
    |> Enum.each(fn _ ->
      if check_phoenix_status() do
        Logger.info("Phoenix Health: ✅ Healthy at #{DateTime.utc_now()}")
      else
        Logger.warning("Phoenix Health: ❌ Unresponsive at #{DateTime.utc_now()}")
      end
    end)
  end

  @spec demo_progress_monitor() :: any()
  defp demo_progress_monitor do
    Stream.interval(60_000)  # Check every minute
    |> Enum.each(fn _ ->
      # Check if demo script is still running
      case System.cmd("pgrep", ["-f", "sop_v51_continuous_enterprise_demo"]) do
        {_pids, 0} -> Logger.info("Demo Progress: ✅ Active at #{DateTime.utc_now()}")
        _ -> Logger.info("Demo Progress: ⏸️  Not running at #{DateTime.utc_now()}")
      end
    end)
  end

  @spec container_resource_monitor() :: any()
  defp container_resource_monitor do
    Stream.interval(120_000)  # Check every 2 minutes
    |> Enum.each(fn _ ->
      # Monitor container resources
      case System.cmd("podman", ["stats", "--no-stream", "--format", "json"]) do
        {output, 0} ->
          Logger.info("Container Resources: #{String.trim(output)}")
        _ ->
          Logger.info("Container Resources: Unable to collect stats")
      end
    end)
  end
