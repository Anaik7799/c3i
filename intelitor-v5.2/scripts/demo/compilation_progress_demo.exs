        file_type <- file_types,
        i <- 1..:rand.uniform(8) do
      "lib/indrajaal/#{domain}#{file_type}/file_#{i}.ex"
    end
    |> Enum.take(45) # Limit to reasonable number for demo
  end

  defp simulate_file_compilation_with_tracking(file, metrics, session_id) do
    start_time = DateTime.utc_now()
    
    # Extract file info
    domain = extract_domain(file)
    subsystem = extract_subsystem(file)
    
    # Simulate compilation time (some files are slower)
    base_time = case subsystem do
      "controllers" -> :rand.uniform(2000) + 800
      "live" -> :rand.uniform(3000) + 1000  
      "components" -> :rand.uniform(1500) + 500
      _ -> :rand.uniform(1000) + 300
    end
    
    # Display current file
    progress = div((metrics.completed + metrics.failed) * 100, metrics.total_files)
    current_file = Path.basename(file)
    
    IO.write("\r⚡ [#{create_progress_bar(progress)}] #{progress}% - #{current_file}")
    
    # Simulate compilation
    :timer.sleep(div(base_time, 20)) # Speed up for demo
    
    end_time = DateTime.utc_now()
    actual_time = DateTime.diff(end_time, start_time, :millisecond) * 20 # Scale back up
    
    # Simulate occasional errors/warnings
    {status, errors, warnings} = simulate_compilation_result(file, domain)
    
    # Update metrics
    updated_metrics = case status do
      :completed -> %{metrics | 
        completed: metrics.completed + 1,
        total_time: metrics.total_time + actual_time,
        warnings: metrics.warnings ++ warnings
      }
      :failed -> %{metrics |
        failed: metrics.failed + 1,
        errors: metrics.errors ++ errors,
        warnings: metrics.warnings ++ warnings  
      }
    end
    
    # Log to simulated session file
    log_entry = %{
      file: file,
      domain: domain,
      subsystem: subsystem,
      status: status,
      compile_time_ms: actual_time,
      errors: errors,
      warnings: warnings,
      timestamp: DateTime.to_string(end_time)
    }
    
    append_to_demo_log(session_id, log_entry)
    
    updated_metrics
  end

  defp simulate_compilation_result(file, domain) do
    # Simulate different success rates by domain
    error_probability = case domain do
      "alarms" -> 0.05      # 5% error rate
      "accounts" -> 0.03    # 3% error rate  
      "video" -> 0.08       # 8% error rate
      _ -> 0.04             # 4% error rate
    end
    
    if :rand.uniform() < error_probability do
      {:failed, generate_sample_errors(file), generate_sample_warnings()}
    else
      warning_probability = 0.15 # 15% warning rate
      warnings = if :rand.uniform() < warning_probability do
        generate_sample_warnings()
      else
        []
      end
      {:completed, [], warnings}
    end
  end

  defp generate_sample_errors(file) do
    error_types = [
      "undefined function #{extract_function(file)}/2",
      "module #{extract_module(file)} is not available",
      "the following clause will never match",
      "variable '__params' is unused"
    ]
    
    Enum.take_random(error_types, :rand.uniform(2) + 1)
  end

  defp generate_sample_warnings do
    warning_types = [
      "variable '__opts' is unused",
      "this clause will never match",
      "deprecated function call"
    ]
    
    if :rand.uniform() < 0.7 do
      Enum.take_random(warning_types, 1)
    else
      []
    end
  end

  defp display_progress_summary(metrics, session_id, start_time) do
    end_time = DateTime.utc_now()
    duration = DateTime.diff(end_time, start_time, :second)
    success_rate = if metrics.completed + metrics.failed > 0 do
      div(metrics.completed * 100, metrics.completed + metrics.failed)
    else
      0
    end
    
    IO.puts("""
    
    
    ✅ COMPILATION PROGRESS SUMMARY
    ═══════════════════════════════════════════════════════════════
    Session: #{session_id}
    Duration: #{duration}s
    
    📊 RESULTS:
       Total Files: #{metrics.total_files}
       Completed: #{metrics.completed}
       Failed: #{metrics.failed}
       Success Rate: #{success_rate}%
    
    ⚡ PERFORMANCE:
       Average Time: #{div(metrics.total_time, max(metrics.completed, 1))}ms
       Files/Second: #{if duration > 0, do: Float.round(metrics.total_files / duration, 2), else: 0}
       Total Errors: #{length(metrics.errors)}
       Total Warnings: #{length(metrics.warnings)}
    
    📋 SESSION LOG: ./__data/tmp/demo_compilation_#{session_id}.jsonl
    ═══════════════════════════════════════════════════════════════
    """)
  end

  ## Helper Functions

  defp extract_domain(file_path) do
    case Regex.run(~r/lib\/indrajaal\/([^\/]+)/, file_path) do
      [_, domain] -> domain
      _ -> "unknown"
    end
  end

  defp extract_subsystem(file_path) do
    cond do
      String.contains?(file_path, "/controllers/") -> "controllers"
      String.contains?(file_path, "/live/") -> "live" 
      String.contains?(file_path, "/components/") -> "components"
      true -> "core"
    end
  end

  defp extract_function(file_path) do
    Path.basename(file_path, ".ex") |> String.replace("_", "")
  end

  defp extract_module(file_path) do
    file_path
    |> String.replace("lib/", "")
    |> String.replace("/", ".")
    |> String.replace(".ex", "")
    |> String.split(".")
    |> Enum.map(&Macro.camelize/1)
    |> Enum.join(".")
  end

  defp create_progress_bar(percentage, width \\ 30) do
    completed = div(percentage * width, 100)
    remaining = width - completed
    String.duplicate("█", completed) <> String.duplicate("░", remaining)
  end

  defp append_to_demo_log(session_id, log_entry) do
    log_dir = "./__data/tmp"
    File.mkdir_p!(log_dir)
    
    log_file = Path.join(log_dir, "demo_compilation_#{session_id}.jsonl")
    json_entry = Jason.encode!(log_entry) <> "\n"
    File.write!(log_file, json_entry, [:append])
  end
