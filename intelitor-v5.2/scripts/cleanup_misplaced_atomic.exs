    IO.puts("🧹 Cleaning up misplaced __require_atomic? false declarations...")

    # Find all Elixir files in lib/indrajaal
    files = Path.wildcard("lib/indrajaal/**/*.ex")

    total_cleaned = 0

    Enum.each(files, fn file_path ->
      content = File.read!(file_path)

      # Remove standalone __require_atomic? false lines that are outside action blo
      # This pattern matches __require_atomic? false that appears after "end\nend"
      pattern1 = ~r/(\nend\n)\n\s*__require_atomic\?\s+false\n?/
      new_content1 = Regex.replace(pattern1, content, "\\1")

      # Also remove any that appear at the very end of files
      pattern2 = ~r/\n\s*__require_atomic\?\s+false\s*$/
      new_content2 = Regex.replace(pattern2, new_content1, "")

      # Remove any that appear outside of action blocks
      pattern3 = ~r/^(\s*)__require_atomic\?\s+false\s*$/m
      new_content3 = Regex.replace(pattern3, new_content2, "")

      if new_content3 != content do
        File.write!(file_path, new_content3)
        IO.puts("✅ Cleaned #{Path.basename(file_path)}")
        total_cleaned = total_cleaned + 1
      end
    end)

    IO.puts("✅ Cleaned #{total_cleaned} files")
