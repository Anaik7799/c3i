    try do
      # Read current document content
      {:ok, original_content} = File.read(doc_path)

      # Apply SOPv5.1 enhancements
      enhanced_content =
        apply_sopv51_documentation_enhancements(original_content, doc_path, category)

      # Create backup of original
      backup_path = "#{doc_path}.sopv51-backup-#{:os.system_time(:second)}"
      File.write!(backup_path, original_content)

      # Write enhanced version
      File.write!(doc_path, enhanced_content)

      Logger.info("SUCCESS: Enhanced: #{doc_path}")
      {:ok, %{document: doc_path, backup: backup_path, enhancements_applied: true}}
    rescue
      error ->
        Logger.error("ERROR: Failed to enhance #{doc_path}: #{inspect(error)}")
        {:error, %{document: doc_path, error: error}}
    end
  end

