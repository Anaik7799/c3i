    |> Enum.filter(& &1.constraint.criticality == :critical)

    high_violations = results.violations
    |> Enum.filter(& &1.constraint.criticality == :high)

    overall_status = cond do
      length(critical_violations) > 0 -> :critical_failure
      length(high_violations) > 2 -> :significant_risk
      length(results.violations) > 0 -> :minor_issues
      length(results.warnings) > 3 -> :monitoring_required
      true -> :all_safe
    end

    %{results | overall_status: overall_status}
  end

