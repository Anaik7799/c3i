      "lib/indrajaal/visitor_management/visit_request.ex",
      "lib/indrajaal/visitor_management/visitor_escort.ex"
    ]

    total_fixed = 0

    Enum.each(files_to_fix, fn file_path ->
      if File.exists?(file_path) do
        fixed_count = fix_file_atomic_warnings(file_path)
        total_fixed = total_fixed + fixed_count

        if fixed_count > 0 do
          IO.puts("✅ Fixed #{fixed_count} actions in #{Path.basename(file_path)}"
        end
      else
        IO.puts("⚠️  File not found: #{file_path}")
      end
    end)

    IO.puts("✅ Fixed #{total_fixed} total actions")
  end

  @spec fix_file_atomic_warnings(term()) :: term()
  defp fix_file_atomic_warnings(file_path) do
    content = File.read!(file_path)

    # Find all update actions that need __require_atomic? false
    pattern = ~r/(update\s+:\w+\s+do\n)(.*?)(\n\s+end)/s

    {new_content, fixes_count} =
      Regex.replace(pattern, content, fn full_match, action_start, action_body, action_end ->
        if String.contains?(action_body, "__require_atomic?") do
          # Already has __require_atomic? declaration
          full_match
        else
          # Check if this action needs atomic false
          needs_atomic_false =
            String.contains?(action_body, "change fn") ||
              String.contains?(action_body, "set_attribute") ||
              String.contains?(action_body, "DateTime.utc_now") ||
              String.contains?(action_body, "arg(") ||
              String.contains?(action_body, "after_action") ||
              String.contains?(action_body, "before_action")

          if needs_atomic_false do
            # Add __require_atomic? false before the end
            updated_body = String.trim_trailing(action_body) <> "\n\n      __require_atomic? false"
            action_start <> updated_body <> action_end
          else
            full_match
          end
        end
      end)
      |> count_fixes()

    if new_content != content do
      File.write!(file_path, new_content)
      fixes_count
    else
      0
    end
  end

  @spec count_fixes(term(), term()) :: term()
  defp count_fixes({new_content, original_content}) do
    original_count = count_require_atomic(original_content)
    new_count = count_require_atomic(new_content)
    {new_content, new_count - original_count}
  end

  @spec count_fixes(term()) :: term()
  defp count_fixes(content), do: {content, 0}

  defp count_require_atomic(content) do
    Regex.scan(~r/__require_atomic\?\s+false/, content) |> length()
  end
end

