    ]

    Enum.each(envs, fn {key, value} ->
      System.put_env(key, value)
      IO.puts("  ✓ #{key}")
    end)
  end

  @spec compile_optimized() :: any()
  defp compile_optimized do
    IO.puts("\n🚀 Compiling with maximum optimizations...")
    IO.puts("  This will take 3-5 minutes on first run...")

    # Clean build
    System.cmd("rm", ["-rf", "_build/dev/lib/indrajaal"])
    System.cmd("mix", ["deps.clean", "ash", "--build"])
    System.cmd("mix", ["deps.clean", "spark", "--build"])

    # Compile dependencies first (already compiled from earlier)
    IO.puts("\n  Step 1/3: Dependencies...")

    System.cmd("mix", ["deps.compile"],
      env: [{"MIX_ENV", "dev"}],
      into: IO.stream(:stdio, :line)
    )

    # Compile protocols separately
    IO.puts("\n  Step 2/3: Protocols...")

    System.cmd("mix", ["compile.protocols"],
      env: [{"MIX_ENV", "dev"}],
      into: IO.stream(:stdio, :line)
    )

    # Compile main project
    IO.puts("\n  Step 3/3: Main project...")

    {_, exit_code} =
      System.cmd("mix", ["compile", "--force"],
        env: [
          {"MIX_ENV", "dev"},
          {"MIX_QUIET", "1"}
        ],
        into: IO.stream(:stdio, :line),
        stderr_to_stdout: true
      )

    if exit_code == 0 do
      IO.puts("\n✅ Compilation successful!")
    else
      IO.puts("\n⚠️  Compilation had issues but continuing...")
    end
  end

