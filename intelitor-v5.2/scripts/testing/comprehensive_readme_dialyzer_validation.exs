  @spec execute_validation_phase(term(), term()) :: term()
  defp execute_validation_phase(phase_name, phase_func) do
    Logger.info("[TARGET] #{phase_name}")
    start_time = System.monotonic_time(:millisecond)

    try do
      result = phase_func.()
      duration = System.monotonic_time(:millisecond) - start_time
      Logger.info("[OK] #{phase_name} completed in #{duration}ms")
      result
    rescue
      error ->
        duration = System.monotonic_time(:millisecond) - start_time
        Logger.error("[ERROR] #{phase_name} failed after #{duration}ms: #{inspect(error)}")
        {:error, error}
    end
  end

