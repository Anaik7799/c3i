  ## Phase Validation and TPS RCA

  defp validate_phase_completion(phase_config, bash_results, dialyzer_results) do
    bash_success_count = Enum.count(bash_results, fn result -> result.status == :success end)

    dialyzer_success_count =
      Enum.count(
        dialyzer_results,
        fn result -> result.status == :success end
      )

    total_bash = length(bash_results)
    total_dialyzer = length(dialyzer_results)

    bash_success_rate = if total_bash > 0, do: bash_success_count / total_bash, else: 1.0

    dialyzer_success_rate =
      if total_dialyzer > 0, do: dialyzer_success_count / total_dialyzer, else: 1.0

    overall_success_rate = (bash_success_rate + dialyzer_success_rate) / 2

    status =
      cond do
        overall_success_rate >= 0.95 -> :success
        overall_success_rate >= 0.80 -> :warning
        true -> :failed
      end

    %{
      status: status,
      bash_success_rate: bash_success_rate,
      dialyzer_success_rate: dialyzer_success_rate,
      overall_success_rate: overall_success_rate,
      recommendations: generate_phase_recommendations(phase_config, status)
    }
  end

