    Logger.info("Level 1 (Symptom): Phase #{phase_config.phase} validation issues")
    Logger.info("Level 2 (Surface Cause): #{inspect(phase_validation.status)}")

    case phase_validation.status do
      :failed ->
        Logger.info("Level 3 (System Behavior): Command execution or integration failures")
        Logger.info("Level 4 (Configuration Gap): Container or Dialyzer configuration issues")

        Logger.info(
          "Level 5 (Design Analysis): README workflow or integration design needs review"
        )

      :warning ->
        Logger.info("Level 3 (System Behavior): Partial success with some integration issues")
        Logger.info("Level 4 (Configuration Gap): Minor configuration or timing issues")
        Logger.info("Level 5 (Design Analysis): Workflow optimization opportunities identified")
    end
  end

