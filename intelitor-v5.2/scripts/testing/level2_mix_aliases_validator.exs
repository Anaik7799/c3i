  def main(args) do
    IO.puts """
    🔍 Level 2: Mix Aliases Configuration Testing - SOPv5.11+STAMP
    =============================================================
    Total Mix Aliases: #{length(@mix_aliases)}
    Test Categories: #{@mix_aliases |> Enum.map(&(&1.category)) |> Enum.uniq() |> length()}
    Framework: TPS 5-Level Analysis + STAMP Safety Validation
    """
    
    case args do
      ["--category", category] -> test_category(String.to_atom(category))
      ["--priority", priority] -> test_priority(String.to_atom(priority))
      ["--safe-only"] -> test_safe_only()
      ["--comprehensive"] -> test_comprehensive()
      ["--report"] -> generate_report()
      ["--help"] -> show_help()
      [] -> test_comprehensive()
      _ -> IO.puts("Invalid arguments. Use --help for usage information.")
    end
  end

  def test_comprehensive do
    IO.puts "\n🚀 Level 2: Comprehensive Mix Aliases Testing"
    IO.puts "============================================="
    
    results = %{
      tested: 0,
      successful: 0,
      failed: 0,
      timeouts: 0,
      help_available: 0,
      failures: [],
      by_category: %{},
      by_priority: %{}
    }
    
    _final_results = Enum.reduce(@mix_aliases, _results, fn alias_info, acc ->
      IO.puts "\n  Testing: mix #{alias_info.name} (#{alias_info.category}/#{alias_info.priority})"
      
      test_result = test_mix_alias(alias_info)
      
      _acc = Map.put(acc, :tested, acc.tested + 1)
      
      acc = case test_result.status do
        :success ->
          IO.puts "    ✅ SUCCESS: #{test_result.message}"
          _acc = Map.put(acc, :successful, acc.successful + 1)
          if test_result.help_available do
            Map.put(acc, :help_available, acc.help_available + 1)
          else
            acc
          end
        :failure ->
          IO.puts "    ❌ FAILED: #{test_result.error}"
          _acc = Map.put(acc, :failed, acc.failed + 1)
          Map.put(acc, :failures, [test_result | acc.failures])
        :timeout ->
          IO.puts "    ⏱️ TIMEOUT: #{test_result.error}"
          _acc = Map.put(acc, :timeouts, acc.timeouts + 1)
          Map.put(acc, :failures, [test_result | acc.failures])
      end
      
      # Track by category
      category_stats = Map.get(acc.by_category, alias_info.category, %{total: 0, success: 0})
      category_stats = %{
        total: category_stats.total + 1,
        success: (if test_result.status == :success, do: category_stats.success + 1, else: category_stats.success)
      }
      acc = put_in(acc, [:by_category, alias_info.category], category_stats)
      
      # Track by priority  
      priority_stats = Map.get(acc.by_priority, alias_info.priority, %{total: 0, success: 0})
      priority_stats = %{
        total: priority_stats.total + 1,
        success: (if test_result.status == :success, do: priority_stats.success + 1, else: priority_stats.success)
      }
      put_in(acc, [:by_priority, alias_info.priority], priority_stats)
    end)
    
    print_comprehensive_summary(final_results)
    save_level2_results(final_results)
  end

  defp test_mix_alias(alias_info) do
    test_args = case alias_info.test_mode do
      :safe -> ["--help"]  # Safe mode: only test help
      :help_only -> ["--help"]  # Help only: conservative testing
      _ -> ["--help"]  # Default to help for safety
    end
    
    task = Task.async(fn ->
      case System.cmd("mix", [alias_info.name | test_args], stderr_to_stdout: true) do
        {output, 0} ->
          help_available = String.contains?(output, alias_info.name) or 
                          String.contains?(output, "Usage") or
                          String.contains?(output, "help")
          {:success, output, help_available}
        {error, exit_code} ->
          {:failure, error, exit_code}
      end
    end)
    
    case Task.yield(task, 10000) do  # 10 second timeout
      {:ok, {:success, output, help_available}} ->
        %{
          alias: alias_info.name,
          category: alias_info.category,
          priority: alias_info.priority,
          status: :success,
          message: "Available and responsive",
          help_available: help_available,
          output_preview: String.slice(output, 0..150)
        }
      {:ok, {:failure, error, exit_code}} ->
        %{
          alias: alias_info.name,
          category: alias_info.category,
          priority: alias_info.priority,
          status: :failure,
          error: String.slice(error, 0..200),
          exit_code: exit_code
        }
      nil ->
        Task.shutdown(task, :brutal_kill)
        %{
          alias: alias_info.name,
          category: alias_info.category, 
          priority: alias_info.priority,
          status: :timeout,
          error: "Command timeout after 10 seconds"
        }
    end
  rescue
    e -> 
      %{
        alias: alias_info.name,
        category: alias_info.category,
        priority: alias_info.priority,
        status: :failure,
        error: "Exception: #{inspect(e)}"
      }
  end

  defp print_comprehensive_summary(results) do
    IO.puts "\n📊 Level 2: Comprehensive Testing Summary"
    IO.puts "========================================="
    IO.puts "✅ Total Tested: #{results.tested}"
    IO.puts "✅ Successful: #{results.successful}"
    IO.puts "❌ Failed: #{results.failed}"
    IO.puts "⏱️ Timeouts: #{results.timeouts}"
    IO.puts "📖 Help Available: #{results.help_available}"
    
    success_rate = if results.tested > 0 do
      Float.round((results.successful / results.tested) * 100, 1)
    else
      0.0
    end
    
    IO.puts "\n🎯 Overall Success Rate: #{success_rate}%"
    
    # Print by category
    IO.puts "\n📋 Results by Category:"
    Enum.each(results.by_category, fn {category, stats} ->
      category_rate = Float.round((stats.success / stats.total) * 100, 1)
      IO.puts "  #{category}: #{stats.success}/#{stats.total} (#{category_rate}%)"
    end)
    
    # Print by priority
    IO.puts "\n⚡ Results by Priority:"
    Enum.each(results.by_priority, fn {priority, stats} ->
      priority_rate = Float.round((stats.success / stats.total) * 100, 1)
      IO.puts "  #{priority}: #{stats.success}/#{stats.total} (#{priority_rate}%)"
    end)
    
    # Print failures
    if length(results.failures) > 0 do
      IO.puts "\n🚨 Failed/Timeout Aliases:"
      Enum.each(results.failures, fn failure ->
        IO.puts "  - #{failure.alias} (#{failure.category}/#{failure.priority}): #{failure.error}"
      end)
    end
    
    # Assessment
    if success_rate >= 80.0 do
      IO.puts "\n✅ Level 2 PASSED - Mix aliases architecture validated"
    else
      IO.puts "\n⚠️ Level 2 NEEDS ATTENTION - Some aliases __require investigation"
    end
  end

