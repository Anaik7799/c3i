  @spec execute_test_suite(term(), term()) :: term()
  defp execute_test_suite(suite_name, tests) do
    Logger.info("🧪 Executing test suite: #{suite_name}")

    _results = Enum.map(tests, fn {test_name, test_func} ->
      Logger.info("  🔍 Running test: #{test_name}")

      case test_func.() do
        {:ok, result} ->
          Logger.info("    ✅ #{test_name}: #{result}")
          {test_name, :passed, result}
        {:error, reason} ->
          Logger.error("    ❌ #{test_name}: #{reason}")
          {test_name, :failed, reason}
      end
    end)

    passed = Enum.count(results, fn {_, status, _} -> status == :passed end)
    total = length(results)

    Logger.info("📊 #{suite_name}: #{passed}/#{total} tests passed")

    if passed < total do
      Logger.warning("⚠️ Some tests failed in #{suite_name}")
      apply_tdg_failure_analysis(suite_name, results)
    end

    results
  end

