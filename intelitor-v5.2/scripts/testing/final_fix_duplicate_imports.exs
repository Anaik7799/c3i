  @spec run() :: any()
  def run do
    IO.puts("Starting final duplicate import fixes...")

    # Find all test files
    test_files = Path.wildcard("test/**/*_test.exs")

    fixed_count =
      test_files
      |> Enum.map(&fix_file/1)
      |> Enum.filter(& &1)
      |> Enum.count()

    IO.puts("\n✅ Fixed #{fixed_count} test files")
    IO.puts("All duplicate imports removed")
  end

  @spec fix_file(term()) :: term()
  defp fix_file(file_path) do
    content = File.read!(file_path)

    lines = String.split(content, "\n")

    # Process lines to remove duplicates
    {fixed_lines, _seen} =
      Enum.reduce(lines, {[], MapSet.new()}, fn line, {acc_lines, seen_imports} ->
        trimmed = String.trim(line)

        if String.starts_with?(trimmed, "import ") do
          if MapSet.member?(seen_imports, trimmed) do
            # Skip duplicate import
            {acc_lines, seen_imports}
          else
            # Keep first occurrence
            {[line | acc_lines], MapSet.put(seen_imports, trimmed)}
          end
        else
          {[line | acc_lines], seen_imports}
        end
      end)

    fixed_content = fixed_lines |> Enum.reverse() |> Enum.join("\n")

    if fixed_content != content do
      IO.puts("Fixed: #{file_path}")
      File.write!(file_path, fixed_content)
      true
    else
      false
    end
  end
end

