  defp process_file(file_path) do
    IO.puts("📄 Processing file: #{file_path}")
    
    case File.read(file_path) do
      {:ok, content} ->
        fixed_content = case Path.basename(file_path) do
          "task_parallelizer.ex" ->
            fix_task_parallelizer_warnings(content)
          "stream_processor.ex" ->
            fix_stream_processor_warnings(content)
          _ ->
            content
        end
        
        File.write!(file_path, fixed_content)
        IO.puts("✅ ULTIMATE fix for #{Path.basename(file_path)}")
        
      {:error, reason} ->
        IO.puts("❌ Error reading #{file_path}: #{reason}")
    end
  end

  defp fix_task_parallelizer_warnings(content) do
    content
    # Fix underscored variable usage by changing function parameter
    |> String.replace(
      "def process(parallelizer, tasks, __opts) do",
      "def process(parallelizer, tasks, opts) do"
    )
    # Fix the usage from _opts to __opts
    |> String.replace(
      "max_concurrency = Map.get(_opts, :max_concurrency, parallelizer.max_concurrency)",
      "max_concurrency = Map.get(__opts, :max_concurrency, parallelizer.max_concurrency)"
    )
    |> String.replace(
      "execute_task(task, _opts)",
      "execute_task(task, __opts)"
    )
  end

  defp fix_stream_processor_warnings(content) do
    content
    # Fix the function parameter to match usage
    |> String.replace(
      "defp process_stream_with_stages(stream, stages, processor, __opts) do",
      "defp process_stream_with_stages(stream, stages, processor, opts) do"
    )
  end
end

