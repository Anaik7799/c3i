  defp calculate_comprehensive_consolidation_rate(results) do
    agents_list =
      case results do
        %{agents: agents} -> agents
        agents when is_list(agents) -> agents
        _ -> []
      end

    total_consolidations = agents_list |> Enum.map& &1.consolidations |> List.flatten()
    successful = Enum.count(total_consolidations, &(&1.status == :consolidated))
    total = length(total_consolidations)

