    ]

    _validation_results =
      Enum.map(shared_modules, fn module_path ->
        case File.exists?(module_path) do
          true ->
            content = File.read!(module_path)
            %{module: module_path, status: :exists, size: String.length(content)}

          false ->
            %{module: module_path, status: :missing, size: 0}
        end
      end)

    total_modules = length(shared_modules)
    existing_modules = Enum.count(validation_results, &(&1.status == :exists))

    IO.puts(
      "   📋 Shared Module Validation: #{existing_modules}/#{total_modules} modules available"
    )

    if existing_modules < total_modules do
      missing_modules = Enum.filter(validation_results, &(&1.status == :missing))
      IO.puts("   ⚠️  Missing Modules:")

      Enum.each(missing_modules, fn result ->
        IO.puts("      - #{result.module}")
      end)
    else
      IO.puts("   ✅ All shared modules validated and ready for deployment")
    end

    validation_results
  end

