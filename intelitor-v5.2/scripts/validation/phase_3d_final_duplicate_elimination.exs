  defp count_comprehensive_changes(original, updated) do
    # Comprehensive change count based on multiple metrics
    original_lines = String.splitoriginal, "\n" |> length()
    updated_lines = String.splitupdated, "\n" |> length()
    line_diff = abs(updated_lines - original_lines)

    # Additional metrics for import additions
    import_additions = count_import_additions(original, updated)

    line_diff + import_additions + 1
  end

  defp count_import_additions(original, updated) do
    original_imports = Regex.scan~r/import Indrajaal\.Shared\./, original |> length()
    updated_imports = Regex.scan~r/import Indrajaal\.Shared\./, updated |> length()
    max(0, updated_imports - original_imports)
  end

