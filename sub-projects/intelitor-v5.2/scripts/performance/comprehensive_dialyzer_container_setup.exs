  @spec create_container(term(), term()) :: term()
  defp create_container(name, config) do
    Logger.info("🔧 Creating container #{name} with Dialyzer support")

    # Build Podman command with comprehensive configuration
    _ports = Enum.map(config.ports, fn port -> ["-p", port] end) |> List.flatten()
    _volumes = Enum.map(config.volumes, fn vol -> ["-v", vol] end) |> List.flatten()
    _env_vars = Enum.map(config.environment, fn {k, v} -> ["-e", "#{k}=#{v}"] end)

    cmd = ["podman", "create", "--name", name] ++
          ports ++ volumes ++ env_vars ++
          ["--rm", "-it", config.image, "/bin/sh"]

    case System.cmd("podman", tl(cmd), stderr_to_stdout: true) do
      {output, 0} ->
        Logger.info("✅ Container #{name} created successfully")
        {:ok, output}

      {error, exit_code} ->
        Logger.error("❌ Failed to create container #{name}: #{error}")
        {:error, {exit_code, error}}
    end
  end

