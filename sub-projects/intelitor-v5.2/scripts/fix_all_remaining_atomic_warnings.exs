  def run do
    IO.puts("🔧 Fixing ALL remaining atomic operation warnings...")
    IO.puts("📋 Total files to process: #{length(@specific_warnings)}")

    total_fixed = 0

    Enum.each(@specific_warnings, fn {file_path, actions} ->
      if File.exists?(file_path) do
        fixes_applied = fix_file_actions(file_path, actions)
        total_fixed = total_fixed + fixes_applied

        if fixes_applied > 0 do
          IO.puts("✅ Fixed #{fixes_applied} actions in #{Path.basename(file_path)
        else
          IO.puts("ℹ️  No changes needed in #{Path.basename(file_path)}")
        end
      else
        IO.puts("⚠️  File not found: #{file_path}")
      end
    end)

    IO.puts("✅ Applied #{total_fixed} total atomic warning fixes")
  end

  @spec fix_file_actions(term(), term()) :: term()
  defp fix_file_actions(file_path, actions) do
    content = File.read!(file_path)

    # Apply fixes for each specific action
    {updated_content, fixes_count} =
      Enum.reduce(actions, {content, 0}, fn action, {acc, count} ->
        {new_content, fixed?} = fix_specific_action(acc, action)
        {new_content, if(fixed?, do: count + 1, else: count)}
      end)

    if updated_content != content do
      File.write!(file_path, updated_content)
      fixes_count
    else
      0
    end
  end

  @spec fix_specific_action(term(), term()) :: term()
  defp fix_specific_action(content, action_name) do
    # More precise pattern matching for specific action blocks
    # Pattern matches: update :action_name do ... end
    pattern = ~r/(update\s+:#{action_name}\s+do\n)(.*?)(\n\s{2}end)/s

    case Regex.run(pattern, content) do
      [full_match, action_start, action_body, action_end] ->
        if String.contains?(action_body, "__require_atomic?") do
          # Already has __require_atomic? declaration
          {content, false}
        else
          # Check if this action has function-based changes that __require atomic=f
          needs_atomic_false =
            String.contains?(action_body, "change fn") ||
              String.contains?(action_body, "validate fn") ||
              String.contains?(action_body, "Ash.Changeset.") ||
              String.contains?(action_body, "DateTime.utc_now") ||
              String.contains?(action_body, "set_attribute") ||
              String.contains?(action_body, "force_change_attribute") ||
              String.contains?(action_body, "after_action") ||
              String.contains?(action_body, "before_action")

          if needs_atomic_false do
            # Add __require_atomic? false at the end of the action body, before the
            new_action_body =
              String.trim_trailing(action_body) <> "\n\n      __require_atomic? false"

            new_full_match = action_start <> new_action_body <> action_end
            new_content = String.replace(content, full_match, new_full_match)
            {new_content, true}
          else
            {content, false}
          end
        end

      nil ->
        # Action not found or already properly formatted
        {content, false}
    end
  end
end

