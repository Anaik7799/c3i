  @spec extract_bash_commands(term()) :: term()
  defp extract_bash_commands(content) do
    bash_pattern = ~r/```bash\n(.*?)\n```/s

    Regex.scanbash_pattern, content, capture: :all_but_first |> List.flatten()
    |> Enum.flat_mapfn block ->
      block
      |> String.split("\n" |> Enum.map&String.trim/1 |> Enum.reject(&(String.starts_with?(&1, "#") or &1 == ""))
    end)
    |> Enum.uniq()
  end

  @spec extract_agent_coordination_commands(term()) :: term()
  defp extract_agent_coordination_commands(content) do
    content
    |> String.split"\n" |> Enum.filter(
      &(String.contains?(
          &1,
          "mix claude"
        ) or
          String.contains?(
            &1,
            "--supervisor"
          ) or String.contains?(&1, "--helpers") or String.contains?(&1, "--workers"))
    )
    |> Enum.map&String.trim/1 |> Enum.reject(&String.starts_with?(&1, "#"))
    |> Enum.uniq()
  end

  @spec extract_container_commands(term()) :: term()
  defp extract_container_commands(content) do
    content
    |> String.split"\n" |> Enum.filter(&String.contains?(&1, "podman exec"))
    |> Enum.map&String.trim/1 |> Enum.reject(&String.starts_with?(&1, "#"))
    |> Enum.uniq()
  end

  @spec extract_phics_commands(term()) :: term()
  defp extract_phics_commands(content) do
    content
    |> String.split"\n" |> Enum.filter(&(String.contains?(&1, "pcis") and String.contains?(&1, "--")))
    |> Enum.map&String.trim/1 |> Enum.reject(&String.starts_with?(&1, "#"))
    |> Enum.uniq()
  end

  @spec suggest_container_equivalent(term()) :: term()
  defp suggest_container_equivalent(command) do
    cond do
      String.contains?(command, "mix ") and not String.contains?(command, "podman exec") ->
        "podman exec indrajaal-app bash -c \"cd /workspace && #{command}\""

      String.contains?(command, "createdb") and not String.contains?(command, "podman exec") ->
        "podman exec indrajaal-db bash -c \"#{command}\""

      String.contains?(command, "psql") and not String.contains?(command, "podman exec") ->
        "podman exec indrajaal-db bash -c \"#{command}\""

      true ->
        # Already container-compliant or doesn't need conversion
        command
    end
  end

