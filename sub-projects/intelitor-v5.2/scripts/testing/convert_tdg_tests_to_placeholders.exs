  @spec convert_file(term()) :: term()
  defp convert_file(file_path) do
    content = File.read!(file_path)

    # Replace all ContinuousEnterpriseDemo.* calls with placeholders
    converted_content = content
    |> String.replace(~r/(\s+)result = ContinuousEnterpriseDemo\.([^\(]+\([^\)]*\
    |> String.replace(~r/(\s+)assert result\.([^\s]+) == ([^\n]+)/, "\\1# TDG: as
    |> String.replace(~r/(\s+)assert result == ([^\n]+)/, "\\1# TDG: assert resul
    |> String.replace(~r/(\s+)refute result\.([^\s]+)/, "\\1# TDG: refute result.
    |> add_placeholder_assertions()

    File.write!(file_path, converted_content)
  end

  @spec add_placeholder_assertions(term()) :: term()
  defp add_placeholder_assertions(content) do
    # Add assert true to tests that only have commented assertions
    content
    |> String.replace(~r/(test "[^"]*" do\s*\n(?:\s*# TDG:[^\n]*\n)*)\s*end/m, "\
  end
