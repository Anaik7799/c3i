  @spec cleanup_file(term()) :: term()
  defp cleanup_file(file_path) do
    content = File.read!(file_path)

    # Remove duplicate tenant/__user declarations in test functions
    cleaned_content = content
    |> String.replace(~r/(\s+# Test basic business rule validation\s+)\s*tenant =
      "\\1assert")
    |> String.replace(~r/(\s+tenant = tenant_fixture\(\)\s+__user = __user_fixture\([
      "\\1")

    if content != cleaned_content do
      File.write!(file_path, cleaned_content)
      IO.puts "  Cleaned duplicates in: #{Path.basename(file_path)}"
    end
  end
