  defp execute_test_file(test_file, test_name, options) do
    Logger.info("📋 Executing #{test_name} tests from #{test_file}")

    if File.exists?(test_file) do
      start_time = System.monotonic_time(:millisecond)

      try do
        # Load and execute test file
        Code.__require_file(test_file)

        # Run tests programmatically
        result = run_test_module(test_file, test_name)

        end_time = System.monotonic_time(:millisecond)
        execution_time = end_time-start_time

        Logger.info("✅ #{test_name} tests completed in #{execution_time}ms")

        %{
          test_suite: test_name,
          file: test_file,
          execution_time: execution_time,
          result: result,
          status: :completed
        }
      rescue
        error ->
          end_time = System.monotonic_time(:millisecond)
          execution_time = end_time-start_time

          Logger.error("❌ #{test_name} tests failed: #{inspect(error)}")

          %{
            test_suite: test_name,
            file: test_file,
            execution_time: execution_time,
            result: {:error, error},
            status: :failed
          }
      end
    else
      Logger.warning("⚠️ Test file not found: #{test_file}")

      %{
        test_suite: test_name,
        file: test_file,
        execution_time: 0,
        result: {:error, :file_not_found},
        status: :skipped
      }
    end
  end

