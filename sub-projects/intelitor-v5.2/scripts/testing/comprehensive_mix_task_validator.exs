  def execute_level(1) do
    IO.puts "\n🔍 Level 1: Symptom Analysis - Basic Functionality Testing"
    IO.puts "============================================================"
    
    results = %{
      discovered: 0,
      help_available: 0,
      basic_execution: 0,
      compilation_errors: 0,
      failures: []
    }
    
    _results = Enum.reduce(@mix_tasks, _results, fn task, acc ->
      IO.puts "\n  Testing: mix #{task.name}"
      
      # Test 1.1: Task Discovery
      _acc = Map.put(acc, :discovered, acc.discovered + 1)
      
      # Test 1.2: Help Documentation
      help_result = test_help_documentation(task.name)
      acc = if help_result.success do
        Map.put(acc, :help_available, acc.help_available + 1)
      else
        Map.put(acc, :failures, [help_result | acc.failures])
        acc
      end
      
      # Test 1.3: Basic Execution Test (safe commands only)
      execution_result = test_basic_execution(task)
      acc = if execution_result.success do
        Map.put(acc, :basic_execution, acc.basic_execution + 1)
      else
        if String.contains?(execution_result.error || "", "CompileError") do
          Map.put(acc, :compilation_errors, acc.compilation_errors + 1)
        end
        Map.put(acc, :failures, [execution_result | acc.failures])
        acc
      end
      
      acc
    end)
    
    print_level_1_summary(results)
    save_level_1_results(results)
  end

  # Test help documentation availability
  defp test_help_documentation(task_name) do
    case System.cmd("mix", ["help", task_name], stderr_to_stdout: true) do
      {output, 0} ->
        %{
          task: task_name,
          test: "help_documentation",
          success: true,
          output: String.slice(output, 0..200) <> "..."
        }
      {error, _} ->
        %{
          task: task_name,
          test: "help_documentation", 
          success: false,
          error: String.slice(error, 0..200) <> "..."
        }
    end
  end

  # Test basic execution (safe mode only)
  defp test_basic_execution(task) do
    # Only test safe operations that won't modify system __state
    safe_args = case task.category do
      :compilation -> ["--help"]  # Compilation tasks: help only
      :container -> ["--help"]    # Container tasks: help only  
      :testing -> ["--help"]      # Test tasks: help only
      :demo -> ["--help"]         # Demo tasks: help only
      :quality -> ["--help"]      # Quality tasks: help only
      :utility -> ["--help"]      # Utility tasks: help only
      _ -> ["--help"]
    end
    
    case System.cmd("mix", [task.name | safe_args], stderr_to_stdout: true) do
      {output, 0} ->
        %{
          task: task.name,
          test: "basic_execution",
          success: true,
          output: String.slice(output, 0..200) <> "...",
          args: safe_args
        }
      {error, exit_code} ->
        %{
          task: task.name,
          test: "basic_execution",
          success: false,
          error: String.slice(error, 0..200) <> "...",
          exit_code: exit_code,
          args: safe_args
        }
    end
  end

