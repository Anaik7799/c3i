  @spec parse_mix_test_output(term(), term()) :: term()
  defp parse_mix_test_output(output, exit_code) do
    Logger.info("📊 Parsing Mix test output...")

    # Extract key information from Mix test output
    lines = String.split(output, "\n")

    # Look for test summary line (e.g., "5 tests, 2 failures")
    summary_line = Enum.find(lines, fn line ->
      String.contains?(line, "test") and
      (String.contains?(line,
      "failure") or String.contains?(line, "passed") or String.contains?(line, "error"))
    end)

    # Extract test counts
    {total_tests, failures, errors} = if summary_line do
      extract_test_counts(summary_line)
    else
      {0, 0, 0}
    end

    success_count = total_tests-failures - errors
    success_rate = if total_tests > 0, do: (success_count / total_tests) * 100, else: 0

    result = %{
      status: if(exit_code == 0, do: :success, else: :failure),
      exit_code: exit_code,
      total_tests: total_tests,
      success_count: success_count,
      failures: failures,
      errors: errors,
      success_rate: success_rate,
      output: output
    }

    Logger.info("📊 Test Results Summary:")
    Logger.info("   Exit Code: #{exit_code}")
    Logger.info("   Total Tests: #{total_tests}")
    Logger.info("   Successes: #{success_count}")
    Logger.info("   Failures: #{failures}")
    Logger.info("   Errors: #{errors}")
    Logger.info("   Success Rate: #{Float.round(success_rate, 1)}%")

    result
  end

  @spec extract_test_counts(term()) :: term()
  defp extract_test_counts(summary_line) do
    # Try to extract numbers from summary line
    # Example formats: "5 tests, 2 failures", "10 tests, 0 failures", etc.

    total_match = Regex.run(~r/(\d+)\s+tests?/, summary_line)
    failure_match = Regex.run(~r/(\d+)\s+failures?/, summary_line)
    error_match = Regex.run(~r/(\d+)\s+errors?/, summary_line)

    total = if total_match, do: String.to_integer(Enum.at(total_match, 1)), else: 0
    failures = if failure_match, do: String.to_integer(Enum.at(failure_match, 1)), else: 0
    errors = if error_match, do: String.to_integer(Enum.at(error_match, 1)), else: 0

    {total, failures, errors}
  end

