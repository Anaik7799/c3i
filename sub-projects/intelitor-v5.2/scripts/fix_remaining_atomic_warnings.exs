        fixes_applied = fix_file(file_path, actions)
        total_fixes = total_fixes + fixes_applied
      else
        IO.puts("⚠️  File not found: #{file_path}")
      end
    end)

    IO.puts("✅ Applied #{total_fixes} total fixes for atomic warnings")
  end

  @spec fix_file(term(), term()) :: term()
  defp fix_file(file_path, actions) do
    content = File.read!(file_path)

    # Apply fixes for each action
    {updated_content, fixes_count} =
      Enum.reduce(actions, {content, 0}, fn action, {acc, count} ->
        {new_content, fixed?} = fix_action(acc, action)
        {new_content, if(fixed?, do: count + 1, else: count)}
      end)

    if updated_content != content do
      File.write!(file_path, updated_content)
      IO.puts("✅ Fixed #{fixes_count} actions in #{file_path}")
      fixes_count
    else
      IO.puts("ℹ️  No changes needed in #{file_path}")
      0
    end
  end

  @spec fix_action(term(), term()) :: term()
  defp fix_action(content, action_name) do
    # Pattern to match action definition and add __require_atomic? false if missing
    # Look for update actions with function-based changes
    pattern =
      ~r/(update\s+:#{action_name}\s+do\n(?:(?:(?!\n    end\n|\n  end\n).*\n)*?)(\n    end|\n  end)/m

    case Regex.run(pattern, content) do
      [full_match, action_content, ending] ->
        if String.contains?(action_content, "__require_atomic?") do
          # Already has __require_atomic? declaration
          {content, false}
        else
          # Check if this action has function-based changes that would __require at
          has_function_changes =
            String.contains?(action_content, "change fn") ||
              String.contains?(action_content, "validate fn") ||
              String.contains?(action_content, "Ash.Changeset.change_attribute") ||
              String.contains?(action_content, "DateTime.utc_now()") ||
              String.contains?(action_content, "set_attribute")

          if has_function_changes do
            # Add __require_atomic? false before the end of the action
            new_action_content =
              String.trim_trailing(action_content) <> "\n\n      __require_atomic? false\n"

            new_full_match = new_action_content <> ending
            new_content = String.replace(content, full_match, new_full_match)
            {new_content, true}
          else
            {content, false}
          end
        end

      nil ->
        # Action not found or already properly formatted
        {content, false}
    end
  end
