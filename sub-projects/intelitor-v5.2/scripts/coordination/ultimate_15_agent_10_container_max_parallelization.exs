  # Advanced compilation error parsing with pattern recognition
  defp parse_compilation_errors_advanced(output) do
    output
    |> String.split("\n")
    |> Enum.filter(&contains_advanced_error_indicators?/1)
    |> Enum.map(&parse_advanced_single_error/1)
    |> Enum.filter(& &1 != nil)
  end

  defp contains_advanced_error_indicators?(line) do
    advanced_patterns = [
      "error:", "warning:", "** (", "undefined function", 
      "undefined variable", "function def", "@moduledoc",
      "unused variable", "unused alias", "CompileError",
      "MismatchedDelimiterError", "TokenMissingError",
      "SyntaxError", "UndefinedFunctionError"
    ]
    
    Enum.any?(advanced_patterns, &String.contains?(line, &1))
  end

  defp parse_advanced_single_error(error_line) do
    cond do
      String.contains?(error_line, "undefined function") ->
        %{type: :undefined_function, line: error_line, severity: :error, fixable: true, priority: :high}
      
      String.contains?(error_line, "undefined variable") ->
        %{type: :undefined_variable, line: error_line, severity: :error, fixable: true, priority: :high}
      
      String.contains?(error_line, "function def") ->
        %{type: :malformed_function, line: error_line, severity: :error, fixable: true, priority: :high}
      
      String.contains?(error_line, "@moduledoc") ->
        %{type: :moduledoc_issue, line: error_line, severity: :warning, fixable: true, priority: :medium}
      
      String.contains?(error_line, "unused variable") ->
        %{type: :unused_variable, line: error_line, severity: :warning, fixable: true, priority: :low}
      
      String.contains?(error_line, "unused alias") ->
        %{type: :unused_alias, line: error_line, severity: :warning, fixable: true, priority: :low}
        
      String.contains?(error_line, "CompileError") ->
        %{type: :compile_error, line: error_line, severity: :error, fixable: true, priority: :critical}
        
      String.contains?(error_line, "MismatchedDelimiterError") ->
        %{type: :syntax_error, line: error_line, severity: :error, fixable: true, priority: :critical}
      
      true ->
        %{type: :generic, line: error_line, severity: :error, fixable: false, priority: :unknown}
    end
  end

