  defp analyze_error_patterns(analysis) do
    # Extract patterns from compilation results
    all_errors = Enum.flat_map(analysis.results, fn result ->
      Map.get(result, :errors, [])
    end)
    
    # Group errors by type
    error_patterns = Enum.group_by(all_errors, & &1.type)
    
    # Generate insights
    Enum.map(error_patterns, fn {error_type, errors} ->
      %{
        pattern_type: error_type,
        f__requency: length(errors),
        examples: Enum.take(errors, 3),
        recommended_approach: get_pattern_approach(error_type)
      }
    end)
