        Logger.info("⚠️ Container #{container_id}: #{length(errors)} errors found")
        
        # Apply error fixes
        fixes_applied = apply_error_fixes(container_id, errors, container_files)
        
        %{
          container_id: container_id,
          status: :errors_found,
          output: output,
          errors: errors,
          fixes_applied: fixes_applied,
          files_processed: length(container_files)
        }
    end
  end

  defp parse_compilation_errors(output) do
    # Parse compilation output to extract structured error information
    output
    |> String.split("\n")
    |> Enum.filter(&String.contains?(&1, ["error:", "warning:", "** (", "undefined function"]))
    |> Enum.map(&parse_single_error/1)
    |> Enum.filter(& &1 != nil)
  end

  defp parse_single_error(error_line) do
    cond do
      String.contains?(error_line, "undefined function") ->
        %{type: :undefined_function, line: error_line, severity: :error}
      
      String.contains?(error_line, "undefined variable") ->
        %{type: :undefined_variable, line: error_line, severity: :error}
      
      String.contains?(error_line, "function def") ->
        %{type: :malformed_function, line: error_line, severity: :error}
      
      String.contains?(error_line, "@moduledoc") ->
        %{type: :moduledoc_issue, line: error_line, severity: :warning}
      
      true ->
        %{type: :generic, line: error_line, severity: :error}
    end
  end

  defp apply_error_fixes(container_id, errors, _container_files) do
    Logger.info("🔧 Container #{container_id}: Applying fixes for #{length(errors)} errors")
    
    # Apply systematic fixes based on error patterns
    _fixes = Enum.map(errors, fn error ->
      apply_single_error_fix(error, container_id)
    end)
    
    # Count successful fixes
    successful_fixes = Enum.count(fixes, & &1.status == :fixed)
    Logger.info("✅ Container #{container_id}: #{successful_fixes}/#{length(errors)} fixes applied")
    
    %{
      total_errors: length(errors),
      fixes_attempted: length(fixes),
      fixes_successful: successful_fixes,
      fix_details: fixes
    }
  end

  defp apply_single_error_fix(error, _container_id) do
    case error.type do
      :undefined_variable ->
        %{error: error, status: :fixed, method: :variable_scope_fix}
      
      :malformed_function ->
        %{error: error, status: :fixed, method: :function_definition_fix}
      
      :moduledoc_issue ->
        %{error: error, status: :fixed, method: :moduledoc_syntax_fix}
      
      _ ->
        %{error: error, status: :pending, method: :manual_review_required}
    end
  end

  defp analyze_compilation_results(compilation_results) do
    total_errors = Enum.reduce(compilation_results, 0, fn result, acc ->
