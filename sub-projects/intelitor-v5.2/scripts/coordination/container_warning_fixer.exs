    IO.puts """
    🐳 CONTAINER-BASED WARNING FIX EXECUTION
    ========================================

    Using existing podman-compose infrastructure:-indrajaal-app-demo container
    - PostgreSQL, Redis, Prometheus, Grafana, Nginx
    - PHICS hot-reloading enabled
    """

    # Ensure containers are running
    ensure_containers_running()

    # Execute master coordinator in app container
    execute_master_coordinator()

    # Monitor and validate
    monitor_and_validate()
  end

  @spec ensure_containers_running() :: any()
  defp ensure_containers_running do
    IO.puts "🔍 Checking container status..."

    {_output, __} = System.cmd("podman-compose", ["ps"])

    if not String.contains?(output, "indrajaal-app-demo") do
      IO.puts "  🚀 Starting containers..."
      System.cmd("podman-compose", ["up", "-d"])
      Process.sleep(5000) # Wait for containers to be ready
    else
      IO.puts "  ✅ Containers already running"
    end
  end

  @spec execute_master_coordinator() :: any()
  defp execute_master_coordinator do
    IO.puts "🏭 Executing SOPv5.1 Master Coordinator in container..."

    # Copy coordinator script to container if needed
    System.cmd("podman", ["cp",
      "scripts/coordination/sopv51_master_coordinator.exs",
      "indrajaal-app-demo:/workspace/scripts/coordination/"
    ])

    # Execute in container with proper environment
    {_output, _exit_code} = System.cmd("podman", ["exec", "-it", "indrajaal-app-demo",
      "bash", "-c", """
      cd /workspace && \
      export ELIXIR_ERL_OPTIONS="+fnu +S 16" && \
      export MIX_ENV=dev && \
      elixir scripts/coordination/sopv51_master_coordinator.exs
      """
    ])

    IO.puts output

    if exit_code != 0 do
      IO.puts "❌ Execution failed with exit code: #{exit_code}"
    else
      IO.puts "✅ Master coordinator execution complete"
    end
  end

  @spec monitor_and_validate() :: any()
  defp monitor_and_validate do
    IO.puts "📊 Monitoring and validating fixes..."

    # Run compilation check in container
    {_output, _exit_code} = System.cmd("podman", ["exec", "-it", "indrajaal-app-demo",
      "bash", "-c", """
      cd /workspace && \
      export ELIXIR_ERL_OPTIONS="+fnu +S 16" && \
      mix compile --jobs 16 --warnings-as-errors --force
      """
    ])

    if exit_code == 0 do
      IO.puts "✅ VALIDATION SUCCESS: Zero warnings achieved!"

      # Run tests to ensure nothing broke
      run_tests_in_container()
    else
      IO.puts "⚠️  Some warnings remain:"
      IO.puts output
    end
  end

  @spec run_tests_in_container() :: any()
  defp run_tests_in_container do
    IO.puts "🧪 Running tests in container..."

    {_output, _exit_code} = System.cmd("podman", ["exec", "-it", "indrajaal-app-demo",
      "bash", "-c", """
      cd /workspace && \
      export ELIXIR_ERL_OPTIONS="+fnu +S 16" && \
      mix test --max-failures 5
      """
    ])

    if exit_code == 0 do
      IO.puts "✅ Tests pass!"
    else
      IO.puts "⚠️  Some tests failed"
      IO.puts output
    end
  end

