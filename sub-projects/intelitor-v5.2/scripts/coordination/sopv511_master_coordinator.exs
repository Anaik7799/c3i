  @spec fix_atomic_in_file(term()) :: term()
  defp fix_atomic_in_file(file_path) do
    # Read file
    content = File.read!(file_path)

    # Apply comprehensive atomic fix pattern
    fixed_content =
      content
      |> fix_update_actions()
      |> fix_destroy_actions()
      |> fix_create_actions()

    # Write back if changed
    if content != fixed_content do
      File.write!(file_path, fixed_content)
      Logger.info("Fixed atomic warnings in #{file_path}")
    end
  end

  @spec fix_update_actions(term()) :: term()
  defp fix_update_actions(content) do
    # Comprehensive pattern for update actions
    Regex.replace(
      ~r/(update\s+:\w+\s+do\n)((?:(?!\s*__require_atomic\?\s+false)(?!\s*update\s+:|destroy\s+:|create\s+:|end\n\s*end).*\n)*?)(\s*(?:change|validate|argument)\s+)/ms,
      content,
      fn _, start, middle, rest ->
        if String.contains?(middle, "__require_atomic? false") do
          start <> middle <> rest
        else
          start <> "      __require_atomic? false\n" <> middle <> rest
        end
      end
    )
  end

  @spec fix_destroy_actions(term()) :: term()
  defp fix_destroy_actions(content) do
    # Pattern for destroy actions with changes
    Regex.replace(
      ~r/(destroy\s+:\w+\s+do\n)((?:(?!\s*__require_atomic\?\s+false).*\n)*?)(\s*change\s+)/ms,
      content,
      fn _, start, middle, change ->
        if String.contains?(middle, "__require_atomic? false") do
          start <> middle <> change
        else
          start <> "      __require_atomic? false\n" <> middle <> change
        end
      end
    )
  end

  @spec fix_create_actions(term()) :: term()
  defp fix_create_actions(content) do
    # Pattern for create actions with function changes (rare)
    Regex.replace(
      ~r/(create\s+:\w+\s+do\n)((?:(?!\s*__require_atomic\?\s+false).*\n)*?)(\s*change\s+fn)/ms,
