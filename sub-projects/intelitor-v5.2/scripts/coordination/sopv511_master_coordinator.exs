  @spec fix_unused_in_file(term(), term()) :: term()
  defp fix_unused_in_file(file_path, warnings) do
    content = File.read!(file_path)

    _fixed_content =
      Enum.reduce(warnings, _content, fn warning, acc ->
        case parse_unused_warning(warning) do
          {:variable, var_name} ->
            prefix_unused_variable(acc, var_name)
          {:function, func_name} ->
            remove_unused_function(acc, func_name)
          {:alias, alias_name} ->
            remove_unused_alias(acc, alias_name)
          _ ->
            acc
        end
      end)

    if content != fixed_content do
      File.write!(file_path, fixed_content)
      1
    else
      0
    end
  end

  @spec prefix_unused_variable(term(), term()) :: term()
  defp prefix_unused_variable(content, var_name) do
    # Add underscore prefix to unused variables
    Regex.replace(
      ~r/(\s)#{Regex.escape(var_name)}(\s*=)/,
      content,
      "\\1_#{var_name}\\2"
    )
  end

  @spec remove_unused_function(term(), term()) :: term()
  defp remove_unused_function(content, func_name) do
    # Remove entire unused function definition
    Regex.replace(
      ~r/^\s*defp?\s+#{Regex.escape(func_name)}.*?(?=^\s*defp?\s+|\z)/ms,
      content,
      ""
    )
  end

  @spec remove_unused_alias(term(), term()) :: term()
  defp remove_unused_alias(content, alias_name) do
    # Remove unused alias
    Regex.replace(
      ~r/^\s*alias\s+.*#{Regex.escape(alias_name)}.*\n/m,
      content,
      ""
    )
  end

