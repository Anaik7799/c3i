  @spec parse_warnings(term()) :: term()
  defp parse_warnings(output) do
    output
    |> String.split("\n")
    |> Enum.reduce(%{atomic: [], wallaby: [], unused: [], other: []}, fn line, acc ->
      cond do
        String.contains?(line, "cannot be done atomically") ->
          Map.update(acc, :atomic, [line], &[line | &1])
        String.contains?(line, "undefined function") and String.contains?(line, "Wallaby") ->
          Map.update(acc, :wallaby, [line], &[line | &1])
        String.contains?(line, "is unused") ->
          Map.update(acc, :unused, [line], &[line | &1])
        String.contains?(line, "warning:") or String.contains?(line, "error:") ->
          Map.update(acc, :other, [line], &[line | &1])
        true ->
          acc
      end
    end)
  end

