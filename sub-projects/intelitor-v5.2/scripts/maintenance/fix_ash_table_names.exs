  def fix_all do
    IO.puts("🔧 Fixing Ash postgres table names...")
    
    ash_stub_files = Path.wildcard("lib/indrajaal/integration/**/*.ex")
    |> Enum.filter(&contains_postgres_table?/1)
    
    results = Enum.map(ash_stub_files, &fix_stub_file/1)
    
    successful = Enum.count(results, fn {status, _} -> status == :ok end)
    IO.puts("\n✅ Fixed #{successful}/#{length(ash_stub_files)} Ash resource files")
  end
  
  defp contains_postgres_table?(file_path) do
    content = File.read!(file_path)
    String.contains?(content, "table :") && String.contains?(content, "postgres do")
  end
  
  defp fix_stub_file(file_path) do
    content = File.read!(file_path)
    
    # Fix table atom to string
    new_content = Regex.replace(~r/table :([a-zA-Z_]+)/, content, ~S(table "\1"))
    
    if new_content != content do
      File.write!(file_path, new_content)
      IO.puts("✅ Fixed: #{file_path}")
      {:ok, file_path}
    else
      IO.puts("ℹ️  No changes needed: #{file_path}")
      {:ok, file_path}
    end
  end
end

