  @spec fix_file_line_lengths(term()) :: term()
  defp fix_file_line_lengths(file_path) do
    try do
      content = File.read!(file_path)
      lines = String.split(content, "\n")

      _updated_lines =
        Enum.map(lines, fn line ->
          if String.length(line) > 80 do
            wrap_long_line(line)
          else
            line
          end
        end)

      updated_content = Enum.join(updated_lines, "\n")

      if content != updated_content do
        File.write!(file_path, updated_content)
      end
    rescue
      error ->
        IO.puts("⚠️  Warning: Could not process #{file_path}: #{inspect(error)}")
    end
  end

  @spec fix_file_blank_lines(term()) :: term()
  defp fix_file_blank_lines(file_path) do
    try do
      content = File.read!(file_path)

      # Replace multiple consecutive blank lines with single blank line
      updated_content = String.replace(content, ~r/\n\n\n+/, "\n\n")

      if content != updated_content do
        File.write!(file_path, updated_content)
      end
    rescue
      error ->
        IO.puts("⚠️  Warning: Could not process #{file_path}: #{inspect(error)}")
    end
  end

  @spec wrap_long_line(term()) :: term()
  defp wrap_long_line(line) do
    cond do
      # Comments-safe to wrap
      String.contains?(line, "#") and String.length(line) > 80 ->
        wrap_comment_line(line)

      # Long string literals-can be wrapped
      String.contains?(line, "\"") and String.length(line) > 100 ->
        wrap_string_line(line)

      # Function calls with many parameters
      String.contains?(line, "(") and String.contains?(line, ",") ->
        wrap_function_call(line)

      # Everything else-leave as is to avoid syntax errors
      true ->
        line
    end
  end

  @spec wrap_comment_line(term()) :: term()
  defp wrap_comment_line(line) do
    # Simple comment wrapping - split at reasonable boundaries
    if String.length(line) <= 80 do
      line
    else
      # Find a good split point
      case String.split(line, " ", parts: 2) do
        [prefix, rest] ->
          if String.length(prefix) < 60 do
            indent = String.duplicate(" ", String.length(prefix))
            "#{prefix} #{String.slice(rest, 0, 80-String.length(prefix))}"
          else
            # Don't break if prefix is too long
            line
          end

        _ ->
          # Don't break if we can't find a good split
          line
      end
    end
  end

  @spec wrap_string_line(term()) :: term()
  defp wrap_string_line(line) do
    # For very long strings, leave as-is to avoid breaking them
    # This is complex and risky - safer to leave unchanged
    line
  end

