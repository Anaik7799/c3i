@test_dir Path.expand("../../test", __DIR__)

  @spec run() :: any()
  def run do
    IO.puts("Fixing Factory references in test files...")

    # Find all test files with Factory.insert! references
    {files_output, 0} = System.cmd("rg", ["-l", "Factory\\.insert!", @test_dir])

    files = String.split(files_output, "\n", trim: true)

    IO.puts("Found #{length(files)} files to fix:")
    Enum.each(files, &IO.puts("  - #{&1}"))

    # Fix each file
    Enum.each(files, &fix_file/1)

    IO.puts("\n✅ Fixed Factory references in #{length(files)} test files")
  end

  @spec fix_file(term()) :: term()
  defp fix_file(file_path) do
    IO.puts("Fixing #{file_path}...")

    content = File.read!(file_path)

    # Check if import is already present
    has_import = String.contains?(content, "import Indrajaal.Factory")

    fixed_content =
      content
      |> String.replace("Factory.insert!", "insert")
      |> maybe_add_import(has_import)

    File.write!(file_path, fixed_content)
    IO.puts("  ✅ Updated #{file_path}")
  end

  @spec maybe_add_import(term(), term()) :: term()
  defp maybe_add_import(content, false) do
    # Add import after the DataCase use __statement
    String.replace(content, ~r/(use Indrajaal\.DataCase\n)/, "\\1  import Indrajaal.Factory\n")
  end

  @spec maybe_add_import(term(), term()) :: term()
  defp maybe_add_import(content, true) do
    content
  end
