        IO.puts("    ✓ WORKER AGENT: Added Wallaby configuration to test.exs")
      else
        IO.puts("    ℹ️  WORKER AGENT: Wallaby already configured in test.exs")
      end
    else
      IO.puts("    ⚠️  HELPER AGENT: config/test.exs not found")
    end
  end

