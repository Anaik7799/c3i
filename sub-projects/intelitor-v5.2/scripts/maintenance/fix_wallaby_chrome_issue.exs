      path when is_binary(path) ->
        if File.exists?(path) do
          IO.puts("    ✓ WORKER AGENT: Chrome driver found at #{path}")
        else
          IO.puts("    ⚠️  WORKER AGENT: Chrome driver path #{path} does not exist
        end
    end

    # Set environment variable to disable Wallaby for this test run
    System.put_env("CHROMEDRIVER_PATH", "/nonexistent")
    IO.puts("    🔧 WORKER AGENT: Set CHROMEDRIVER_PATH to disable Wallaby for Core tests")
  end
