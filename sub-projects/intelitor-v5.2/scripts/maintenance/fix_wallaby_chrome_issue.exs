  @spec ensure_chrome_path_handling(term()) :: term()
  defp ensure_chrome_path_handling(content) do
    if String.contains?(content, "CHROMEDRIVER_PATH") do
      content
    else
      # Add Chrome path handling before Wallaby initialization
      wallaby_section = """
# AGENT FIX: Handle Chrome path for Wallaby testing
chrome_path = System.get_env("CHROMEDRIVER_PATH")

if chrome_path && File.exists?(chrome_path) do
  # Initialize Wallaby with proper Chrome configuration
  Application.put_env(:wallaby, :chromedriver, %{
    path: chrome_path
  })
  {:ok, _} = Application.ensure_all_started(:wallaby)
else
  # Disable Wallaby if Chrome is not available
  IO.puts("⚠️  Chrome driver not found-Wallaby tests will be skipped")
  Application.put_env(:wallaby, :driver, Wallaby.Experimental.Driver.None)
end"""

      String.replace(content,
        ~r/(ExUnit\.start\(\).*?\n)/,
        "\\1\n" <> wallaby_section <> "\n"
      )
    end
  end

  @spec ensure_wallaby_conditional_start(term()) :: term()
  defp ensure_wallaby_conditional_start(content) do
    # Remove any existing Wallaby.start calls that might cause issues
    content

    |> String.replace(~r/\{\:ok,\s*_\}\s*=\s*Application\.ensure_all_started\(:wallaby\)\n?/, "")
    |> String.replace(~r/Application\.ensure_all_started\(:wallaby\)\n?/, "")
  end

  @spec ensure_otlp_disabled(term()) :: term()
  defp ensure_otlp_disabled(content) do
    if String.contains?(content, "traces_exporter: :none") do
      content
    else
      # Add OTLP disabling before ExUnit.start
      otlp_fix = """
# AGENT FIX: Disable OTLP exporter to pr__event test initialization warnings
Application.put_env(:opentelemetry, :traces_exporter, :none)
Application.put_env(:opentelemetry, :metrics_exporter, :none)
"""

      String.replace(content,
        ~r/(ExUnit\.start\(\))/,
        otlp_fix <> "\n\\1"
      )
    end
  end

  @spec fix_application_config() :: any()
  defp fix_application_config do
    IO.puts("  🔧 HELPER AGENT: Updating application configuration for testing...")

    config_path = "config/test.exs"

