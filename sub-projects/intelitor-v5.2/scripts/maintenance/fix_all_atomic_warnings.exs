  @spec run() :: any()
  def run do
    IO.puts("""
    🔧 FIXING ASH ATOMIC WARNINGS
    =============================
    Automatically fixing all atomic operation warnings
    """)

    # Get list of all Elixir files in lib/indrajaal
    elixir_files = find_elixir_files("lib/indrajaal")

    IO.puts("Found #{length(elixir_files)} Elixir files to check")

    _total_fixes = 0

    Enum.each(elixir_files, fn file_path ->
      fixes = fix_atomic_warnings_in_file(file_path)

      if fixes > 0 do
        IO.puts("✅ Fixed #{fixes} warnings in #{file_path}")
        total_fixes = total_fixes + fixes
      end
    end)

    IO.puts("\n📊 SUMMARY:")
    IO.puts("Total files processed: #{length(elixir_files)}")
    IO.puts("Total warnings fixed: #{total_fixes}")

    if total_fixes > 0 do
      IO.puts("\n🎉 All atomic warnings have been fixed!")
      IO.puts("You can now run: mix compile --jobs 16 --warnings-as-errors")
    else
      IO.puts("\n✅ No atomic warnings found to fix")
    end
  end

  @spec find_elixir_files(term()) :: term()
  defp find_elixir_files(directory) do
    Path.wildcard("#{directory}/**/*.ex")
    |> Enum.filter(fn file -> File.exists?(file) end)
  end

  @spec fix_atomic_warnings_in_file(term()) :: term()
  defp fix_atomic_warnings_in_file(file_path) do
    content = File.read!(file_path)

    # Look for actions that need atomic fixes
    {_new_content, _fixes_count} = fix_actions_in_content(content)

    if fixes_count > 0 do
      File.write!(file_path, new_content)
    end

    fixes_count
  end

  @spec fix_actions_in_content(term()) :: term()
  defp fix_actions_in_content(content) do
    # Pattern to match actions that likely need atomic fixes
    action_patterns = [
      # Update actions with function changes
      ~r/(update\s+:[\w_]+\s+do\n(?:[^\n]*\n)*?(?=\s*change\s+fn))((?:[^\n]*\n)*?)(\s*end)/m,
      # Create actions with function changes
      ~r/(create\s+:[\w_]+\s+do\n(?:[^\n]*\n)*?(?=\s*change\s+fn))((?:[^\n]*\n)*?)(\s*end)/m,
      # Destroy actions with function changes
      ~r/(destroy\s+:[\w_]+\s+do\n(?:[^\n]*\n)*?(?=\s*change\s+fn))((?:[^\n]*\n)*?)(\s*end)/m
    ]

    fixes_count = 0
    new_content = content

    # Fix each pattern
    Enum.each(action_patterns, fn pattern ->
      new_content =
        Regex.replace(pattern, new_content, fn full_match,
                                               action_start,
                                               action_body,
                                               action_end ->
          if String.contains?(action_body, "change fn") and
               not String.contains?(action_body, "__require_atomic?") do
            fixes_count = fixes_count + 1
            action_start <> action_body <> "    __require_atomic? false\n" <> action_end
          else
            full_match
          end
        end)
    end)

    # Alternative approach: Look for specific named actions mentioned in warnings
    warning_actions = [
      "update_project_details",
      "update_insurance_info",
      "update_certifications",
      "assign_work_areas",
      "register_equipment",
      "register_hazardous_materials",
      "report_safety_incident",
      "update_safety_protocols",
      "generate_report",
      "schedule_safety_training",
      "update_compliance_status",
      "finalize_report"
    ]

    Enum.each(warning_actions, fn action_name ->
      # Look for the specific action and add __require_atomic? false if missing
      action_pattern = ~r/(#{action_name}\s+do\n)((?:(?!\n\s*end\n).)*)(\n\s*end)

      new_content =
        Regex.replace(action_pattern, new_content, fn full_match,
                                                      action_start,
                                                      action_body,
                                                      action_end ->
          if not String.contains?(action_body, "__require_atomic?") do
            fixes_count = fixes_count + 1
            action_start <> action_body <> "\n    __require_atomic? false" <> action_end
          else
            full_match
          end
        end)
    end)

    {new_content, fixes_count}
  end
end

