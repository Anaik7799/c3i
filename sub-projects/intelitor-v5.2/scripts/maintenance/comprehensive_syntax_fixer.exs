  @spec run() :: any()
  def run do
    IO.puts("🔧 Starting comprehensive syntax fix for #{length(@files_to_fix)} files...")

    results =
      @files_to_fix
      |> Enum.with_index(1)
      |> Enum.map(fn {file, idx} ->
        IO.write("\r[#{idx}/#{length(@files_to_fix)}] Processing #{file}...")
        result = fix_file(file)

        case result do
          :ok -> IO.puts(" ✅")
          :unchanged -> IO.puts(" ⏭️")
          {:error, _} -> IO.puts(" ❌")
        end

        {file, result}
      end)

    successes = Enum.count(results, fn {_, result} -> result == :ok end)
    unchanged = Enum.count(results, fn {_, result} -> result == :unchanged end)
    failures = Enum.count(results, fn {_, result} -> match?({:error, _}, result) end)

    IO.puts("\n📊 Summary:")
    IO.puts("  ✅ Fixed: #{successes} files")
    IO.puts("  ⏭️  Unchanged: #{unchanged} files")
    IO.puts("  ❌ Failed: #{failures} files")

    # Show failures
    if failures > 0 do
      IO.puts("\n❌ Failed files:")

      results
      |> Enum.filter(fn {_, result} -> match?({:error, _}, result) end)
      |> Enum.each(fn {file, {:error, reason}} ->
        IO.puts("  - #{file}: #{inspect(reason)}")
      end)
    end
  end

  defp fix_file(file_path) do
    case File.read(file_path) do
      {:ok, content} ->
        fixed_content = apply_fixes(content, file_path)

        if fixed_content != content do
          File.write!(file_path, fixed_content)
          :ok
        else
          :unchanged
        end

      {:error, reason} ->
        {:error, reason}
    end
  end

  defp apply_fixes(content, file_path) do
    content
    |> fix_double_quotes_at_line_end()
    |> fix_constraint_syntax()
    |> fix_broken_strings()
    |> fix_case_statements()
    |> fix_broken_comments()
    |> fix_missing_commas_in_lists()
    |> fix_extra_end_quotes()
    |> fix_use_statement_breaks()
    |> fix_message_breaks()
    |> fix_specific_patterns(file_path)
  end

  # Fix patterns like: text")" or text"]" or text"}"
  defp fix_double_quotes_at_line_end(content) do
    content
    |> String.replace(~r/([^"\s])"\)"/, "\\1\"")
    |> String.replace(~r/([^"\s])"\]"/, "\\1\"]")
    |> String.replace(~r/([^"\s])"\}"/, "\\1\"}")
    |> String.replace(~r/"\s*"\)"/, "\"")
    |> String.replace(~r/"\s*"\]"/, "\"]")
    |> String.replace(~r/"\s*"\}"/, "\"}")
  end

  # Fix constraint syntax issues
  defp fix_constraint_syntax(content) do
    content
    |> String.replace(~r/constraints\s+max_length:\s*(\d+)"\s*\)"/, "constraints max_length: \\1")
    |> String.replace(
      ~r/constraints\s+max_length:\s*(\d+)""\s*end/,
      "constraints max_length: \\1\n    end"
    )
    |> String.replace(~r/constraints\s+one_of:\s*\[(.*?)\]"\]"/, "constraints one_of: [\\1]]")
    |> String.replace(
      ~r/constraints\s+items:\s*\[\s*one_of:\s*\[(.*?)\]"\]"/,
      "constraints items: [one_of: [\\1]]]"
    )
  end

  # Fix broken strings across lines
  defp fix_broken_strings(content) do
    content
    |> String.replace(~r/"([^"]*?)\n\s*(end|def|defp|\]|\)|\})/, "\"\\1\"\\n    \\2")
    |> String.replace(~r/"([^"]*?)\n\s*([a-z_]+:)/, "\"\\1\"\\n    \\2")
  end

  # Fix case __statement issues
  defp fix_case_statements(content) do
    content
    |> String.replace(~r/(\s*->\s*nil)"\s*\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*false)"\s*\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*true)"\s*\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*:ok)"\s*\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*:error)"\s*\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*\[\])"\s*\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*\[\])""\s*end/, "\\1\n        end")
  end

  # Fix broken comments
  defp fix_broken_comments(content) do
    content
    |> String.replace(~r/(#[^\n]*?)"\)"/, "\\1")
    |> String.replace(~r/(#[^\n]*?)"\]"/, "\\1")
    |> String.replace(~r/(#[^\n]*?)"\}"/, "\\1")
    |> String.replace(~r/(#[^\n]*?),([^\n]*?)$/, "\\1\\2")
  end

  # Fix missing commas in lists
  defp fix_missing_commas_in_lists(content) do
    content
    |> String.replace(~r/(:[\w_]+)"\]"/, "\\1]")
    |> String.replace(~r/(:[\w_]+)\s+:/, "\\1,\n      :")
  end

  # Fix extra quotes at end
  defp fix_extra_end_quotes(content) do
    content
    |> String.replace(~r/""(\s*end)/, "\"\\1")
    |> String.replace(~r/""(\s*\))/, "\"\\1")
    |> String.replace(~r/""(\s*\])/, "\"\\1")
    |> String.replace(~r/""(\s*\})/, "\"\\1")
  end

  # Fix use __statement line breaks
  defp fix_use_statement_breaks(content) do
    content
    |> String.replace(~r/use\s+([A-Z][\w.]+),"\s*domain:/, "use \\1,\n    domain:")
    |> String.replace(~r/use\s+([A-Z][\w.]+),"\s*otp_app:/, "use \\1,\n    otp_app:")
  end

  # Fix message breaks
  defp fix_message_breaks(content) do
    content
    |> String.replace(~r/message\s+"([^"]*?)"\s*and\s+([^"]+?)""/, "message \"\\1 and \\2\"")
    |> String.replace(
      ~r/message:\s*"([^"]*?)"\s*for\s*#\{([^}]+)\}\s*([^"]*?)""/,
      "message: \"\\1 for #\{\\2} \\3\""
    )
  end

  # Apply file-specific patterns
  defp fix_specific_patterns(content, file_path) do
    cond do
      String.contains?(file_path, "device_type.ex") ->
        content
        |> String.replace("one_o,f:", "one_of:")
        |> String.replace("Responsibilitie,s:", "Responsibilities:")
        |> String.replace("Cybernetic Feedbac,k:", "Cybernetic Feedback:")

      String.contains?(file_path, "/errors/") ->
        content
        |> String.replace(~r/message:\s*"([^"]*?)"\s*"\)"/, "message: \"\\1\"")

      String.contains?(file_path, "container_compliance.ex") ->
        content
        |> String.replace("_ -> false\")", "_ -> false")

      true ->
        content
    end
  end
end

