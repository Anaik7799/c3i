  @spec apply_spec_additions_to_file(term()) :: term()
  defp apply_spec_additions_to_file(file_path) do
    try do
      content = File.read!(file_path)
      lines = String.split(content, "\n")

      # Enhanced @spec addition approach with multiple safe patterns
      updated_lines = add_specs_to_functions(lines, file_path)

      updated_content = Enum.join(updated_lines, "\n")

      if content != updated_content do
        File.write!(file_path, updated_content)
      end
    rescue
      # Skip problematic files safely
      _error -> :ok
    end
  end

  @spec add_specs_to_functions(term(), term()) :: term()
  defp add_specs_to_functions(lines, file_path) do
    is_test_file = String.contains?(file_path, "_test.exs")

    # Skip test files for now (too complex and lower priority)
    if is_test_file do
      lines
    else
      add_specs_to_lines(lines, [])
    end
  end

  @spec add_specs_to_lines(list(), term()) :: term()
  defp add_specs_to_lines([], acc), do: Enum.reverse(acc)

  defp add_specs_to_lines([line | rest], acc) do
    trimmed = String.trim(line)

    cond do
      # Skip if already has @spec above
      has_spec_above?(acc) ->
        add_specs_to_lines(rest, [line | acc])

      # Public function-add simple @spec
      String.starts_with?(trimmed, "def ") and not String.starts_with?(trimmed, "defp") ->
        spec = generate_simple_spec(line)

        if spec do
          add_specs_to_lines(rest, [line, spec | acc])
        else
          add_specs_to_lines(rest, [line | acc])
        end

      # Private function-add simple @spec (lower priority)
      String.starts_with?(trimmed, "defp ") ->
        spec = generate_simple_private_spec(line)

        if spec do
          add_specs_to_lines(rest, [line, spec | acc])
        else
          add_specs_to_lines(rest, [line | acc])
        end

      # Normal line
      true ->
        add_specs_to_lines(rest, [line | acc])
    end
  end

  @spec has_spec_above?(term()) :: term()
  defp has_spec_above?(acc) do
    # Check if the previous non-empty line contains @spec
    case acc do
      [prev_line | _] when is_binary(prev_line) ->
        String.contains?(prev_line, "@spec")

      _ ->
        false
    end
  end

  @spec generate_simple_spec(term()) :: term()
  defp generate_simple_spec(line) do
    # Extract function name and basic pattern
    case extract_function_info(line) do
      {name, arity} when arity <= 2 ->
        # Only add @spec for simple functions (0-2 parameters)
        generate_basic_spec(name, arity)

      _ ->
        nil
    end
  end

  @spec generate_simple_private_spec(term()) :: term()
  defp generate_simple_private_spec(line) do
    # More conservative for private functions
    case extract_function_info(line) do
      {name, 0} ->
        # Only add @spec for private functions with no parameters
        generate_basic_private_spec(name)

      _ ->
        nil
    end
  end

  @spec extract_function_info(term()) :: term()
  defp extract_function_info(line) do
    # Extract function name and estimate arity
    trimmed = String.trim(line)

    cond do
      String.starts_with?(trimmed, "def ") ->
        parse_function_def(trimmed, "def ")

      String.starts_with?(trimmed, "defp ") ->
        parse_function_def(trimmed, "defp ")

      true ->
        nil
    end
  end

  @spec parse_function_def(term(), term()) :: term()
  defp parse_function_def(line, prefix) do
    # Simple parsing-extract name and count parameters
    without_prefix = String.replace_prefix(line, prefix, "")

    case String.split(without_prefix, ["(", " "], parts: 2) do
      [name | _] ->
        # Count parameters by counting commas + 1 if there are parentheses
        arity =
          if String.contains?(without_prefix, "(") and String.contains?(without_prefix, ")") do
            param_part =
              without_prefix
              |> String.split("(", parts: 2)
              |> List.last()
              |> String.split(")", parts: 2)
              |> List.first()

            if String.trim(param_part) == "" do
              0
            else
              String.split(param_part, ",") |> length()
            end
          else
            0
          end

        {String.trim(name), arity}

      _ ->
        nil
    end
  end

  @spec generate_basic_spec(term(), term()) :: term()
  defp generate_basic_spec(name, arity) do
    # Generate very simple @spec annotations
    case arity do
      0 ->
        "  @spec #{name}() :: any()"

      1 ->
        "  @spec #{name}(any()) :: any()"

      2 ->
        "  @spec #{name}(any(), any()) :: any()"

      _ ->
        nil
    end
  end

  @spec generate_basic_private_spec(term()) :: term()
