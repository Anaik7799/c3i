  @spec analyze_spec_function_patterns(term()) :: term()
  defp analyze_spec_function_patterns(source_files) do
    pattern_counts = %{
      public_functions: 0,
      private_functions: 0,
      module_functions: 0,
      api_functions: 0,
      test_functions: 0
    }

    Enum.reduce(source_files, pattern_counts, fn file_path, acc ->
      try do
        content = File.read!(file_path)
        lines = String.split(content, "\n")

        patterns = analyze_file_spec_patterns(lines, file_path)

        %{
          public_functions: acc.public_functions + patterns.public_functions,
          private_functions: acc.private_functions + patterns.private_functions,
          module_functions: acc.module_functions + patterns.module_functions,
          api_functions: acc.api_functions + patterns.api_functions,
          test_functions: acc.test_functions + patterns.test_functions
        }
      rescue
        _ -> acc
      end
    end)
  end

  @spec analyze_file_spec_patterns(term(), term()) :: term()
  defp analyze_file_spec_patterns(lines, file_path) do
    is_test_file = String.contains?(file_path, "_test.exs")
    is_api_file = String.contains?(file_path, "controller") or String.contains?(file_path, "_web")

    Enum.reduce(
      lines,
      %{
        public_functions: 0,
        private_functions: 0,
        module_functions: 0,
        api_functions: 0,
        test_functions: 0
      },
      fn line, acc ->
        cond do
          # Public function definition
          String.starts_with?(String.trim(line), "def ") and not String.contains?(line, "defp") ->
            if is_test_file do
              %{acc | test_functions: acc.test_functions + 1}
            else
              if is_api_file do
                %{acc | api_functions: acc.api_functions + 1}
              else
                %{acc | public_functions: acc.public_functions + 1}
              end
            end

          # Private function definition
          String.starts_with?(String.trim(line), "defp ") ->
            %{acc | private_functions: acc.private_functions + 1}

          # Module function (defmodule level)
          String.starts_with?(String.trim(line), "defmodule") ->
            %{acc | module_functions: acc.module_functions + 1}

          true ->
            acc
        end
      end
    )
  end

