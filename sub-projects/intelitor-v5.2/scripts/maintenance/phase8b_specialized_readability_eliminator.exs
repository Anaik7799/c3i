  @spec can_safely_wrap_line?(term()) :: term()
  defp can_safely_wrap_line?(line) do
    # Only wrap simple cases to avoid syntax errors
    # Comments
    # String literals
    # Map keys
    String.contains?(line, "  #") or
      String.contains?(line, "\"") or
      String.match?(line, ~r/^\s*\w+:/)
  end

  @spec wrap_line_safely(term()) :: term()
  defp wrap_line_safely(line) do
    cond do
      String.contains?(line, "  #") ->
        # Comment wrapping
        wrap_comment_line(line)

      String.contains?(line, "\"") ->
        # String wrapping
        wrap_string_line(line)

      true ->
        line
    end
  end

