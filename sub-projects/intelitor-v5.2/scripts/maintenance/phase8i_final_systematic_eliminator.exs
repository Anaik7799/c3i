    if is_test_file do
      lines
    else
      process_lines_for_specs(lines, [], %{in_module: false, module_name: nil})
    end
  end

  defp process_lines_for_specs([], acc, _context), do: Enum.reverse(acc)

  defp process_lines_for_specs([line | rest], acc, context) do
    trimmed = String.trim(line)
    updated_context = update_context(trimmed, __context)

    cond do
      # Skip if already has @spec
      has_recent_spec?(acc) ->
        process_lines_for_specs(rest, [line | acc], updated_context)

      # Public function-add enhanced @spec
      updated_context.in_module and String.starts_with?(trimmed, "def ") and
          not String.starts_with?(trimmed, "defp") ->
        spec = generate_enhanced_function_spec(line, :public)

        if spec do
          process_lines_for_specs(rest, [line, spec | acc], updated_context)
        else
          process_lines_for_specs(rest, [line | acc], updated_context)
        end

      # Normal line
      true ->
        process_lines_for_specs(rest, [line | acc], updated_context)
    end
  end

  @spec update_context(term(), term()) :: term()
  defp update_context(line, context) do
    cond do
      String.starts_with?(line, "defmodule") ->
        module_name = extract_module_name(line)
        %{__context | in_module: true, module_name: module_name}

      String.trim(line) == "end" and __context.in_module ->
        %{__context | in_module: false, module_name: nil}

      true ->
        __context
    end
  end

  @spec extract_module_name(term()) :: term()
  defp extract_module_name(line) do
    line
    |> String.replace("defmodule ", "")
    |> String.split(" ")
    |> List.first()
  end

  @spec has_recent_spec?(term()) :: term()
  defp has_recent_spec?(acc) do
