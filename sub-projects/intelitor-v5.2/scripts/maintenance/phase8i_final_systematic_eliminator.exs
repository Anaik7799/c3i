  @spec parse_function_signature(term()) :: term()
  defp parse_function_signature(line) do
    trimmed = String.trim(line)

    case String.split(trimmed, ["def ", "defp "], parts: 2) do
      [_prefix, function_part] ->
        case String.split(function_part, "(", parts: 2) do
          [name | _] ->
            # Count parameters
            arity =
              if String.contains?(function_part, "(") do
                param_section =
                  function_part
                  |> String.split("(", parts: 2)
                  |> List.last()
                  |> String.split(")", parts: 2)
                  |> List.first()

                count_function_parameters(param_section)
              else
                0
              end

            {String.trim(name), arity}

          _ ->
            nil
        end

      _ ->
        nil
    end
  end

  @spec count_function_parameters(term()) :: term()
  defp count_function_parameters(param_str) do
    if String.trim(param_str) == "" do
      0
    else
      # Simple parameter counting
      param_str
      |> String.split(",")
      |> Enum.map(&String.trim/1)
      |> Enum.filter(&(&1 != ""))
      |> length()
    end
  end

