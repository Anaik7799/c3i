  @spec apply_safe_line_breaks(term()) :: term()
  defp apply_safe_line_breaks(line) do
    cond do
      String.contains?(line, " |> ") and String.length(line) > 120 ->
        # Pipe operator break
        String.replace(line, " |> ", "\n    |> ", global: false)

      String.contains?(line, " and ") and String.length(line) > 120 ->
        String.replace(line, " and ", "\n      and ", global: false)

      String.contains?(line, ", ") and String.length(line) > 120 ->
        # Parameter list break
        String.replace(line, ", ", ",\n      ", global: false)

      true ->
        line
    end
  end

