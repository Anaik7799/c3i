  @spec main(any()) :: any()
  def main(_args \\ []) do
    IO.puts("🔧 Fixing malformed backslash escaping in test files...")

    test_files = Path.wildcard("test/demo/*test.exs")

    Enum.each(test_files, &fix_file/1)

    IO.puts("✅ Backslash escaping fixes complete!")
  end

  @spec fix_file(term()) :: term()
  defp fix_file(file_path) do
    content = File.read!(file_path)

    # Fix malformed backslash patterns
    new_content = content
    |> String.replace("attrs \\\\\\\\ %{}", "attrs \\\\ %{}")
    |> String.replace("attrs \\\\ %{}", "attrs \\\\ %{}")

    |> String.replace(~r/defp\s+tenant_fixture\(attrs\s+\\\\\s+%\{\}\)\s+do/,
      "defp tenant_fixture(attrs \\\\ %{}) do")
    |> String.replace("```elixir", "```elixir")
    |> String.replace("```", "```")

    if content != new_content do
      File.write!(file_path, new_content)
      IO.puts("  ✅ Fixed: #{file_path}")
    end
  end
