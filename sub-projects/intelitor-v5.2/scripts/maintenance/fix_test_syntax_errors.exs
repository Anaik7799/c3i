  def run do
    IO.puts("🔧 Fixing test syntax errors...")

    # Get all test files with the syntax error
    test_files = Path.wildcard("test/**/*_test.exs")

    fixed_count = 0

    Enum.each(test_files, fn file ->
      content = File.read!(file)

      # Fix single backslash to double backslash
      if String.contains?(content, "attrs \\ %{") do
        updated_content = String.replace(content, "attrs \\ %{", "attrs \\\\ %{")
        File.write!(file, updated_content)
        IO.puts("✓ Fixed: #{file}")
        fixed_count = fixed_count + 1
      end
    end)

    IO.puts("\n✅ Fixed #{fixed_count} files with syntax errors!")
  end
