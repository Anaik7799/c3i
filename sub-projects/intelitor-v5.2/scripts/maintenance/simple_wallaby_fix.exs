# Handle Chrome path for Wallaby testing
chrome_path = System.get_env("CHROMEDRIVER_PATH")

if chrome_path && File.exists?(chrome_path) do
  # Initialize Wallaby with proper Chrome configuration
  Application.put_env(:wallaby, :chromedriver, %{path: chrome_path})
  {:ok, _} = Application.ensure_all_started(:wallaby)
else
  # Disable Wallaby if Chrome is not available
  IO.puts("⚠️  Chrome driver not found - Wallaby tests will be skipped")
