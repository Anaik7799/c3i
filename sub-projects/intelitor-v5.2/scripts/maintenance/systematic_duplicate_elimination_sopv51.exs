  defp apply_math_utilities_replacement(content, file_path) do
    # Add alias if not present
    alias_added =
      if String.contains?(content, "alias Indrajaal.Shared.MathUtilities") do
        content
      else
        add_math_utilities_alias(content)
      end

    # Replace the update_average function calls
    alias_added
    |> String.replace(
      ~r/update_average\(/,
      "MathUtilities.update_average("
    )
    |> replace_update_average_definition()
  end

  defp add_math_utilities_alias(content) do
    # Find the right place to add the alias (after existing aliases or __requires)
    lines = String.split(content, "\n")

    # Find the last alias or __require line
    last_import_index =
      lines
      |> Enum.with_index()
      |> Enum.reverse()
      |> Enum.find(fn {line, _index} ->
        String.trim(line) |> String.starts_with?("alias ") or
          String.trim(line) |> String.starts_with?("__require ") or
          String.trim(line) |> String.starts_with?("import ")
      end)

    case last_import_index do
      {_, index} ->
        # Insert after the last import/alias/__require
        {_before, _after_lines} = Enum.split(lines, index + 1)

        (before ++ ["  alias Indrajaal.Shared.MathUtilities"] ++ after_lines)
        |> Enum.join("\n")

      nil ->
        # Find module declaration and add after it
        case Enum.find_index(lines, fn line -> String.contains?(line, "defmodule ") end) do
          # Can't find safe place, skip alias addition
          nil ->
            content

          module_index ->
            {_before, _after_lines} = Enum.split(lines, module_index + 1)

            (before ++ ["  alias Indrajaal.Shared.MathUtilities", ""] ++ after_lines)
            |> Enum.join("\n")
        end
    end
  end

  defp replace_update_average_definition(content) do
    # Replace the private function definition with a comment
