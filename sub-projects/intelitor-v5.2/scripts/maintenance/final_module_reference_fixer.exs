  defp fix_module_references(files) do
    files
    |> Enum.map(&fix_file_module_references/1)
    |> Enum.filter(fn {_file, count} -> count > 0 end)
    |> Enum.reduce({[], 0}, fn {file, count}, {fixed_files, total} ->
