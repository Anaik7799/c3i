  defp fix_file_module_references(file_path) do
    if File.exists?(file_path) do
      content = File.read!(file_path)

      # Count occurrences of triple underscore MODULE
      original_count = count_occurrences(content, "__MODULE__")

      if original_count > 0 do
        Logger.info("Fixing #{original_count} __MODULE__ patterns in #{file_path}")

        # Fix triple underscore MODULE references
        fixed_content = String.replace(content, "__MODULE__", "__MODULE__")

        # Verify the fix worked
        remaining_count = count_occurrences(fixed_content, "__MODULE__")

        if remaining_count == 0 do
          File.write!(file_path, fixed_content)
          {file_path, original_count}
        else
          Logger.warning("Some __MODULE__ patterns remain in #{file_path}")
          {file_path, original_count - remaining_count}
        end
