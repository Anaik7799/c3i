    end
  end

  @spec wrap_enhanced_comment(term()) :: term()
  defp wrap_enhanced_comment(line) do
    # Enhanced comment wrapping with better formatting
    if String.length(line) > 98 do
      case String.split(line, "#", parts: 2) do
        [prefix, comment] ->
          comment_trimmed = String.trim(comment)

          if String.length(comment_trimmed) > 80 do
            # Wrap comment at word boundary
            words = String.split(comment_trimmed, " ")
            {_first_line, __rest} = split_words_at_length(words, 75)
            first_line_text = Enum.join(first_line, " ")
            "#{prefix}# #{first_line_text} ..."
          else
            line
          end

        _ ->
          line
      end
    else
      line
    end
  end

  @spec split_words_at_length(term(), term()) :: term()
  defp split_words_at_length(words, max_length) do
    {first_part, _rest} =
      Enum.reduce_while(words, {[], 0}, fn word, {acc, current_length} ->
        new_length = current_length + String.length(word) + 1

        if new_length <= max_length do
          {:cont, {[word | acc], new_length}}
        else
          {:halt, {acc, current_length}}
        end
      end)

    first_words = Enum.reverse(first_part)
    remaining_words = Enum.drop(words, length(first_words))
    {first_words, remaining_words}
  end

