  @spec add_advanced_specs_to_functions(term(), term()) :: term()
  defp add_advanced_specs_to_functions(lines, file_path) do
    # Skip test files to focus on business logic
    is_test_file = String.contains?(file_path, "_test.exs")

    if is_test_file do
      lines
    else
      add_specs_with_advanced_context(lines, [], false, %{})
    end
  end

  defp add_specs_with_advanced_context([], acc, _in_module, _context), do: Enum.reverse(acc)

  defp add_specs_with_advanced_context([line | rest], acc, in_module, context) do
    trimmed = String.trim(line)

    cond do
      # Track module __context with enhanced tracking
      String.starts_with?(trimmed, "defmodule") ->
        module_name = extract_module_name(trimmed)
        _new_context = Map.put(__context, :current_module, module_name)
        add_specs_with_advanced_context(rest, [line | acc], true, new_context)

      # Enhanced spec detection-skip if already has @spec above
      has_advanced_spec_in_context?(acc) ->
        add_specs_with_advanced_context(rest, [line | acc], in_module, __context)

      # Public function in module - highest priority for @spec with advanced type inference
      in_module and String.starts_with?(trimmed, "def ") and
          not String.starts_with?(trimmed, "defp") ->
        spec = generate_advanced_spec(line, :public, __context)

        if spec do
          add_specs_with_advanced_context(rest, [line, spec | acc], in_module, __context)
        else
          add_specs_with_advanced_context(rest, [line | acc], in_module, __context)
        end

      # Private function in module-enhanced targeting
      in_module and String.starts_with?(trimmed, "defp ") ->
        spec = generate_advanced_spec(line, :private, __context)

        if spec do
          add_specs_with_advanced_context(rest, [line, spec | acc], in_module, __context)
        else
          add_specs_with_advanced_context(rest, [line | acc], in_module, __context)
        end

      # Normal line
      true ->
        add_specs_with_advanced_context(rest, [line | acc], in_module, __context)
    end
  end

  @spec extract_module_name(term()) :: term()
  defp extract_module_name(line) do
    line
    |> String.replace("defmodule", "")
    |> String.replace("do", "")
    |> String.trim()
  end

  @spec has_advanced_spec_in_context?(term()) :: term()
  defp has_advanced_spec_in_context?(acc) do
    # Check previous 5 lines for @spec with enhanced detection
    acc
    |> Enum.take(5)
    |> Enum.any?(fn line -> String.contains?(line, "@spec") end)
  end

  defp generate_advanced_spec(line, function_type, context) do
    case extract_advanced_function_info(line) do
      {name, arity} when arity <= 4 and function_type == :public ->
        # Generate @spec for public functions with enhanced type inference
        generate_enhanced_typed_spec(name, arity, __context)

      {name, arity} when arity <= 2 and function_type == :private ->
        # Generate @spec for private functions with up to 2 parameters
        generate_enhanced_private_spec(name, arity, __context)

      _ ->
        nil
    end
  end

  @spec extract_advanced_function_info(term()) :: term()
  defp extract_advanced_function_info(line) do
    trimmed = String.trim(line)

    cond do
      String.starts_with?(trimmed, "def ") ->
        parse_advanced_function_def(trimmed, "def ")

      String.starts_with?(trimmed, "defp ") ->
        parse_advanced_function_def(trimmed, "defp ")

      true ->
        nil
    end
  end

  @spec parse_advanced_function_def(term(), term()) :: term()
  defp parse_advanced_function_def(line, prefix) do
    without_prefix = String.replace_prefix(line, prefix, "")

    case String.split(without_prefix, "(", parts: 2) do
      [name_part | _] ->
        name = String.trim(name_part)

        # Enhanced arity detection with better parameter parsing
        arity =
          if String.contains?(without_prefix, "(") and String.contains?(without_prefix, ")") do
            param_section =
              without_prefix
              |> String.split("(", parts: 2)
              |> List.last()
              |> String.split(")", parts: 2)
              |> List.first()

            if String.trim(param_section) == "" or param_section == nil do
              0
            else
              # Enhanced parameter counting with pattern matching support
              count_advanced_parameters(param_section)
            end
          else
            0
          end

        {name, arity}

      _ ->
        nil
    end
  end

  @spec count_advanced_parameters(term()) :: term()
  defp count_advanced_parameters(param_str) do
    # Enhanced parameter counting handling nested structures and pattern matching
    param_str
    # Replace tuple patterns
    |> String.replace(~r/\{[^}]*\}/, "TUPLE")
    # Replace list patterns
    |> String.replace(~r/\[[^\]]*\]/, "LIST")
    # Replace struct patterns
    |> String.replace(~r/%[^}]*}/, "STRUCT")
    |> String.split(",")
    |> Enum.map(&String.trim/1)
    |> Enum.filter(&(&1 != ""))
    |> length()
  end

  @spec generate_enhanced_typed_spec(term(), term(), term()) :: term()
  defp generate_enhanced_typed_spec(name, arity, context) do
    # Generate enhanced @spec annotations with better type inference
    module_name = Map.get(__context, :current_module, "")

    case {name, arity} do
      {n, 0} when n in ["new", "build", "create"] ->
        "  @spec #{name}() :: {:ok, term()} | {:error, term()}"

      {n, 1} when n in ["get", "find", "fetch"] ->
        "  @spec #{name}(term()) :: {:ok, term()} | {:error, term()}"

      {n, 2} when n in ["update", "modify", "set"] ->
        "  @spec #{name}(term(), term()) :: {:ok, term()} | {:error, term()}"

      _ ->
        # Enhanced generic spec based on arity
        base_spec =
          case arity do
            0 -> "  @spec #{name}() :: term()"
            1 -> "  @spec #{name}(term()) :: term()"
            2 -> "  @spec #{name}(term(), term()) :: term()"
            3 -> "  @spec #{name}(term(), term(), term()) :: term()"
            4 -> "  @spec #{name}(term(), term(), term(), term()) :: term()"
            _ -> nil
          end

        # Check if it's a controller and adjust accordingly
        if (base_spec && String.contains?(to_string(module_name), "Controller")) and arity == 2 do
          "  @spec #{name}(Plug.Conn.t(), map()) :: Plug.Conn.t()"
        else
          base_spec
        end
    end
  end

  @spec generate_enhanced_private_spec(term(), term(), term()) :: term()
  defp generate_enhanced_private_spec(name, arity, __context) do
    # Enhanced private function specs
    case arity do
      0 -> "  @spec #{name}() :: term()"
      1 -> "  @spec #{name}(term()) :: term()"
      2 -> "  @spec #{name}(term(), term()) :: term()"
      _ -> nil
    end
  end

