    end
  end

  @spec apply_enhanced_line_wrapping(term()) :: term()
  defp apply_enhanced_line_wrapping(line) do
    cond do
      # Enhanced comment wrapping
      String.contains?(line, "#") and String.length(line) > 98 ->
        wrap_enhanced_comment(line)

      # Function definitions with many parameters
      String.contains?(line, "def ") and String.contains?(line, "(") and
        String.contains?(line, ",") and String.length(line) > 98 ->
        wrap_function_definition(line)

      # Function calls with many parameters
      String.contains?(line, "(") and String.contains?(line, ",") and
          String.length(line) > 98 ->
        wrap_function_call_enhanced(line)

      # Pipe operator chains
      String.contains?(line, "|>") and String.length(line) > 98 ->
