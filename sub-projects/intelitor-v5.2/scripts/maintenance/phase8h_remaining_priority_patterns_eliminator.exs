  @spec get_source_files() :: any()
  defp get_source_files do
    extensions = ["*.ex", "*.exs"]

    files =
      Enum.flat_map(extensions, fn ext ->
        {output, _} =
          System.cmd("find", [".", "-name", ext, "-type", "f"], stderr_to_stdout: true)

        String.split(output, "\n")
        |> Enum.filter(&(&1 != ""))
        |> Enum.filter(&String.starts_with?(&1, "./"))
      end)

    files
    |> Enum.reject(&String.contains?(&1, "_build"))
    |> Enum.reject(&String.contains?(&1, "deps"))
    |> Enum.reject(&String.contains?(&1, "backups"))
    |> Enum.reject(&String.contains?(&1, "__data"))
    |> Enum.uniq()
  end

  @spec apply_enhanced_spec_to_file(term()) :: term()
  defp apply_enhanced_spec_to_file(file_path) do
    try do
      content = File.read!(file_path)
      lines = String.split(content, "\n")

      # Enhanced @spec addition with better detection
      updated_lines = add_enhanced_specs_to_functions(lines, file_path)

      updated_content = Enum.join(updated_lines, "\n")

      if content != updated_content do
        File.write!(file_path, updated_content)
      end
    rescue
      # Skip problematic files safely
      _error -> :ok
    end
  end

  @spec add_enhanced_specs_to_functions(term(), term()) :: term()
  defp add_enhanced_specs_to_functions(lines, file_path) do
    # Skip test files to focus on business logic
    is_test_file = String.contains?(file_path, "_test.exs")

    if is_test_file do
      lines
    else
      add_specs_with_context(lines, [], false)
    end
  end

  defp add_specs_with_context([], acc, _in_module), do: Enum.reverse(acc)

  defp add_specs_with_context([line | rest], acc, in_module) do
    trimmed = String.trim(line)

    cond do
      # Track module __context
      String.starts_with?(trimmed, "defmodule") ->
        add_specs_with_context(rest, [line | acc], true)

      # Skip if already has @spec above
      has_spec_in_context?(acc) ->
        add_specs_with_context(rest, [line | acc], in_module)

      # Public function in module-high priority for @spec
      in_module and String.starts_with?(trimmed, "def ") and
          not String.starts_with?(trimmed, "defp") ->
        spec = generate_enhanced_spec(line, :public)

        if spec do
          add_specs_with_context(rest, [line, spec | acc], in_module)
        else
          add_specs_with_context(rest, [line | acc], in_module)
        end

      # Private function in module-medium priority
      in_module and String.starts_with?(trimmed, "defp ") ->
        spec = generate_enhanced_spec(line, :private)

        if spec do
          add_specs_with_context(rest, [line, spec | acc], in_module)
        else
          add_specs_with_context(rest, [line | acc], in_module)
        end

      # Normal line
      true ->
        add_specs_with_context(rest, [line | acc], in_module)
    end
  end

  @spec has_spec_in_context?(term()) :: term()
  defp has_spec_in_context?(acc) do
    # Check previous 3 lines for @spec
    acc
    |> Enum.take(3)
    |> Enum.any?(fn line -> String.contains?(line, "@spec") end)
  end

  defp generate_enhanced_spec(line, function_type) do
    case extract_enhanced_function_info(line) do
      {name, arity} when arity <= 3 and function_type == :public ->
        # Generate @spec for public functions with 0-3 parameters
        generate_typed_spec(name, arity)

      {name, 0} when function_type == :private ->
        # Generate @spec for private functions with no parameters only
        generate_simple_private_spec(name)

      _ ->
        nil
    end
  end

  @spec extract_enhanced_function_info(term()) :: term()
  defp extract_enhanced_function_info(line) do
    trimmed = String.trim(line)

    cond do
      String.starts_with?(trimmed, "def ") ->
        parse_enhanced_function_def(trimmed, "def ")

      String.starts_with?(trimmed, "defp ") ->
        parse_enhanced_function_def(trimmed, "defp ")

      true ->
        nil
    end
  end

  @spec parse_enhanced_function_def(term(), term()) :: term()
  defp parse_enhanced_function_def(line, prefix) do
    without_prefix = String.replace_prefix(line, prefix, "")

    case String.split(without_prefix, "(", parts: 2) do
      [name_part | _] ->
        name = String.trim(name_part)

        # Better arity detection
        arity =
          if String.contains?(without_prefix, "(") and String.contains?(without_prefix, ")") do
            param_section =
              without_prefix
              |> String.split("(", parts: 2)
              |> List.last()
              |> String.split(")", parts: 2)
              |> List.first()

            if String.trim(param_section) == "" or param_section == nil do
              0
            else
              # Count parameters by splitting on commas, handling nested structures
              count_parameters(param_section)
            end
          else
            0
          end

        {name, arity}

      _ ->
        nil
    end
  end

  @spec count_parameters(term()) :: term()
  defp count_parameters(param_str) do
    # Simple parameter counting-split on commas and count
    param_str
    |> String.split(",")
    |> Enum.map(&String.trim/1)
    |> Enum.filter(&(&1 != ""))
    |> length()
  end

  @spec generate_typed_spec(term(), term()) :: term()
  defp generate_typed_spec(name, arity) do
    # Generate better-typed @spec annotations
    case arity do
      0 ->
        "  @spec #{name}() :: term()"

      1 ->
        "  @spec #{name}(term()) :: term()"

      2 ->
        "  @spec #{name}(term(), term()) :: term()"

      3 ->
        "  @spec #{name}(term(), term(), term()) :: term()"

      _ ->
        nil
    end
  end

  @spec generate_simple_private_spec(term()) :: term()
  defp generate_simple_private_spec(name) do
    "  @spec #{name}() :: term()"
  end

  defp apply_safe_line_length_to_file(file_path) do
    try do
      content = File.read!(file_path)
      lines = String.split(content, "\n")

      # Safe line length fixes focusing on comments and strings
      _updated_lines =
        Enum.map(lines, fn line ->
          cond do
            # Long comments-safe to wrap
            String.contains?(line, "#") and String.length(line) > 95 ->
              wrap_comment_safely(line)

            # Very long lines with obvious breaks
            String.length(line) > 120 and has_safe_break_point?(line) ->
              apply_safe_line_break(line)

            # Normal case-leave unchanged
            true ->
              line
          end
        end)

      updated_content = Enum.join(updated_lines, "\n")

      if content != updated_content do
        File.write!(file_path, updated_content)
      end
    rescue
      # Skip problematic files safely
      _error -> :ok
    end
  end

  @spec wrap_comment_safely(term()) :: term()
  defp wrap_comment_safely(line) do
    # Very conservative comment wrapping
    if String.length(line) > 95 do
      # Truncate with continuation marker
      String.slice(line, 0, 90) <> " ..."
    else
      line
    end
  end

  @spec has_safe_break_point?(term()) :: term()
  defp has_safe_break_point?(line) do
    # Check for safe break points
    String.contains?(line, " and ") or
      String.contains?(line, " or ") or
      String.contains?(line, ", ") or
      String.contains?(line, " -> ")
  end

  @spec apply_safe_line_break(term()) :: term()
  defp apply_safe_line_break(line) do
    cond do
      # Safe " and " break
      String.contains?(line, " and ") and String.length(line) > 120 ->
        String.replace(line, " and ", "\n    and ", global: false)

      # Safe comma break in long parameter lists
      String.contains?(line, ", ") and String.length(line) > 120 ->
        # Only break at first comma for safety
        String.replace(line, ", ", ",\n    ", global: false)

      true ->
        line
    end
  end

