  @spec fix_file(term()) :: any()
  def fix_file(file_path) do
    case File.read(file_path) do
      {:ok, content} ->
        # Apply all common fixes
        fixed_content =
          content
          |> fix_unclosed_strings()
          |> fix_extra_quotes()
          |> fix_extra_parentheses()
          |> fix_missing_ends()
          |> fix_constraints_syntax()
          |> fix_extra_brackets()
          |> fix_missing_commas()
          |> fix_unclosed_maps()
          |> fix_comment_issues()

        if content != fixed_content do
          File.write!(file_path, fixed_content)
          IO.puts("✅ Fixed: #{file_path}")
          :ok
        else
          # Try to parse to see what the specific error is
          case check_syntax(file_path) do
            {:error, error} ->
              IO.puts("⚠️  Needs manual fix: #{file_path}")
              IO.puts("   Error: #{inspect(error)}")
              :error

            :ok ->
              :ok
          end
        end

      {:error, reason} ->
        IO.puts("❌ Could not read: #{file_path} - #{reason}")
        :error
    end
  end

  defp fix_unclosed_strings(content) do
    # Fix patterns like: "text
    content
    |> String.replace(~r/"([^"]*?)\n(\s*end|\s*\]|\s*\)|\s*\}|\s*,)/, "\"\\1\"\\2")
    |> String.replace(~r/"([^"]*?)\n(\s*[a-z_]+:)/, "\"\\1\"\\2")
    # Fix patterns with extra quotes at end like: text")"
    |> String.replace(~r/([^"])"\)"/, "\\1\"")
    |> String.replace(~r/([^"])"\]"/, "\\1\"]")
    |> String.replace(~r/([^"])"\}"/, "\\1\"}")
  end

  defp fix_extra_quotes(content) do
    # Fix patterns like: "text"]" -> "text"]
    content
    |> String.replace(
      ~r/"\]"/,
      "\"]"
      |> String.replace(~r/"\)"/, "\")")
      |> String.replace(~r/"\}"/, "\"}")
      # Fix double closing quotes
      |> String.replace(~r/""(\s*[,\]\)\}])/, "\"\\1")
    )
  end

  defp fix_extra_parentheses(content) do
    # Fix patterns like: _ -> nil")"
    content
    |> String.replace(~r/(\s*->\s*[^"\n]+)"\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*nil)"\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*false)"\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*true)"\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*:ok)"\)"/, "\\1")
    |> String.replace(~r/(\s*->\s*:error)"\)"/, "\\1")
  end

  defp fix_missing_ends(content) do
    # Count def/defp/defmodule vs end
    lines = String.split(content, "\n")

    def_count =
      Enum.count(lines, fn line ->
        line =~ ~r/^\s*(def|defp|defmodule|if|unless|case|cond|receive|try|with)\s+/
      end)

    end_count =
      Enum.count(lines, fn line ->
        line =~ ~r/^\s*end\s*$/
      end)

    if def_count > end_count do
      missing_ends = def_count - end_count
      content <> String.duplicate("\nend", missing_ends)
    else
      content
    end
  end

  defp fix_constraints_syntax(content) do
    # Fix constraints syntax patterns
    content
    |> String.replace(~r/constraints\s+max_length:\s*(\d+)"\)"/, "constraints max_length: \\1")
    |> String.replace(~r/constraints\s+one_of:\s*\[(.*?)\]"\]"/, "constraints one_of: [\\1]]")
    |> String.replace(~r/constraints\s+items:\s*\[(.*?)\]"\]"/, "constraints items: [\\1]]")
  end

  defp fix_extra_brackets(content) do
    # Fix extra brackets in lists
    content
    |> String.replace(~r/,\s*([a-zA-Z_:]+)"\]"/, ", \\1]")
    |> String.replace(~r/:([a-zA-Z_]+)"\]"/, ":\\1]")
  end

  defp fix_missing_commas(content) do
    # Add missing commas between map entries
    content
    |> String.replace(~r/("\w+"\s*=>\s*[^,\}]+)(\s*"\w+"\s*=>)/, "\\1,\\2")
    |> String.replace(~r/(\w+:\s*[^,\}]+)(\s*\w+:)/, "\\1,\\2")
  end

  defp fix_unclosed_maps(content) do
    # Fix unclosed maps - count %{ vs }
    open_maps = length(Regex.scan(~r/%\{/, content))
    close_maps = length(Regex.scan(~r/\}/, content))

    if open_maps > close_maps do
      missing_braces = open_maps - close_maps
      content <> String.duplicate("}", missing_braces)
    else
      content
    end
  end

  defp fix_comment_issues(content) do
    # Fix broken comment endings
    content
    |> String.replace(~r/(\s*#[^\n]*?)"\)"/, "\\1")
    |> String.replace(~r/(\s*#[^\n]*?)"\]"/, "\\1")
    |> String.replace(~r/(\s*#[^\n]*?)"\}"/, "\\1")
  end

  defp check_syntax(file_path) do
    try do
      case File.read!(file_path |> Code.string_to_quoted()) do
        {:ok, _ast} -> :ok
        {:error, error} -> {:error, error}
      end
    rescue
      e -> {:error, e}
    end
  end
