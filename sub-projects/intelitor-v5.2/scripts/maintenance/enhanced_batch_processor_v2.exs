  # Helper calculation functions
  defp calculate_total_processed(batch_results) do
    Enum.sum(Enum.map(batch_results, &(&1[:processed] || 0)))
  end

  defp calculate_total_success(batch_results) do
    Enum.sum(Enum.map(batch_results, &(&1[:success] || 0)))
  end

  defp calculate_success_rate(success, total) when total > 0 do
    Float.round(success / total * 100, 1)
  end

  defp calculate_success_rate(_, _), do: 0.0

  defp format_batch_results(batch_results) do
    batch_results
    |> Enum.map(fn batch ->
      category = batch[:category] || "unknown"
      processed = batch[:processed] || 0
      success = batch[:success] || 0
      rate = calculate_success_rate(success, processed)
      "#{category}: #{success}/#{processed} (#{rate}% success rate)"
    end)
    |> Enum.join("\n")
  end

  defp format_validation_results(validation_results) do
    validation_results
    |> Enum.map(fn {category, result} ->
      status = result[:status] || :unknown
      details = result[:details] || "No details"
      "#{category}: #{status} - #{details}"
    end)
    |> Enum.join("\n")
