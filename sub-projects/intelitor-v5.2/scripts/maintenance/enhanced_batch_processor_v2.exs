  defp apply_ep097_emoji_fixes(content) do
    content
    |> String.replace("✅", "[OK]")
    |> String.replace("❌", "[ERROR]")
    |> String.replace("⚠️", "[WARN]")
    |> String.replace("🔧", "[FIX]")
    |> String.replace("📊", "[STATS]")
