  @spec run() :: any()
  def run do
    IO.puts("🔧 Fixing specific Ash atomic operation warnings...")

    IO.puts(
      "📋 Based on actual compilation errors - Adding __require_atomic? false to function-based actions"
    )

    _total_fixes = 0

    Enum.each(@specific_warnings, fn {file_path, actions} ->
      if File.exists?(file_path) do
        fixes_applied = fix_file(file_path, actions)

        if fixes_applied > 0 do
          total_fixes = total_fixes + fixes_applied
        end
      else
        IO.puts("⚠️  File not found: #{file_path}")
      end
    end)

    IO.puts("✅ Applied #{total_fixes} total fixes for atomic warnings")
  end

  @spec fix_file(term(), term()) :: term()
  defp fix_file(file_path, actions) do
    content = File.read!(file_path)

    # Apply fixes for each action
    {updated_content, fixes_count} =
      Enum.reduce(actions, {content, 0}, fn action, {acc, count} ->
        {new_content, fixed?} = fix_action_in_content(acc, action)
        {new_content, if(fixed?, do: count + 1, else: count)}
      end)

    if updated_content != content do
      File.write!(file_path, updated_content)
      IO.puts("✅ Fixed #{fixes_count} actions in #{Path.basename(file_path)}")
      fixes_count
    else
      IO.puts("ℹ️  No changes needed in #{Path.basename(file_path)}")
      0
    end
  end

  @spec fix_action_in_content(term(), term()) :: term()
  defp fix_action_in_content(content, action_name) do
    # More precise pattern to match update actions and their complete blocks
    pattern = ~r/(update\s+:#{action_name}\s+do\n)(.*?)(\n\s+end(?:\n|$))/s

    case Regex.run(pattern, content) do
      [full_match, action_start, action_body, action_end] ->
        if String.contains?(action_body, "__require_atomic?") do
          # Already has __require_atomic? declaration
          {content, false}
        else
          # Check if this action has elements that __require non-atomic operation
          needs_atomic_false =
            String.contains?(action_body, "change fn") ||
              String.contains?(action_body, "validate fn") ||
              String.contains?(action_body, "Ash.Changeset.change_attribute") ||
              String.contains?(action_body, "DateTime.utc_now") ||
              String.contains?(action_body, "set_attribute") ||
              String.contains?(action_body, "after_action") ||
              String.contains?(action_body, "before_action")

          if needs_atomic_false do
            # Add __require_atomic? false at the end of the action body, before the
            new_action_body =
              String.trim_trailing(action_body) <> "\n\n      __require_atomic? false"

            new_full_match = action_start <> new_action_body <> action_end
            new_content = String.replace(content, full_match, new_full_match)
            {new_content, true}
          else
            {content, false}
          end
        end

      nil ->
        # Action not found
        {content, false}
    end
  end
end

