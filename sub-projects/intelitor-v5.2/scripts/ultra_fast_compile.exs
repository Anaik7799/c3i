  @spec compile_ultra_fast() :: any()
  defp compile_ultra_fast do
    IO.puts("\n⚡ Starting ULTRA-FAST compilation...")

    # Set maximum optimization environment
    envs = [
      {"MIX_ENV", "dev"},
      {"ERL_AFLAGS", "+P 10_000_000 +Q 1_000_000 +K true +A 256 +sbt db +sub true +Muacnl 0"},
      {"ELIXIR_ERL_OPTIONS", "+P 10_000_000 +Q 65_536 +hmbs 46_422 +hms 8348"},
      {"ELIXIR_COMPILER_OPTS", "--no-warnings-as-errors"},
      {"MIX_QUIET", "1"},
      {"MIX_BUILD_EMBEDDED", "true"},
      {"SKIP_ASH_COMPILE_VALIDATION", "true"}
    ]

    Enum.each(envs, fn {k, v} -> System.put_env(k, v) end)

    # Clean only problematic parts
    IO.puts("  Cleaning build artifacts...")
    System.cmd("rm", ["-rf", "_build/dev/lib/indrajaal"])

    # First compile deps (should be fast since already done)
    IO.puts("\n  Step 1/4: Dependencies...")

    {_, _} =
      System.cmd("mix", ["deps.compile"],
        env: envs,
        into: IO.stream(:stdio, :line),
        stderr_to_stdout: true
      )

    # Compile protocols separately
    IO.puts("\n  Step 2/4: Protocols...")

    {_, _} =
      System.cmd("mix", ["compile.protocols"],
        env: envs,
        into: IO.stream(:stdio, :line),
        stderr_to_stdout: true
      )

    # Pre-compile the most problematic files with relaxed settings
    IO.puts("\n  Step 3/4: Pre-compiling heavy modules...")

    heavy_modules = [
      "lib/indrajaal/base_resource.ex",
      "lib/indrajaal/multitenancy/tenant_resource.ex",
      "lib/indrajaal/core.ex",
      "lib/indrajaal/accounts.ex",
      "lib/indrajaal/policy.ex"
    ]

    Enum.each(heavy_modules, fn file ->
      if File.exists?(file) do
        IO.puts("    Pre-compiling #{Path.basename(file)}...")

        System.cmd(
          "elixirc",
          ["--ignore-module-conflict", "-o", "_build/dev/lib/indrajaal/ebin", file],
          env: envs,
          stderr_to_stdout: true
        )
      end
    end)

    # Final compilation with all cores
    IO.puts("\n  Step 4/4: Main compilation...")
    start_time = System.monotonic_time(:second)

    {output, exit_code} =
      System.cmd("mix", ["compile", "--force", "--all-warnings"],
        env: envs,
        into: IO.stream(:stdio, :line),
        stderr_to_stdout: true
      )

    duration = System.monotonic_time(:second)-start_time

    IO.puts("\n📊 Compilation Results:")
    IO.puts("  Duration: #{duration} seconds")
    IO.puts("  Status: #{if exit_code == 0, do: "✅ Success", else: "⚠️  Completed

    # Save compilation log
    File.write!("ultra_fast_compilation.log", output || "")
  end

