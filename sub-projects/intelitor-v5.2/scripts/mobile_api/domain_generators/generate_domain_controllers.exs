  @spec generate_all() :: any()
  def generate_all do
    IO.puts("🎯 Starting controller generation for #{length(@domains)} domains..."

    # Create directories
    File.mkdir_p!("lib/indrajaal_web/controllers/api/mobile/config")
    File.mkdir_p!("lib/indrajaal_web/views/api/mobile/config")

    # Log start
    log_start()

    # Generate controllers in parallel by worker
    @domains
    |> Enum.group_by(& &1.worker)
    |> Enum.map(fn {worker, domains} ->
      Task.async(fn ->
        generate_worker_controllers(worker, domains)
      end)
    end)
    |> Task.await_many(60_000)

    # Generate router updates
    generate_router_updates()

    IO.puts("✅ Controller generation complete!")
    log_completion()
  end

  @spec generate_worker_controllers(term(), term()) :: term()
  defp generate_worker_controllers(worker, domains) do
    Enum.each(domains, fn domain ->
      IO.puts("  #{worker}: Generating controller for #{domain.name}...")
      generate_controller(domain)
      generate_view(domain)
    end)
  end

