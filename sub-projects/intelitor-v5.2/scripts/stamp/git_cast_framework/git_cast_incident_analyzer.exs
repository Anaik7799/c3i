  @spec analyze_incident(term()) :: term()
  defp analyze_incident(incident_id) do
    IO.puts("🔍 Starting comprehensive CAST analysis for incident: #{incident_id}"

    # Record investigation start
    GitTelemetryCollector.record_git_event(
      [:indrajaal, :cast, :investigation, :start],
      %{incident_id: incident_id, timestamp: DateTime.utc_now()},
      %{investigation_type: :comprehensive, incident_id: incident_id}
    )

    # Execute all investigation phases
    investigation_results = Enum.reduce(@investigation_phases, %{}, fn phase, acc ->
      IO.puts("📋 Executing phase: #{phase}")

      phase_result = execute_investigation_phase(phase, incident_id)
      Map.put(acc, phase, phase_result)
    end)

    # Generate comprehensive analysis summary
    analysis_summary = generate_analysis_summary(incident_id, investigation_results)

    # Record investigation completion
    GitTelemetryCollector.record_git_event(
      [:indrajaal, :cast, :investigation, :stop],
      %{
        incident_id: incident_id,
        duration: calculate_investigation_duration(investigation_results),
        phases_completed: length(@investigation_phases)
      },
      %{
        investigation_results: investigation_results,
        analysis_summary: analysis_summary
      }
    )

    IO.puts("✅ CAST investigation completed for incident: #{incident_id}")
    IO.puts("📊 Analysis Summary:")
    print_analysis_summary(analysis_summary)

    investigation_results
  end

