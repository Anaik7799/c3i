  @spec verify_test_safety(any()) :: any()
  def verify_test_safety(test_file) do
    content = File.read!(test_file)

    violations = []

    # Check for async: false without justification
    if content =~ ~r/async:\s*false/ and not (content =~ ~r/# SAFETY:/) do
      violations = ["async: false without safety justification" | violations]
    end

    # Check for production URLs/IPs
    if content =~ ~r/\d+\.\d+\.\d+\.\d+/ and not (content =~ ~r/127\.0\.0\.1|localhost/) do
      violations = ["Possible production IP address" | violations]
    end

    # Check for real email domains
    if content =~ ~r/@(gmail|yahoo|hotmail|outlook)\.com/ do
      violations = ["Real email domain used" | violations]
    end

    # Check for skipped auth
    if content =~ ~r/skip_auth|bypass_auth/ do
      violations = ["Possible authentication bypass" | violations]
    end

    # Check for missing cleanup
    if content =~ ~r/setup\s+do/ and not (content =~ ~r/on_exit/) do
      violations = ["Setup without on_exit cleanup" | violations]
    end

    {:ok, violations}
  end

