  @spec get_system_memory() :: any()
  defp get_system_memory do
    case :os.type() do
      {:unix, :linux} ->
        case System.cmd("free", ["-b"]) do
          {output, 0} ->
            output
            |> String.split("\n")
            |> Enum.at(1)
            |> String.split()
            |> Enum.at(1)
            |> String.to_integer()
          _ -> 8 * 1024 * 1024 * 1024  # Default 8GB
        end
      _ -> 8 * 1024 * 1024 * 1024  # Default 8GB
    end
  end

