  @spec write_test_config(term()) :: term()
  defp write_test_config(vm_args) do
    config_dir = "config"
    File.mkdir_p!(config_dir)

    # Write test-specific VM args
    vm_args_path = Path.join(config_dir, "test.vm.args")
    File.write!(vm_args_path, vm_args)
    IO.puts("\n✅ Created: #{vm_args_path}")

    # Write test environment config
    test_config = """
    # Test Environment Configuration
    # Maximum Parallelization Settings

    import Config

    # Configure test environment
    config :indrajaal, :environment, :test

    # Disable unnecessary features for faster tests
    config :logger, level: :warning

    # Configure Ecto for parallel tests
    config :indrajaal, Indrajaal.Repo,
      pool: Ecto.Adapters.SQL.Sandbox,
      pool_size: System.schedulers_online() * 2,
      queue_target: 5000,
      queue_interval: 10_000

    # Disable telemetry for tests
    config :telemetry_poller, :default, enabled: false

    # Maximum test parallelization
    config :ex_unit,
      max_cases: System.schedulers_online() * 2,
      timeout: :infinity,
      assert_receive_timeout: :infinity
    """

    test_config_path = Path.join(config_dir, "test.exs")

    # Only append if not already configured
    if File.exists?(test_config_path) do
      content = File.read!(test_config_path)
      unless String.contains?(content, "Maximum Parallelization Settings") do
        File.write!(test_config_path, content <> "\n\n" <> test_config)
        IO.puts("✅ Updated: #{test_config_path}")
      else
        IO.puts("✓ Already configured: #{test_config_path}")
      end
    else
      File.write!(test_config_path, test_config)
      IO.puts("✅ Created: #{test_config_path}")
    end
  end

