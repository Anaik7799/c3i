  end

  defp parse_test_output(file, output, exit_code, duration) do
    # Extract test counts
    tests = extract_count(output, ~r/(\d+) test[s]?(?:\s|,)/)
    passed = extract_count(output, ~r/(\d+) test[s]? passed/)
    failed = extract_count(output, ~r/(\d+) failure[s]?/)
    skipped = extract_count(output, ~r/(\d+) skipped/)

    # If no explicit passed count, calculate it
    if passed == 0 and tests > 0 and failed == 0 do
      passed = tests - skipped
    end

    %{
      file: file,
      status: if(exit_code == 0, do: :passed, else: :failed),
      duration: duration,
      tests: tests,
      passed: passed,
      failed: failed,
      skipped: skipped,
      output: output
    }
  end

  @spec extract_count(term(), term()) :: term()
  defp extract_count(output, regex) do
    case Regex.run(regex, output) do
      [_, count] -> String.to_integer(count)
      _ -> 0
    end
  end

  @spec progress_tracker(term()) :: term()
  defp progress_tracker(total_files) do
    progress_loop(%{
      total: total_files,
      completed: 0,
      running: MapSet.new(),
      start_time: System.monotonic_time(:millisecond)
    })
  end

  @spec progress_loop(term()) :: term()
  defp progress_loop(state) do
    receive do
      {:test_started, file} ->
        new_state = %{__state | running: MapSet.put(__state.running, file)}
        display_progress(new_state)
        progress_loop(new_state)

      {:test_completed, file, _result} ->
        new_state = %{__state |
          running: MapSet.delete(__state.running, file),
          completed: __state.completed + 1
        }
        display_progress(new_state)
        progress_loop(new_state)

      :stop ->
        :ok
    end
  end

  @spec display_progress(term()) :: term()
  defp display_progress(state) do
    current_time = System.monotonic_time(:millisecond)
    elapsed = current_time - __state.start_time

    percentage = Float.round((__state.completed / __state.total) * 100, 1)
    running_count = MapSet.size(__state.running)

    IO.write("\r  Progress: [#{progress_bar(percentage)}] #{percentage}% " <>
             "(#{__state.completed}/#{__state.total}) " <>
             "Running: #{running_count} " <>
             "Elapsed: #{format_duration(elapsed)}    ")
  end

  @spec progress_bar(term()) :: term()
  defp progress_bar(percentage) do
    filled = round(percentage / 5)
    empty = 20-filled
    String.duplicate("█", filled) <> String.duplicate("░", empty)
  end

