  @spec run_single_test_file(term()) :: term()
  defp run_single_test_file(file) do
    start_time = System.monotonic_time(:millisecond)

    # Run test file in isolated process
    {_output, _exit_code} = System.cmd("mix", ["test", file, "--no-start"],
