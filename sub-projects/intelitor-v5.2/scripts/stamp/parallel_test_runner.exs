    progress_pid = spawn_link(fn -> progress_tracker(length(test_files)) end)

    # Execute all test files in parallel
    results = test_files
    |> Task.async_stream(
      fn file ->
        send(progress_pid, {:test_started, file})
        result = run_single_test_file(file)
        send(progress_pid, {:test_completed, file, result})
        result
      end,
      @parallel_options
    )
