  __require Logger

  @prohibited_registries [
    "registry.nixos.org/nixos/",
    "registry.registry.nixos.org/",
    "quay.io/",
    "gcr.io/"
  ]

  @spec main(any()) :: any()
  def main(args) do
    case args do
      ["--validate"] -> validate_containers()
      ["--analyze"] -> analyze_container_images()
      ["--compliance"] -> check_compliance()
      _ -> show_help()
    end
  end

  @spec validate_containers() :: any()
  defp validate_containers do
    Logger.info("🔍 PCIS Simple Container Validation")

    containers = get_all_containers()

    Logger.info("Found #{length(containers)} containers to validate")

    results = Enum.map(containers, &validate_container/1)

    display_results(results)
  end

  @spec analyze_container_images() :: any()
  defp analyze_container_images do
    Logger.info("🔍 PCIS Container Image Analysis")

    containers = get_all_containers()

    Enum.each(containers, fn container ->
      analyze_container_image(container)
    end)
  end

  @spec check_compliance() :: any()
  defp check_compliance do
    Logger.info("🔍 PCIS Compliance Check")

    containers = get_all_containers()

    compliant_count = 0
    total_count = length(containers)

    {compliant_count, violations} =
      Enum.reduce(containers, {0, []}, fn container, {compliant, viols} ->
        case validate_container_compliance(container) do
          {:ok, _} ->
            {compliant + 1, viols}

          {:error, violation} ->
            {compliant, [violation | viols]}
        end
      end)

    compliance_score =
      if total_count > 0, do: Float.round(compliant_count / total_count * 100, 1), else: 100.0

    Logger.info("""

    📊 PCIS Compliance Summary
    ══════════════════════════════════════════════════════════════
    Total Containers: #{total_count}
    Compliant: #{compliant_count}
    Compliance Score: #{compliance_score}%
    Violations: #{length(violations)}
    """)

    if violations != [] do
      Logger.info("Violations found:")

      Enum.each(violations, fn violation ->
        Logger.warning("  • #{violation}")
      end)
    end

    if compliance_score == 100.0 do
      Logger.info("✅ EXCELLENT: 100% PCIS compliance achieved!")
    else
      Logger.warning("⚠️  Compliance issues detected - remediation recommended")
    end
  end

  @spec get_all_containers() :: any()
  defp get_all_containers do
    case System.cmd("podman", ["ps", "-a", "--format", "{{.Names}}\t{{.Image}}\t{{.Status}}"]) do
      {output, 0} ->
        output
        |> String.split("\n", trim: true)
        |> Enum.map(&parse_container_line/1)
        |> Enum.filter(&(&1 != nil))
        |> Enum.filter(&is_indrajaal_container?/1)

      {error, _} ->
        Logger.error("Failed to get containers: #{error}")
        []
    end
  end

  @spec parse_container_line(term()) :: term()
  defp parse_container_line(line) do
    case String.split(line, "\t") do
      [name, image, status] ->
        %{name: name, image: image, status: status}

      _ ->
        nil
    end
  end

  @spec is_indrajaal_container?(term()) :: term()
  defp is_indrajaal_container?(container) do
    String.starts_with?(container.name, "indrajaal-") or
      String.starts_with?(container.name, "TPS (Toyota Production System (TPS)) methodology-")
  end

  @spec validate_container(term()) :: term()
  defp validate_container(container) do
    violations = []

    # 1.0 - Check image compliance
    image_violations = check_image_compliance(container.image)

    # 1.0 - Check volume mounts (simplified)
    volume_violations = check_volume_compliance(container.name)

    all_violations = image_violations ++ volume_violations

    %{
      container: container.name,
      image: container.image,
      status: container.status,
      violations: all_violations,
      compliant: length(all_violations) == 0
    }
  end

  @spec validate_container_compliance(term()) :: term()
  defp validate_container_compliance(container) do
    violations = []

    # 1.0 - Check image
    if is_prohibited_image?(container.image) do
      violations = ["Prohibited image: #{container.image}"] ++ violations
    end

    # 1.0 - Check for external volume mounts
    case get_container_mounts(container.name) do
      {:ok, mounts} ->
        external_mounts = Enum.filter(mounts, &is_external_mount?/1)

        if external_mounts != [] do
          violations = ["External volume mounts detected"] ++ violations
        end

      _ ->
        violations = ["Unable to inspect container mounts"] ++ violations
    end

    if violations == [] do
      {:ok, "Container is compliant"}
    else
      {:error, "#{container.name}: #{Enum.join(violations, ", ")}"}
    end
  end

  @spec check_image_compliance(term()) :: term()
  defp check_image_compliance(image) do
    if is_prohibited_image?(image) do
      ["Non-compliant image: #{image} (should use NixOS via DevEnv)"]
    else
      []
    end
  end

  @spec check_volume_compliance(term()) :: term()
  defp check_volume_compliance(container_name) do
    case get_container_mounts(container_name) do
      {:ok, mounts} ->
        external_mounts = Enum.filter(mounts, &is_external_mount?/1)

        Enum.map(external_mounts, fn mount ->
          "External volume mount: #{mount}"
        end)

      {:error, _} ->
        ["Unable to inspect volume mounts"]
    end
  end

  @spec get_container_mounts(term()) :: term()
  defp get_container_mounts(container_name) do
    case System.cmd(
           "podman",
           [
             "inspect",
             container_name,
             "--format",
             "{{range .Mounts}}{{.Source}}:{{.Destination}} {{end}}"
           ]
         ) do
      {output, 0} ->
        mounts =
          output
          |> String.trim()
          |> String.split(" ", trim: true)

        {:ok, mounts}

      {_error, _} ->
        {:error, "Inspection failed"}
    end
  end

  @spec is_external_mount?(term()) :: term()
  defp is_external_mount?(mount) do
    case String.split(mount, ":") do
      [source, _destination] ->
        not is_local_path?(source)

      _ ->
        false
    end
  end

  @spec is_local_path?(term()) :: term()
  defp is_local_path?(path) when is_binary(path) do
    # 1.0 - Allow some container-internal paths
    String.starts_with?(path, "./") or
      String.starts_with?(path, "__data/") or
      String.starts_with?(path, "logs/") or
      String.starts_with?(path, "artifacts/") or
      String.starts_with?(path, "backups/") or
      String.starts_with?(path, "/app") or
      String.starts_with?(path, "/tmp")
  end

  @spec is_local_path?(term()) :: term()
  defp is_local_path?(_), do: false

  defp is_prohibited_image?(image) do
    Enum.any?(@prohibited_registries, &String.starts_with?(image, &1))
  end

  @spec analyze_container_image(term()) :: term()
  defp analyze_container_image(container) do
    compliance_status =
      if is_prohibited_image?(container.image), do: "❌ NON-COMPLIANT", else: "✅ COMPLIANT"

    Logger.info("#{container.name}:")
    Logger.info("  Image: #{container.image}")
    Logger.info("  Status: #{container.status}")
    Logger.info("  Compliance: #{compliance_status}")

    if is_prohibited_image?(container.image) do
      recommended = recommend_nixos_image(container.image)
      Logger.info("  Recommended: #{recommended}")
    end

    Logger.info("")
  end

  @spec recommend_nixos_image(term()) :: term()
  defp recommend_nixos_image(image) do
    cond do
      String.contains?(image, "postgres") -> "nixos/postgresql17"
      String.contains?(image, "elixir") -> "nixos/elixir118"
      String.contains?(image, "prometheus") -> "nixos/prometheus"
      String.contains?(image, "grafana") -> "nixos/grafana"
      String.contains?(image, "nginx") -> "nixos/nginx"
      true -> "nixos/stable"
    end
  end

  @spec display_results(term()) :: term()
  defp display_results(results) do
    total = length(results)
    compliant = Enum.count(results, & &1.compliant)
    non_compliant = total - compliant

    compliance_score = if total > 0, do: Float.round(compliant / total * 100, 1), else: 100.0

    Logger.info("""

    📊 PCIS Container Validation Results
    ══════════════════════════════════════════════════════════════
    Total Containers: #{total}
    Compliant: #{compliant}
    Non-Compliant: #{non_compliant}
    Compliance Score: #{compliance_score}%
    """)

    if non_compliant > 0 do
      Logger.info("Non-compliant containers:")

      results
      |> Enum.filter(&(not &1.compliant))
      |> Enum.each(fn result ->
        Logger.warning("❌ #{result.container}:")

        Enum.each(result.violations, fn violation ->
          Logger.warning("   • #{violation}")
        end)
      end)

      Logger.info("\nRecommendations:")
      Logger.info("1. Migrate container images to NixOS via DevEnv/Nix")
      Logger.info("2. Update volume mounts to use relative paths")

      Logger.info(
        "3. Run full migration: elixir scripts/pcis/migration/container_image_migrator.exs --plan"
      )
    else
      Logger.info("🎉 EXCELLENT: All containers are PCIS compliant!")
    end
  end

