  defp find_pattern_matches(output, patterns) do
    Enum.flat_map(patterns, fn pattern ->
      output
      |> String.split("\n")
      |> Enum.with_index()
      |> Enum.filter(fn {line, _index} -> Regex.match?(pattern, line) end)
      |> Enum.map(fn {line, index} -> 
        %{line: line, pattern: inspect(pattern), line_number: index + 1}
      end)
    end)
  end

  defp check_validation_consensus(methods) do
    _error_counts = Enum.map(methods, fn {_method, result} -> result.error_count end)
    _warning_counts = Enum.map(methods, fn {_method, result} -> result.warning_count end)
    
    error_consensus = Enum.uniq(error_counts) |> length() == 1
    warning_consensus = Enum.uniq(warning_counts) |> length() == 1
    
    %{
      agreement: error_consensus and warning_consensus,
      error_consensus: error_consensus,
      warning_consensus: warning_consensus,
      error_counts: error_counts,
      warning_counts: warning_counts,
      methods_count: map_size(methods)
    }
  end

  defp validate_single_constraint(constraint, validation_result) do
    case constraint do
      "SC-CV-001: System SHALL detect 100% of compilation errors" ->
        %{
          constraint: constraint,
          passed: validation_result.consensus_achieved and 
                 hd(validation_result.error_counts) >= 0,
          criticality: :critical,
          reason: if(validation_result.consensus_achieved, do: "Consensus achieved", else: "Methods disagree")
        }
      
      "SC-CV-002: System SHALL NOT report success with any errors present" ->
        has_errors = validation_result.consensus_achieved and hd(validation_result.error_counts) > 0
        %{
          constraint: constraint,
          passed: not has_errors or not validation_result.consensus_achieved,
          criticality: :critical,
          reason: if(has_errors, do: "Errors detected correctly", else: "No errors present")
        }
      
      "SC-CV-003: System SHALL validate using multiple independent methods" ->
        %{
          constraint: constraint,
          passed: map_size(validation_result.methods) >= 3,
          criticality: :high,
          reason: "Using #{map_size(validation_result.methods)} validation methods"
        }
      
      "SC-CV-005: System SHALL halt on validation discrepancies" ->
        %{
          constraint: constraint,
          passed: not validation_result.consensus_achieved,
          criticality: :critical,
          reason: if(validation_result.consensus_achieved, do: "Consensus achieved", else: "Properly halted on disagreement")
        }
      
      _ ->
        %{
          constraint: constraint,
          passed: true,
          criticality: :medium,
          reason: "Default validation passed"
        }
    end
  end

  defp check_ep110_scenario(validation_result) do
    # EP-110: False negative (0 errors when errors exist)
    has_consensus = validation_result.consensus_achieved
    error_count = if has_consensus, do: hd(validation_result.error_counts), else: -1
    
    # Check if any method detected errors but consensus shows 0
    _method_errors = Enum.map(validation_result.methods, fn {_method, result} -> 
      result.error_count 
    end)
    
    max_errors = Enum.max(method_errors, fn -> 0 end)
    min_errors = Enum.min(method_errors, fn -> 0 end)
    
    ep110_risk = has_consensus and error_count == 0 and max_errors > 0
    
    %{
      name: "EP-110 False Negative Pr__evention",
      passed: not ep110_risk,
      risk_level: if(ep110_risk, do: :critical, else: :low),
      details: %{
        consensus_error_count: error_count,
        max_method_errors: max_errors,
        min_method_errors: min_errors,
        false_negative_detected: ep110_risk
      }
    }
  end

  defp check_ep111_scenario(validation_result) do
    # EP-111: Process drift (methods becoming unreliable over time)
    method_results = validation_result.methods
    
    # Check variance in error detection
    _error_counts = Enum.map(method_results, fn {_method, result} -> result.error_count end)
    error_variance = calculate_variance(error_counts)
    
    # High variance indicates potential drift
    drift_risk = error_variance > 5.0
    
    %{
      name: "EP-111 Process Drift Pr__evention",
      passed: not drift_risk,
      risk_level: if(drift_risk, do: :high, else: :low),
