  defp calculate_variance(numbers) do
    if length(numbers) <= 1 do
      0.0
    else
      mean = Enum.sum(numbers) / length(numbers)
      variance_sum = Enum.reduce(numbers, 0, fn x, acc ->
        acc + :math.pow(x - mean, 2)
      end)
      variance_sum / (length(numbers) - 1)
    end
  end

