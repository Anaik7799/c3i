      String.contains?(line, "warning:") and
      not String.contains?(line, "error:")
    end)
    
    %{
      method: "__contextual_line_analysis",
      errors: error_lines,
      warnings: warning_lines,
      error_count: length(error_lines),
      warning_count: length(warning_lines),
      total_lines_analyzed: length(lines)
    }
  end

