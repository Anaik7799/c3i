  @doc """
  Find all enhanced environment files.
  """
  @spec find_enhanced_environment_files() :: any()
  def find_enhanced_environment_files do
    case System.cmd("grep",
      ["-r", "-l", "SOPv5.1 ENHANCED ENVIRONMENT CONFIGURATION", "."], stderr_to_stdout: true) do
      {output, 0} ->
        output
        |> String.split("\n")
        |> Enum.filter(&(&1 != ""))
        |> Enum.sort()

      {_error, _} ->
        []
    end
  end

  @doc """
  Validate individual environment file enhancement.
  """
  @spec validate_environment_enhancement(any()) :: any()
  def validate_environment_enhancement(file_path) do
    try do
      {:ok, content} = File.read(file_path)

      validations = %{
        sopv51_header: String.contains?(content, "SOPv5.1 ENHANCED ENVIRONMENT CONFIGURATION"),
        framework_variables: String.contains?(content, "SOPV51_ENABLED=true"),
        patient_mode: String.contains?(content, "PATIENT_MODE=enabled"),
        container_variables: String.contains?(content, "CONTAINER_ONLY=true"),
        agent_coordination: String.contains?(content, "AGENT_COORDINATION=enabled"),
        framework_integration: String.contains?(content, "TPS_INTEGRATION=enabled"),
        enhancement_footer: String.contains?(content, "SOPv5.1 ENVIRONMENT ENHANCEMENT COMPLETE"),
        cybernetic_execution: String.contains?(content, "CYBERNETIC_EXECUTION=true")
      }

      validation_score = validations
      |> Map.values()
      |> Enum.count(&(&1))
      |> Kernel./(8)
      |> Kernel.*(100)

      {:ok, %{file: file_path, score: validation_score, validations: validations}}

    rescue
      error ->
        {:error, %{file: file_path, error: error}}
    end
  end

