  @spec scan_for_missing_tests() :: any()
  defp scan_for_missing_tests do
    # Scan for .exs files and check for corresponding tests
    demo_scripts = Path.wildcard("scripts / demo/**/*.exs")

    Enum.filter(demo_scripts, fn script_path ->
      test_path = convert_to_test_path(script_path)
      not File.exists?(test_path)
    end)
  end

  @spec convert_to_test_path(term()) :: term()
  defp convert_to_test_path(script_path) do
    script_path
    |> String.replace("scripts / demo/", "test / demo/")
    |> String.replace(".exs", "_test.exs")
  end

  @spec extract_affected_modules(term()) :: term()
  defp extract_affected_modules(missing_tests) do
