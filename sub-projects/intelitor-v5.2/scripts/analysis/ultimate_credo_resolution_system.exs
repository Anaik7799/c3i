  defp analyze_compilation_errors do
    Logger.info("🔨 Analyzing compilation errors and warnings...")

    case System.cmd("mix", ["compile", "--warnings-as-errors"], stderr_to_stdout: true) do
      {output, exit_code} ->
        lines = String.split(output, "\n", trim: true)

        # Parse compilation output
        errors = extract_compilation_errors(lines)
        warnings = extract_compilation_warnings(lines)

        %{
          status: if(exit_code == 0, do: "success", else: "failed"),
          exit_code: exit_code,
          error_count: length(errors),
          warning_count: length(warnings),
          errors: errors,
          warnings: warnings,
          raw_output: output
        }
    end
  end

  defp extract_compilation_errors(lines) do
    lines
    |> Enum.filter(&String.contains?(&1, "error:"))
    |> Enum.map(&parse_compilation_issue(&1, "error"))
    |> Enum.reject(&is_nil/1)
  end

  defp extract_compilation_warnings(lines) do
    lines
    |> Enum.filter(&String.contains?(&1, "warning:"))
    |> Enum.map(&parse_compilation_issue(&1, "warning"))
    |> Enum.reject(&is_nil/1)
  end

  defp parse_compilation_issue(line, type) do
    # Extract file, line, and message from compilation output
    case Regex.run(~r/^\s*([^:]+):(\d+):\s*(.+)$/, line) do
      [_, file, line_num, message] ->
        %{
          type: type,
          file: String.trim(file),
          line: safe_parse_integer(line_num),
          message: String.trim(message),
          priority: if(type == "error", do: "critical", else: "high")
        }

      _ ->
        # Try alternative pattern
        if String.contains?(line, type) do
          %{
            type: type,
            file: "unknown",
            line: 0,
            message: String.trim(line),
            priority: if(type == "error", do: "critical", else: "high")
          }
        else
          nil
        end
    end
  end

