    # Get Credo-related phases (phases 2, 3, 4)
    credo_phases = Enum.filter(execution_plan.phases, &(&1.phase > 1))

    all_results = []

    Enum.reduce(credo_phases, all_results, fn phase, acc_results ->
      Logger.info("📋 Processing Phase #{phase.phase}: #{phase.name}")

      if length(phase.issues) == 0 do
        Logger.info("✅ No issues in phase #{phase.phase} - skipping")
        acc_results
      else
        phase_results =
          phase.issues
          |> Enum.chunk_every(phase.batch_size)
          |> Enum.with_index(1)
          |> Enum.map(fn {issue_batch, batch_num} ->
            total_batches =
              div(length(phase.issues), phase.batch_size) +
                if rem(length(phase.issues), phase.batch_size) > 0, do: 1, else: 0

            progress = 40 + (phase.phase - 1) * 10 + batch_num * 10 / total_batches

            update_progress(
              progress_pid,
              trunc(progress),
              "Phase #{phase.phase} Batch #{batch_num}/#{total_batches} - #{length(issue_batch)} issues"
            )

            process_credo_issue_batch(issue_batch, phase.phase, batch_num)
          end)

        acc_results ++ phase_results
      end
    end)
  end

