  defp apply_unused_code_fixes_to_file(file, issues) do
    case File.read(file) do
      {:ok, content} ->
        _updated_content =
          Enum.reduce(issues, _content, fn issue, acc ->
            apply_unused_code_fix(acc, issue)
          end)

        if updated_content != content do
          File.write!(file, updated_content)
          {:ok, length(issues)}
        else
          {:ok, 0}
        end

      {:error, reason} ->
        {:error, reason}
    end
  end

  defp apply_unused_code_fix(content, issue) do
    # Extract variable name and apply prefix if needed
    case Regex.run(~r/variable "([^"]+)" is unused/, issue.message) do
      [_, var_name] ->
        if not String.starts_with?(var_name, "_") do
          Regex.replace(~r/\b#{Regex.escape(var_name)}\b/, content, "_#{var_name}")
        else
          content
        end

      _ ->
        content
    end
  end

