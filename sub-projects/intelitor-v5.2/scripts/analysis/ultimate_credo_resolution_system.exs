  defp search_for_similar_patterns(patterns) do
    # Search entire codebase for similar patterns
    similar_issues = []

    # Get all Elixir files
    all_files = Path.wildcard("lib/**/*.ex") ++ Path.wildcard("test/**/*.ex")

    Enum.each(patterns, fn pattern ->
      case pattern.type do
        "documentation_fix" ->
          # Find files missing @moduledoc
          files_needing_docs = find_files_missing_moduledoc(all_files)

          similar_issues =
            similar_issues ++
              Enum.map(files_needing_docs, fn file ->
                %{type: "missing_moduledoc", file: file, pattern: pattern}
              end)

        "unused_variable_fix" ->
          # Find additional unused variables
          files_with_unused_vars = find_files_with_unused_variables(all_files)

          similar_issues =
            similar_issues ++
              Enum.map(files_with_unused_vars, fn file ->
                %{type: "unused_variables", file: file, pattern: pattern}
              end)

        _ ->
          :ok
      end
    end)

    similar_issues
  end

  defp find_files_missing_moduledoc(files) do
    files
    |> Enum.filter(fn file ->
      case File.read(file) do
        {:ok, content} ->
          String.contains?(content, "defmodule") and not String.contains?(content, "@moduledoc")

        {:error, _} ->
          false
      end
    end)
  end

  defp find_files_with_unused_variables(files) do
    # Simple heuristic - look for common unused variable patterns
    files
    |> Enum.filter(fn file ->
      case File.read(file) do
        {:ok, content} ->
          # Look for variables that might be unused (basic pattern matching)
          Regex.match?(~r/\b[a-z_][a-zA-Z0-9_]*\s*=\s*[^=]/, content)

        {:error, _} ->
          false
      end
    end)
    # Limit to avoid processing too many files
    |> Enum.take(10)
  end

