  defp resolve_compilation_errors(execution_plan, progress_pid) do
    Logger.info("🔧 Resolving compilation errors with systematic approach...")

    # Find critical phase (compilation errors)
    critical_phase = Enum.find(execution_plan.phases, &(&1.phase == 1))

    if length(critical_phase.issues) == 0 do
      Logger.info("✅ No compilation errors found - proceeding to next phase")
      %{status: "no_errors", fixes_applied: 0}
    else
      Logger.info(
        "🚨 Found #{length(critical_phase.issues)} compilation errors - applying systematic fixes"
      )

      # Process compilation errors in batches
      results =
        critical_phase.issues
        |> Enum.chunk_every(critical_phase.batch_size)
        |> Enum.with_index(1)
        |> Enum.map(fn {error_batch, batch_num} ->
          progress =
            15 + batch_num * 15 / div(length(critical_phase.issues), critical_phase.batch_size)

          update_progress(
            progress_pid,
            trunc(progress),
            "Fixing compilation errors batch #{batch_num} (#{length(error_batch)} errors)"
          )

          fix_compilation_error_batch(error_batch, batch_num)
        end)

      successful_fixes = Enum.count(results, &(&1.status == "success"))
      total_fixes = Enum.sum(Enum.map(results, & &1.fixes_applied))

      Logger.info("📊 Compilation error resolution:")
      Logger.info("  - Successful batches: #{successful_fixes}/#{length(results)}")
      Logger.info("  - Total fixes applied: #{total_fixes}")

      %{
        status: if(successful_fixes > 0, do: "partial_success", else: "no_fixes"),
        fixes_applied: total_fixes,
        successful_batches: successful_fixes,
        batch_results: results
      }
    end
  end

  defp fix_compilation_error_batch(errors, batch_num) do
    Logger.info("🔧 Processing compilation error batch #{batch_num} (#{length(errors)} errors)")

    # Group errors by file for efficient processing
    errors_by_file = Enum.group_by(errors, & &1.file)

    # Apply fixes based on error patterns
    fixes_applied =
      Enum.reduce(errors_by_file, 0, fn {file, file_errors}, acc ->
        case apply_compilation_fixes_to_file(file, file_errors) do
          {:ok, count} ->
            Logger.info("✅ Applied #{count} fixes to #{file}")
            acc + count

          {:error, reason} ->
            Logger.warning("⚠️ Failed to fix errors in #{file}: #{reason}")
            acc
        end
      end)

    %{
      batch_number: batch_num,
      status: if(fixes_applied > 0, do: "success", else: "no_fixes"),
      fixes_applied: fixes_applied,
      errors_processed: length(errors)
    }
  end

  defp apply_compilation_fixes_to_file(file, errors) do
    case File.read(file) do
      {:ok, content} ->
        # Apply systematic fixes based on error types
        _updated_content =
          Enum.reduce(errors, _content, fn error, acc_content ->
            apply_single_compilation_fix(acc_content, error)
          end)

        if updated_content != content do
          case File.write(file, updated_content) do
            :ok ->
              {:ok, length(errors)}

            {:error, reason} ->
              {:error, "Write failed: #{reason}"}
          end
        else
          # No changes needed
          {:ok, 0}
        end

      {:error, reason} ->
        {:error, "Read failed: #{reason}"}
    end
  end

  defp apply_single_compilation_fix(content, error) do
    msg = error.message

    cond do
      String.contains?(msg, "undefined variable") ->
        fix_undefined_variable(content, error)

      String.contains?(msg, "variable") and String.contains?(msg, "unused") ->
        fix_unused_variable(content, error)

      String.contains?(msg, "underscored variable") ->
        fix_underscored_variable_misuse(content, error)

      String.contains?(msg, "redefining module") ->
        fix_module_redefinition(content, error)

      true ->
        # Log unknown error type for future pattern development
        Logger.info("🔍 Unknown compilation error pattern: #{error.message}")
        content
    end
  end

  defp fix_undefined_variable(content, error) do
    # Extract variable name from error message
    case Regex.run(~r/undefined variable "([^"]+)"/, error.message) do
      [_, var_name] ->
        # Try to fix common undefined variable patterns
        content
        |> String.replace("#{var_name} =", "_#{var_name} =")
        |> String.replace("#{var_name},", "_#{var_name},")

      _ ->
        content
    end
  end

  defp fix_unused_variable(content, error) do
    # Extract variable name from error message
    case Regex.run(~r/variable "([^"]+)" is unused/, error.message) do
      [_, var_name] ->
        # Prefix with underscore to indicate intentionally unused
        if not String.starts_with?(var_name, "_") do
          Regex.replace(~r/\b#{Regex.escape(var_name)}\b/, content, "_#{var_name}")
        else
          content
        end

      _ ->
        content
    end
  end

  defp fix_underscored_variable_misuse(content, error) do
    # Extract variable name from error message
    case Regex.run(~r/underscored variable "([^"]+)" is used/, error.message) do
      [_, var_name] ->
        # Remove underscore prefix when variable is actually used
        if String.starts_with?(var_name, "_") do
          clean_var_name = String.slice(var_name, 1..-1)
          Regex.replace(~r/\b#{Regex.escape(var_name)}\b/, content, clean_var_name)
        else
          content
        end

      _ ->
        content
    end
  end

  defp fix_module_redefinition(content, error) do
