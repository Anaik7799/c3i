  defp fix_documentation_issues(doc_issues) do
    successful_fixes = 0

    # Group by file for efficient processing
    issues_by_file = Enum.group_by(doc_issues, & &1.filename)

    Enum.each(issues_by_file, fn {file, file_issues} ->
      case add_missing_documentation(file, file_issues) do
        {:ok, count} ->
          successful_fixes = successful_fixes + count
          Logger.info("✅ Added documentation to #{file}")

        {:error, reason} ->
          Logger.warning("⚠️ Failed to add documentation to #{file}: #{reason}")
      end
    end)

    %{count: successful_fixes, files_processed: map_size(issues_by_file)}
  end

  defp add_missing_documentation(file, issues) do
    case File.read(file) do
      {:ok, content} ->
        # Check if @moduledoc is needed
        needs_moduledoc =
          Enum.any?(issues, fn issue ->
            String.contains?(issue.message, "moduledoc")
          end)

        updated_content =
          if needs_moduledoc and not String.contains?(content, "@moduledoc") do
            add_moduledoc_to_content(content, file)
          else
            content
          end

        # Add @spec to functions if needed
        final_content = add_missing_specs(updated_content, issues)

        if final_content != content do
          File.write!(file, final_content)

          {:ok,
           Enum.count(
             issues,
             &(String.contains?(&1.message, "moduledoc") or String.contains?(&1.message, "@spec"))
           )}
        else
          {:ok, 0}
        end

      {:error, reason} ->
        {:error, reason}
    end
  end

  defp add_moduledoc_to_content(content, file) do
    module_name = extract_module_name_from_content(content, file)
    moduledoc = generate_contextual_moduledoc(module_name, file)

    case Regex.run(~r/defmodule\s+[\w\.]+\s+do/, content) do
      [defmodule_line] ->
        String.replace(content, defmodule_line, "#{defmodule_line}\n#{moduledoc}", global: false)

      _ ->
        content
    end
  end

  defp extract_module_name_from_content(content, file) do
    case Regex.run(~r/defmodule\s+([\w\.]+)/, content) do
      [_, module_name] ->
