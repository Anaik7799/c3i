    plan = %{
      phase_1: %{
        name: "Directory Structure Creation",
        tasks: [
          "Create docs/ directory for all documentation",
          "Create scripts/ directory for utility scripts",
          "Create priv / scripts/ for Mix - integrated scripts",
          "Create logs/ directory for all log files",
          "Create backups/ directory for archived files",
          "Create test_results/ for test outputs"
        ]
      },
      phase_2: %{
        name: "File Organization",
        tasks: [
          "Move all *.md files to docs/ (except README.md)",
          "Move all test_*.exs to test/ or scripts / testing/",
          "Move all setup_*.exs to scripts / setup/",
          "Move all update_*.exs to scripts / maintenance/",
          "Move unified - 4.exs to scripts / installation/",
          "Archive all one - time execution scripts"
        ]
      },
      phase_3: %{
        name: "Mix Task Conversion",
        tasks: [
          "Create mix setup task from setup scripts",
          "Create mix test.coverage task from test scripts",
          "Create mix project.analyze task from analyzers",
          "Create mix unified.install task from unified - 4.exs",
          "Create mix docs.update task for documentation"
        ]
      },
      phase_4: %{
        name: "Documentation Update",
        tasks: [
          "Update CLAUDE.md with Mix project structure",
          "Update CLAUDE - CODEGEN.md with Mix conventions",
          "Update CLAUDE - TESTING.md with mix test usage",
          "Update CLAUDE - DEPLOYMENT.md with Mix releases",
          "Update CLAUDE - DEVELOPMENT.md with devenv.sh integration",
          "Create comprehensive README.md in root"
        ]
      },
      phase_5: %{
        name: "Integration & Cleanup",
        tasks: [
          "Integrate unified - 4.exs with Mix configuration",
          "Remove all execution artifacts (.json, .log)",
          "Update .gitignore for new structure",
          "Create project journal in docs / journal/",
          "Validate all scripts work from new locations",
          "Create cleanup Mix task for maintenance"
        ]
      }
    }

    Enum.eachplan, fn {phase, details} ->
      IO.puts("\n#{phase |> to_string( |> String.upcase()}: #{details.name}")
      IO.puts(String.duplicate("-", 50))

      Enum.with_indexdetails.tasks, 1 |> Enum.each(fn {task, idx} ->
        IO.puts("  #{idx}. #{task}")
      end)
    end)

    generate_directory_structure()
  end

