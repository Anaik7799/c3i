  defp run_credo_json_analysis do
    Logger.info("📊 Running Credo JSON analysis...")

    case System.cmd("mix", ["credo", "--format", "json"], stderr_to_stdout: true) do
      {output, _exit_code} ->
        parse_credo_json_output(output)
    end
  end

  defp run_credo_strict_analysis do
    Logger.info("📊 Running Credo strict analysis...")

    case System.cmd("mix", ["credo", "--strict", "--format", "flycheck"], stderr_to_stdout: true) do
      {output, _exit_code} ->
        parse_credo_strict_output(output)
    end
  end

  defp parse_credo_json_output(output) do
    try do
      # Clean output - remove any non-JSON lines
      cleaned_output =
        output
        |> String.split("\n")
        |> Enum.filter(&String.starts_with?(&1, "{"))
        |> Enum.join("\n")

      if String.length(cleaned_output) > 0 do
        case Jason.decode(cleaned_output) do
          {:ok, __data} ->
            issues = get_in(__data, ["issues"]) || []

            %{
              format: "json",
              issues: issues,
              issue_count: length(issues),
              raw_output: output
            }

          {:error, reason} ->
            Logger.warning(
              "⚠️ Failed to parse Credo JSON output: #{inspect(reason)}, using fallback"
            )

            parse_credo_text_fallback(output)
        end
      else
        Logger.warning("⚠️ No valid JSON found in Credo output, using fallback")
        parse_credo_text_fallback(output)
      end
    rescue
      error ->
        Logger.warning("⚠️ Error parsing Credo JSON: #{inspect(error)}")
        parse_credo_text_fallback(output)
    end
  end

  defp parse_credo_text_fallback(output) do
    # Parse Credo output as text format
    lines = String.split(output, "\n", trim: true)

    # Count issues from text output patterns
    issue_lines =
      Enum.filter(lines, fn line ->
        String.contains?(line, "│") and
          (String.contains?(line, ".ex:") or String.contains?(line, ".exs:"))
      end)

    %{format: "json_fallback", issues: [], issue_count: length(issue_lines), raw_output: output}
  end

  defp parse_credo_strict_output(output) do
    # Parse flycheck format output
    lines = String.split(output, "\n", trim: true)

    _issues =
      Enum.map(lines, fn line ->
        case String.split(line, ":", limit: 5) do
          [file, line_num, col, level, message] ->
            # Safe parsing with fallbacks
            safe_line_num =
              case Integer.parse(String.trim(line_num || "1")) do
                {num, _} -> num
                :error -> 1
              end

            safe_col =
              case Integer.parse(String.trim(col || "1")) do
                {num, _} -> num
                :error -> 1
              end

            %{
              filename: file,
              line_no: safe_line_num,
              column: safe_col,
              priority: map_credo_level_to_priority(level),
              message: String.trim(message || ""),
              category: extract_category_from_message(message || "")
            }

          _ ->
            nil
        end
      end)
      |> Enum.reject(&is_nil/1)

    %{
      format: "strict",
      issues: issues,
      issue_count: length(issues),
      raw_output: output
    }
  end

  defp map_credo_level_to_priority(level) do
    case String.trim(level) do
      "error" -> "high"
      "warning" -> "medium"
      "info" -> "low"
      _ -> "medium"
    end
  end

  defp extract_category_from_message(message) do
    cond do
      String.contains?(message, "moduledoc") -> "documentation"
      String.contains?(message, "doc") -> "documentation"
      String.contains?(message, "name") -> "naming"
      String.contains?(message, "complex") -> "complexity"
      String.contains?(message, "readable") -> "readability"
      String.contains?(message, "performance") -> "performance"
      true -> "general"
    end
  end

  defp combine_credo_analyses(json_analysis, strict_analysis) do
    # Combine issues from both analyses
    all_issues = json_analysis.issues ++ strict_analysis.issues

    # Remove duplicates and categorize
    unique_issues = deduplicate_issues(all_issues)

    # Categorize by priority
