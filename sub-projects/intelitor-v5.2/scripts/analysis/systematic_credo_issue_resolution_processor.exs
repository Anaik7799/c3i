    medium_priority = Enum.filter(unique_issues, &(&1.priority == "medium"))
    low_priority = Enum.filter(unique_issues, &(&1.priority == "low"))

    %{
      total_issues: length(unique_issues),
      high_priority_count: length(high_priority),
      medium_priority_count: length(medium_priority),
      low_priority_count: length(low_priority),
      high_priority_issues: high_priority,
      medium_priority_issues: medium_priority,
      low_priority_issues: low_priority,
      all_issues: unique_issues,
      json_analysis: json_analysis,
      strict_analysis: strict_analysis
    }
  end

  defp deduplicate_issues(issues) do
    # Simple deduplication based on file, line, and message
    issues
    |> Enum.uniq_by(fn issue ->
      {
        Map.get(issue, :filename, Map.get(issue, "filename", "")),
        Map.get(issue, :line_no, Map.get(issue, "line_no", 0)),
        Map.get(issue, :message, Map.get(issue, "message", ""))
      }
    end)
  end

