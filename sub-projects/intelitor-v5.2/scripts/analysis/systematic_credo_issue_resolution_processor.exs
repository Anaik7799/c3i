  defp fix_documentation_issues(classified_issues) do
    Logger.info("🔧 Fixing documentation issues...")

    documentation_issues = classified_issues.documentation_pattern.issues

    if length(documentation_issues) == 0 do
      Logger.info("✅ No documentation issues found")
      %{status: "no_issues", fixed_count: 0, issues_processed: 0}
    else
      # Group documentation issues by file
      issues_by_file =
        Enum.group_by(documentation_issues, fn issue ->
          Map.get(issue, :filename, Map.get(issue, "filename", ""))
        end)

      _results =
        Enum.map(issues_by_file, fn {file, file_issues} ->
          fix_file_documentation_issues(file, file_issues)
        end)

      successful_fixes = Enum.count(results, &(&1.status == "success"))
      total_fixes_attempted = length(results)

      Logger.info(
        "📊 Documentation fixes: #{successful_fixes}/#{total_fixes_attempted} files processed"
      )

      %{
        status: if(successful_fixes > 0, do: "partial", else: "no_fixes"),
        fixed_count: successful_fixes,
        issues_processed: length(documentation_issues),
        file_results: results
      }
    end
  end

  defp fix_file_documentation_issues(file, issues) do
    try do
      case File.read(file) do
        {:ok, content} ->
          # Check if file needs moduledoc
          needs_moduledoc =
            Enum.any?(issues, fn issue ->
              message = Map.get(issue, :message, Map.get(issue, "message", ""))
              String.contains?(message, "moduledoc")
            end)

          if needs_moduledoc and not String.contains?(content, "@moduledoc") do
            # Add moduledoc after defmodule line
            updated_content = add_moduledoc_to_file(content, file)
            File.write!(file, updated_content)
            Logger.info("✅ Added @moduledoc to #{file}")
            %{file: file, status: "success", fix_type: "added_moduledoc"}
          else
            %{file: file, status: "no_fix_needed", fix_type: "already_documented"}
          end

        {:error, reason} ->
          Logger.warning("⚠️ Could not read file #{file}: #{reason}")
          %{file: file, status: "error", error: reason}
      end
    rescue
      error ->
        Logger.warning("⚠️ Error fixing documentation in #{file}: #{inspect(error)}")
        %{file: file, status: "error", error: inspect(error)}
    end
  end

  defp add_moduledoc_to_file(content, file) do
    # Extract module name for appropriate documentation
    module_name = extract_module_name_from_file(file)

    # Create appropriate moduledoc
    moduledoc = generate_moduledoc_for_module(module_name, file)

    # Add moduledoc after defmodule line
    case Regex.run(~r/defmodule\s+[\w\.]+\s+do/, content) do
      [defmodule_line] ->
        String.replace(content, defmodule_line, "#{defmodule_line}\n#{moduledoc}", global: false)

      _ ->
        # If no defmodule found, return original content
        content
    end
  end

  defp extract_module_name_from_file(file) do
    case File.read(file) do
      {:ok, content} ->
        case Regex.run(~r/defmodule\s+([\w\.]+)/, content) do
          [_, module_name] ->
            module_name

          _ ->
            Path.basename(file, ".ex")
            |> Macro.camelize()
        end

      {:error, _} ->
        Path.basename(file, ".ex")
        |> Macro.camelize()
    end
  end

  defp generate_moduledoc_for_module(module_name, file) do
    # Generate __context-appropriate moduledoc
    doc_content =
      cond do
        String.contains?(file, "test/") ->
          "Test module for #{module_name} functionality and behavior verification."

        String.contains?(file, "controller") ->
          "Phoenix controller handling HTTP __requests and responses for #{module_name} operations."

        String.contains?(file, "live") ->
          "Phoenix LiveView module providing real-time __user interface for #{module_name}."

        String.contains?(file, "schema") ->
          "Database schema definition and __data structure for #{module_name}."

        String.contains?(module_name, "Test") ->
          "Comprehensive test suite for #{String.replace(module_name, "Test", "")} functionality."

        true ->
          "#{module_name} module providing core functionality and business logic implementation."
      end

    "  @moduledoc \"\"\"\n  #{doc_content}\n  \"\"\""
  end

