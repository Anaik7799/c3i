  defp apply_line_length_pattern, do: 0

  defp perform_validation_checkpoint do
    {output, exit_code} =
      System.cmd("mix", ["compile", "--warnings-as-errors"], stderr_to_stdout: true, cd: ".")

    error_count =
      output
