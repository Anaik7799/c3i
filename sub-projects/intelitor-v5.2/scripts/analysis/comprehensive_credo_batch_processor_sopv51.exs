      |> Enum.count(&String.contains?(&1, "error:"))

    warning_count =
      output
      |> String.split("\n")
      |> Enum.count(&String.contains?(&1, "warning:"))

    %{
      error_count: error_count,
      warning_count: warning_count,
      compilation_successful: exit_code == 0
    }
  end

  defp perform_comprehensive_testing do
    # Run unit tests
    {test_output, test_exit_code} =
      System.cmd("mix", ["test", "--only", "unit"], stderr_to_stdout: true, cd: ".")

    # Check TDG compliance
    {tdg_output, tdg_exit_code} =
      System.cmd("mix", ["test", "--only", "tdg"], stderr_to_stdout: true, cd: ".")

    # Extract coverage information
    coverage_match = Regex.run(~r/(\d+\.\d+)% coverage/, test_output)
    coverage = if coverage_match, do: String.to_float(List.last(coverage_match)), else: 0.0

    %{
      test_status: if(test_exit_code == 0, do: "passed", else: "failed"),
      tdg_status: if(tdg_exit_code == 0, do: "passed", else: "failed"),
      coverage: coverage
    }
  end

  defp perform_five_level_rca(fixes_history) do
    # TPS 5-Level Root Cause Analysis
    recent_fixes = Enum.take(fixes_history, -@rca_interval)

    patterns =
      recent_fixes
      |> Enum.group_by(& &1.fix_applied)
      |> Enum.map(fn {fix_type, fixes} ->
        %{
          fix_type: fix_type,
          f__requency: length(fixes),
          success_rate: Enum.count(fixes, & &1.success) / length(fixes) * 100
        }
      end)

    %{
      root_causes_identified: length(patterns),
      patterns: patterns,
      recommendations: generate_rca_recommendations(patterns)
    }
  end

  defp generate_rca_recommendations(patterns) do
    patterns
    |> Enum.filter(&(&1.success_rate < 80))
    |> Enum.map(
      &"Improve #{&1.fix_type} fix strategy (#{Float.round(&1.success_rate, 1)}% success)"
    )
  end

