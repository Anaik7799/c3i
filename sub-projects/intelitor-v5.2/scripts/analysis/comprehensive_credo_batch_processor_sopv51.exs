  defp analyze_credo_issues_comprehensive do
    log_progress("🔍 Starting comprehensive Credo analysis...")

    # Get Credo issues with categorization
    {credo_output, _} =
      System.cmd("mix", ["credo", "list", "--format=oneline"], stderr_to_stdout: true, cd: ".")

    issues =
      String.split(credo_output, "\n")
      |> Enum.filter(&(String.length(&1) > 10))
      |> Enum.map(&parse_credo_issue_line/1)
      |> Enum.filter(& &1)

    # Categorize issues by type and priority
    categorized_issues = categorize_credo_issues(issues)

    log_progress("📊 Credo Analysis Complete: #{length(issues)} issues categorized")
    log_progress("📊 Categories: #{inspect(Map.keys(categorized_issues))}")

    %{
      total_issues: length(issues),
      issues: issues,
      categorized: categorized_issues,
      # Ensure 500+ per batch
      batch_size: max(500, div(length(issues), 10))
    }
  end

  defp parse_credo_issue_line(line) do
    # Parse Credo oneline format: [L] → file:line:column category: message
    case Regex.run(~r/\[(.)\] → (.+?):(\d+):(\d+) (.+?): (.+)/, line) do
      [_, priority, file, line_no, _col, category, message] ->
        %{
          priority: priority,
          file: file,
          line: String.to_integer(line_no),
          category: category,
          message: message,
          original_line: line
        }

      _ ->
        nil
    end
  end

  defp categorize_credo_issues(issues) do
    issues
    |> Enum.group_by(& &1.category)
    |> Map.new(fn {category, category_issues} ->
      {category,
       %{
         count: length(category_issues),
         issues: category_issues,
         priority: determine_category_priority(category),
         fix_pattern: determine_fix_pattern(category)
       }}
    end)
  end

  defp determine_category_priority(category) do
    case category do
      "readability" -> :high
      "refactor" -> :high
      "warning" -> :critical
      "consistency" -> :medium
      "design" -> :medium
      _ -> :low
    end
  end

  defp determine_fix_pattern(category) do
    case category do
      "readability" -> :systematic_readability
      "refactor" -> :systematic_refactor
      "warning" -> :immediate_fix
      "consistency" -> :pattern_consistency
      "design" -> :architectural_improvement
      _ -> :manual_review
    end
  end

  defp process_credo_batches_systematic(credo_analysis) do
    log_progress("🚀 Starting systematic batch processing...")

    # Create batches ensuring 500+ issues per batch
    batches = create_optimized_batches(credo_analysis.issues, credo_analysis.batch_size)

    log_progress(
      "📦 Created #{length(batches)} batches with #{credo_analysis.batch_size}+ issues each"
    )

    # Process each batch with comprehensive monitoring
    batch_results =
      batches
      |> Enum.with_index()
      |> Enum.map(fn {batch, index} ->
        process_single_credo_batch(batch, index + 1, length(batches))
      end)

    # Calculate overall results
    total_processed = Enum.sum(Enum.map(batch_results, &Map.get(&1, :issues_processed, 0)))
    total_fixed = Enum.sum(Enum.map(batch_results, &Map.get(&1, :successful_fixes, 0)))

    overall_success_rate =
      if total_processed > 0, do: total_fixed / total_processed * 100, else: 0.0

    log_progress(
      "✅ Batch Processing Complete: #{total_fixed}/#{total_processed} issues fixed (#{Float.round(overall_success_rate, 1)}%)"
    )

    %{
      batches_completed: length(batch_results),
      total_processed: total_processed,
      total_fixed: total_fixed,
      overall_success_rate: overall_success_rate,
      batch_results: batch_results
    }
  end

  defp create_optimized_batches(issues, batch_size) do
    # Group by file for better fix efficiency
    issues_by_file = Enum.group_by(issues, & &1.file)

    # Create batches respecting file boundaries when possible
    {batches, _} =
      issues_by_file
      |> Map.values()
      |> Enum.reduce({[], []}, fn file_issues, {completed_batches, current_batch} ->
        new_batch = current_batch ++ file_issues

        if length(new_batch) >= batch_size do
          {completed_batches ++ [new_batch], []}
        else
          {completed_batches, new_batch}
        end
      end)

    batches
  end

  defp process_single_credo_batch(batch, batch_number, total_batches) do
    log_progress(
      "📦 Processing Credo Batch #{batch_number}/#{total_batches}: #{length(batch)} issues"
    )

    batch_start_time = System.monotonic_time(:millisecond)
    changes_made = 0
    fixes_history = []

    # Process each issue in the batch
    results =
      batch
      |> Enum.with_index()
      |> Enum.map(fn {issue, index} ->
        result = process_single_credo_issue(issue, batch_number, index + 1)

        # Track changes for validation checkpoints
        changes_made = changes_made + if result.success, do: 1, else: 0
        fixes_history = fixes_history ++ [result]

        # Validation checkpoint every 50 changes
        if changes_made > 0 && rem(changes_made, @validation_interval) == 0 do
          log_progress(
            "🔍 Validation Checkpoint: #{changes_made} changes in batch #{batch_number}"
          )

          validation_result = perform_validation_checkpoint()

          log_progress(
            "📊 Validation: #{validation_result.error_count} errors, #{validation_result.warning_count} warnings"
          )

          # Unit and TDG testing
          test_result = perform_comprehensive_testing()

          log_progress(
            "🧪 Testing: #{test_result.test_status} - #{test_result.coverage}% coverage"
          )
        end

        # TPS 5-Level RCA every 10 fixes
        if changes_made > 0 && rem(changes_made, @rca_interval) == 0 do
          log_progress("🏭 TPS 5-Level RCA Analysis: #{changes_made} fixes completed")
          rca_result = perform_five_level_rca(fixes_history)
          log_progress("📋 RCA Result: #{rca_result.root_causes_identified} patterns identified")
        end

        result
      end)

    successful_fixes = Enum.count(results, & &1.success)
    batch_duration = System.monotonic_time(:millisecond) - batch_start_time

    result = %{
      batch_number: batch_number,
      issues_processed: length(batch),
      successful_fixes: successful_fixes,
      changes_made: changes_made,
      duration_ms: batch_duration,
      success_rate: Float.round(successful_fixes / length(batch) * 100, 1),
      fixes_history: fixes_history
    }

    log_progress(
      "✅ Batch #{batch_number} completed: #{result.successful_fixes}/#{length(batch)} fixes (#{result.success_rate}%)"
    )

    result
  end

  defp process_single_credo_issue(issue, batch_number, issue_number) do
    try do
      log_progress(
        "  🔧 B#{batch_number}.#{issue_number}: #{issue.category} in #{Path.basename(issue.file)}:#{issue.line}"
      )

      # Apply appropriate fix based on issue category
      fix_result =
        case issue.category do
          "readability" -> fix_readability_issue_systematic(issue)
          "refactor" -> fix_refactor_issue_systematic(issue)
          "warning" -> fix_warning_issue_systematic(issue)
          "consistency" -> fix_consistency_issue_systematic(issue)
          "design" -> fix_design_issue_systematic(issue)
          _ -> %{success: false, reason: "unsupported_category"}
        end

      %{
        issue: issue,
        success: fix_result.success,
        fix_applied: fix_result.fix_type || "none",
        changes_made: if(fix_result.success, do: 1, else: 0),
        batch_number: batch_number,
        issue_number: issue_number,
        timestamp: DateTime.utc_now()
      }
    rescue
      error ->
        log_progress("  ❌ Error processing issue: #{inspect(error)}")

        %{
          issue: issue,
          success: false,
          error: inspect(error),
          changes_made: 0,
          batch_number: batch_number,
          issue_number: issue_number
        }
    end
  end

  defp fix_readability_issue_systematic(issue) do
    # Systematic readability fixes
