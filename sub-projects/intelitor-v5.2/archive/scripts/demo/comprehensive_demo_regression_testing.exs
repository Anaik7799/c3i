    else
      IO.puts("⚠️ Some demo modes failed-investigation required")
    end

    results
  end

  defp execute_demo_mode(mode, index, options) do
    IO.puts("\n🎬 Demo Mode #{index}/16: #{mode.mode}")
    IO.puts("Description: #{mode.description}")
    IO.puts("Priority: #{mode.priority}")
    IO.puts("Command: mix demo --#{mode.mode}")

    start_time = System.monotonic_time(:millisecond)

    # Execute demo mode
    result = case System.cmd("mix", ["demo", "--#{mode.mode}"], stderr_to_stdout: true) do
      {output, 0} ->
        end_time = System.monotonic_time(:millisecond)
        execution_time = end_time-start_time

        IO.puts("✅ Demo mode '#{mode.mode}' completed successfully")
        IO.puts("⏱️ Execution time: #{execution_time}ms")

        %{mode: mode.mode, status: :success, execution_time: execution_time, output: output}

      {output, exit_code} ->
        end_time = System.monotonic_time(:millisecond)
        execution_time = end_time-start_time

        IO.puts("❌ Demo mode '#{mode.mode}' failed with exit code #{exit_code}")
        IO.puts("⏱️ Execution time: #{execution_time}ms")
        IO.puts("📝 Output (last 500 chars): #{String.slice(output, -500..-1)}")

        %{mode: mode.mode, status: :failure, execution_time: execution_time, output: output, exit_code: exit_code}
    end

    # Validate performance if successful
    if result.status == :success do
      validate_demo_mode_performance(mode, result)
    end

    result
  end

