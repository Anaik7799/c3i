  @spec apply_search(term(), term()) :: term()
  defp apply_search(query, search) do
    search_term = "%#{search}%"

    where(
      query,
      [item],
      ilike(item.name, ^search_term) or
        ilike(item.description, ^search_term)
    )
  end

